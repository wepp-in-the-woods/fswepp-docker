    <div class="instructions">
        <h4>Option 2: Add a Trusted Location through Excel's Trust Center</h4>
        <p>If you can't modify your Internet Options security settings, follow these steps to add a Trusted Location through Excel:</p>
        <ol>
            <li>Open Microsoft Excel.</li>
            <li>Click on the <strong>File</strong> tab located at the top-left corner.</li>
            <li>Choose <strong>Options</strong> from the menu list.</li>
            <li>In the Excel Options window, select <strong>Trust Center</strong> from the list on the left.</li>
            <li>Click on the <strong>Trust Center Settings...</strong> button.</li>
            <li>Choose <strong>Trusted Locations</strong> from the list on the left.</li>
            <li>Click on the <strong>Add new location...</strong> button.</li>
            <li>Browse and select the location where your Excel tool is saved.</li>
            <li>Ensure the "Subfolders of this location are also trusted" option is checked if you want to trust all subfolders within the chosen location.</li>
            <li>Click <strong>OK</strong> to confirm and add the location.</li>
            <li>Click <strong>OK</strong> again to close the Trust Center Settings.</li>
            <li>Finally, click <strong>OK</strong> to close the Excel Options window.</li>
        </ol>
