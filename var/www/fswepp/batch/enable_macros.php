
    Enable Macros
    <p>These workbooks require macros to load the input data and query wepp runs. Below are two options to enable macros.
        We recommend using <strong>Option 1</strong> if you have the necessary permissions to modify your Internet
        Options security settings. However, if you have a government computer, you might not have this capability, and
        you'll need to proceed with <strong>Option 2</strong>.</p>

    <div class="instructions">
        <h4>Option 1: Add to Trusted Sites</h4>
        <p>This method involves adding <code>forest.moscowfsl.wsu.edu</code> to your list of trusted sites through the Internet Options.</p>
        <ol>
            <li>Open the <strong>Control Panel</strong> on your computer.</li>
            <li>Select <strong>Network and Internet</strong>.</li>
            <li>Select <strong>Internet Options</strong>.</li>
            <li>Click on the <strong>Security</strong> tab.</li>
            <li>Select <strong>Trusted Sites</strong> (it has a green check mark icon).</li>
            <li>Click on the <strong>Sites</strong> button.</li>
            <li>Under "Add this website to the zone:", enter <code>forest.moscowfsl.wsu.edu</code> and click <strong>Add</strong>.</li>
            <li>Click <strong>Close</strong> and then <strong>OK</strong>.</li>
        </ol>
