			<h2 class="section">LONG-TERM SOIL PRODUCTIVITY (LTSP)</h2>
			<p>Protecting forest productivity is a goal of ethical forest management. For the Forest Service, U.S. Department of Agriculture, it is a legal obligation, too. Under Section 6(g) of the National Forest Management Act of 1976 (NFMA) the Secretary of Agriculture is required, through research and continuous monitoring, to ensure that management systems &quot;will not produce substantial and permanent impairment of the productivity of the land.&quot;</p>
			<h3>Land productivity</h3>
			<p>We define land productivity as the &quot;capacity of a given site to sustain plant growth.&quot; This capacity provides the underpinning of ecosystem health and sustainability. A loss of the capacity spells the loss of other resource and sustainability values as well.</p>
			<p>To help protect the land's productive capacity, the Forest Service is monitoring the impact of forest management practices on the soil.</p>
			<h3>Why monitor soil?<br />
Why not monitor the vegetation itself?</h3>
			<p>Although vegetation does reflect the plant growth capacity of a site, using vegetation to sense changes in productivity poses several problems. First, harvesting removes vegetation. Thus, nothing may be available to measure until a new forest has grown. And even if vegetation does remain, growth rates will be affected not only by changes in the soil, but also by changes in stocking. Also, vegetation grows slowly when a forest is very young, then more rapidly as it matures. At advanced ages, growth slows and finally stops. This means that forest growth at any particular moment may not reflect the forest's potential for growth when it is fully stocked and mature. Finally, forest growth in any given year is affected by climatic differences and pest outbreaks that may have little to do with the overall productive potential of the site.</p>
			<p>Soil is a more logical focus for monitoring. Along with climate and energy from the sun, soil sets the productive potential for a site. However, unlike climate and solar energy, the soil's productivity can be affected directly by management. If the soil's ability to supply air, water and nutrients to plant roots has been changed, then its ability to grow forest vegetation has been changed, too. The task is to identify and monitor the soil properties most vital to forest growth to see if long-term soil productivity has been seriously altered by management.</p>
			<p>To accomplish this task in response to NFMA, Forest Service Regions are developing threshold soil quality monitoring standards for detecting declines in potential soil productivity based on the best available information. Often, standards rest on professional judgment because research has not addressed the problem satisfactorily, To meet its legal and ethical charge, the Forest Service must develop accurate calibrations and effective soil monitoring standards.</p>
			<p>Answers are needed to the following questions: </p>
			<ul>
			  <li>What is the inherent carrying capacity of forest land for producing vegetation? </li>
			  <li>What are the key soil variables controlling this capacity? </li>
			  <li>What fundamental processes do key soil variables reflect? </li>
			</ul>
			<h2 class="section">THE NATIONAL LONG-TERM SOIL PRODUCTIVITY STUDY</h2>
			<p>Scientists from the research branch of the Forest Service and professionals from National Forests are working together in a cooperative national study to find answers to many questions concerning long-term soil productivity. Our main objectives are...</p>
	  	<ol>
    		<li>Quantifying the effects of soil disturbance on soil productivity</li>
				<li>Validating standards and methods for soil quality monitoring</li>
				<li>Understanding the fundamental relationships between soil properties, long-term productivity and forest management practices</li>
			</ol>
			<p><a href="http://forest.moscowfsl.wsu.edu/smp/ltsp/">The National Long-Term Soil Productivity study</a> is a research program that will meet these objectives. Findings will show us how changes in site organic matter and soil porosity affect fundamental site processes controlling forest health, productivity, and sustainability.</p>
			<h3>A National Partnership</h3>
			<p>LTSP research focuses on the joint roles of soil porosity and site organic matter and their effects on the site processes that control productivity. The study will be carried out through a standard series of experimental treatments designed to create varying degrees of stress and to provide measures of biological response and soil recovery. Work will center on National Forest lands covering major forest and soil types across the nation. The experimental sites are protected from conflicting uses and are dedicated to long-term research.</p>
			<p>Results from this cooperative research will provide credible responses to many challenges created through public review of Forest Service Land Management Plans and timber sales.</p>
			<p><a href="LTSP_sites.php">A national network of LTSP research sites</a> will provide the scientific basis for validating soil quality standards established by the Forest Service. The study will yield research opportunities of unusual scope and significance, foster close cooperation within the agency between Forest Service Research and the National Forest System, and open doors for important work with <a href="collaborators.php">universities and industry colleagues</a>.</p>
			<p>LTSP research furthers our understanding of sustainable productivity and how soil disturbance affects</p>
			<ul>
			  <li>Photosynthesis and carbon allocation </li>
			  <li>Water use </li>
			  <li>Nutrient use </li>
			  <li>Resistance to pests </li>
			  <li>Fundamental soil productivity </li>
			  <li>Biotic diversity </li>
			  <li>Other processes </li>
			</ul>
			<p>LTSP supports soil management efforts by 
			<ul>
			  <li>Calibrating changes in soil properties against:
			  	<ol>
						<li>Stand productivity (trees only)</li>
						<li>Total productivity (all forest vegetation)</li>
					</ol>
				</li>
			  <li>Evaluating and improving field monitoring methods</li>
  			<li>Finding ways to extend results to many other sites</li>
			</ul>
