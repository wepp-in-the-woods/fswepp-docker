<p><font size=-1>Fine scale sampling is most useful for detecting local
details of soil quality with a relatively high amount of resolution. In
contrast, broad-scale sampling is most useful for detecting landscape level
patterns of soil quality with less resolution. Frequent sampling of soil
quality indicator variables allows rapid changes in soil quality to be detected
and is thus useful for monitoring short-term variations in soil quality.
In contrast, long-term trends in soil quality can be monitored with less
frequent samplings. In general, the larger the size of the land area on
which we are interested in measuring soil quality, the longer we will need
to monitor it in order to observe the dynamics of soil quality change (Weins
1989).<br />
<br />
<br />
</font>Scale influences data interpretation and decision making <br />
<br />
<font size=-1>Scale influences the way we can use our data to manage land
and formulate policy by affecting the generalizations we are able to make.
If we extrapolate estimates of soil quality beyond the spatial or temporal
boundaries of our sampling we are assuming &quot;a scale-independent uniformitarianism
of patterns and processes (which we know to be false)&quot; (Weins 1989).
This means that samples collected at a fine spatial or temporal scale may
not accurately predict general patterns discernible at broader scales.<br />
<br />
The problem of extrapolating soil quality data to large spatial scales or
time periods may be particularly troublesome because management programs
or remediation actions will likely be applied to much larger areas of land
than the area sampled for soil quality. That is, we tend to sample at much
smaller spatial and temporal scales than we can manage for. If large-scale
management or remediation operations are based on soil quality information
derived from small-sized samples, we may be misled about the statistical
characteristics of soil quality at the scale of the management or remediation
unit, especially for variables with strong skewed distributions (Isaaks
and Srivastava 1989). However, if we rely upon composite samples, we combine
many small samples collected from locations of high and low concentration,
decreasing our ability to detect variability and the proportion and quantity
of samples the will be above (or below) some critical threshold (Weins).
Evaluation of soil quality will also be influenced by patterns of sample
collection especially if soil quality indicators are distributed over the
sampling area in a systematic gradient or a deterministic pattern related
to a factor such as slope or water movement.<br />
<br />
The spatial and temporal scale of soil quality data must also be considered
when contrasting soil quality between sites. Because comparisons between
a small field plot and a large watershed; or between hourly measurements
and annual values would not be particularly useful, comparisons of soil
quality should be made between land units of similar spatial and temporal
scales. Further, direct comparisons of soil quality between sites should
recognize possible inherent differences related to the spatial and temporal
history of each ecosystem. for example, when we compare an agricultural
site to a natural or undisturbed site we compare soils with contrasting
histories, representing conceptually different spatial and temporal scales.
The natural soil is often more representative of near-equilibrium conditions
and landscape level processes (long-term, broad-spatial scale). Conversely,
agriculturally managed soils are typically representative of non-equilibrium,
small-scale conditions because they are actively managed by humans for relatively
short-term productivity. Thus, commonly used concepts like efficiency, may
not mean the same thing in an agricultural system as in natural systems
and direct comparisons of actively managed agricultural soil to unmanaged,
natural soil may not be straight-forward (Granatstein and Bezdicek 1992).<br />
<br />
The spatial and temporal scale of soil quality measurements must thus be
coordinated with needs of decision makers to avoid confusion caused by measuring
soil quality at one scale, and managing it at another. Some of the soil
quality indicator variables now proposed may not prove useful for evaluating
soil quality at the scales of management and policy formation because they
reflect the perspectives and expertise of specialized soil scientists and
methodologies developed for short-term, small-scale research. This bias
is reflected by the observation that many proposed soil quality indicators
can change significantly in 1-3 years and exhibit a relatively high degree
of spatial variability (Doran and Parkin 1994, Table 1, table 3). To date,
policy makers and regulatory agencies have had only limited inputs in the
selection of soil quality indicators and design of a sampling strategy even
though they will be the ones to formulate and implement policy.</font></p>

