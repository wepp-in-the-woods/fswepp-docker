<p>Other scale related problems<br />
<br />
<font size=-1>A suite of variables has been identified to evaluate soil
quality. However, many important questions remain about how to interpret
the relationships between two or more individual variables in a fashion
that accounts for the productivity, environmental and health attributes
of soil quality. Many of the remaining problems relate to spatial and temporal
scales of our measurements or to our interpretations of soil quality.<br />
<br />
A basic issue is determining which soil indicators are most important at
different spatial and temporal scales. We know that some soil quality indicators,
such as mineralizable-N, will be more important for assessing one aspect
of soil quality such as productivity, than environmental quality or human
and animal health. Similarly, the relationship between soil variables and
soil quality may not be the same at all scales of study. Some soil quality
indicators will be better suited for assessing it at the regional scale.
But which ones? Field-scale patterns of soil quality may be most related
to short-term management history or small-scale soil characteristics and
local topography. Conversely, landscape-scale patterns of soil quality may
be most related to long term patterns of climate, parent material, landform
and vegetation.<br />
<br />
Another issue is exactly where and when soil quality should be measured.
We must take care that samples are spatially and temporally related to the
functional attribute of soil quality that they are meant to represent. In
other words, those soil quality measurements related to productivity must
be collected from locations that actually influence the crop at times related
to plant development and management. Soil quality measurements concentrated
at the soil surface might not reflect the conditions in the rooting zone.
Further, soil quality might be judged as good for most of the growing season
but be unacceptable during critical stages of crop development. On the other
hand, evaluating soil quality for environmental attributes might require
measurements collected from the soil surface in order to estimate potential
erosion or runoff of materials into surface water. Soil characteristics
or depths below the rooting zone might be most important for determining
the ability of soil to filter out pollution or serve as a water purifier.<br />
<br />
A third problem is how to account for complex interactions between several
soil quality indicators operating at different spatial and temporal scales.
For example, the potential for trace metal contamination of crops or water
is related not only to the amount of these materials present in the soil,
but also highly dependent on the nature of the soil and on the soil solution
pH. Besides pH, buffering capacity might be important when evaluating the
impacts of heavy metal concentrations in the soil. thus, evaluating soil
quality with respect to metal contamination would require the measurement
of several variables and assessment of their interactions.<br />
<br />
Another set of issues relates to how we view change of soil quality. Our
basic need is to determine whether significant change has occurred and the
rate of change. However, to detect change of soil quality requires that
we view soil ecosystems from an appropriate spatial and temporal scale.
Some soil quality indicators such as pH, mineralizable-N or soil microbial
biomass can change rapidly; making them useful for determining fine-scale
variations in soil quality during the course of a growing season but less
useful for monitoring large-scale trends. Other soil quality indicators
such as soil texture or organic matter change much more slowly making them
unsuitable for assessing changes in seasonal crop productivity but appropriate
for monitoring long-term trends in yield potential or for assessing changes
in environmental or health attributes. One important implication of selecting
soil quality indicators that change rapidly is that they will need to be
measured often in order to distinguish between trends and very small scale
variability.</font></p>

<p><font size=-1><br />
We also need to understand how changes in individual soil quality indicators
relate to changes in overall soil quality. Will an increase or decrease
in a particular soil quality indicator be associated with a corresponding
linear change in soil quality? Will a change in soil quality observed at
a fine scale, be a good predictor of change in soil quality at a larger
scale? Also important are the cumulative impacts of improvements or degradations
in soil quality. Small changes in soil quality may prove significant if
viewed on a large enough spatial scale or over a long enough period of time.
Finally, how should we evaluate soil quality when significant changes of
several soil quality indicators are detected, but in opposite directions?
In reality, soil quality is the net result of ongoing aggradation and degradation
processes that combine to influence plant productivity, environmental quality,
food safety and quality and the environment. The simultaneous interaction
of soil quality indicator variables will challenge our view of the structure
and function of the soil and deter us from defining soil quality based on
one or two narrow aspects of soils. For example, in the Pacific Northwest
wheat growing region, controlling soil erosion has been a main focus of
soils research and extension for the last 60 years. Little attention has
been given to the declining soil pH due to the use of ammonium based fertilizers
or soil compaction from use of heavy equipment. thus, soil quality in this
region may be viewed as improving from the perspective of erosion control
but declining with respect to pH and compaction. Under the new view of soil
quality, all processes and interactions that affect soil quality would be
considered in producing a net assessment.</font></p>

<p><font size=-1><br />
</font><b>Concluding remarks</b><font size=-1><br />
<br />
As a holistic or emergent property of the soil ecosystem, soil quality will
exhibit characteristics or behavior not predictable as a simple function
of the individual components (Odum 1983). thus the spatial and temporal
patterns of an integrative estimate of soil quality need not be correlated
to those of any individual soil variable. Some ecologists suggest that natural
phenomena possess inherent scales (Morris 1987; Carlile et al. 1989; Wiens
1989). However, sampling on &quot;natural&quot; scales to estimate soil
quality will be of little value if the resulting information does not fit
into a spatial and temporal framework useful to humans. <br />
<br />
Evaluating soil quality for specialized land uses like hazardous waste storage
will require detailed, small-scale, site specific information as well as
some measure of the reliability of the soil quality estimate. Such small-scale
estimates must also be evaluated within the context of the other relatively
large-scale phenomena like drainage or precipitation patterns. For a farmer,
concerned mainly with crop productivity, soil quality must be assessed on
the spatial scale less than or equal to the size of a single field during
a time period less than or equal to a single growing cycle. Farm management
decisions will require less than or equal to the entire farm unit within
the temporal context of annual yield cycles or rotation schedules. Environmental
monitoring will require that soil quality be assessed over large enough
land areas and for long enough periods of time to be able to identify trends
at the scale of a management unit. Integrative national environmental policy
may require soil quality estimates at scales comparable to those for air
and water quality. Such estimates may exceed local jurisdictional and natural
boundaries but might also be constrained to scales influenced by political
and economic cycles.<br />
<br />
A functional soil quality index can only be developed if we recognize that
quality is a relative term, dependent upon the intended function of the
soil and the spatial and temporal scale of reference.</font></p>

