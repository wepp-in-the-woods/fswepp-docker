			<h3><a name="overview"></a>Overview</h3>
			<p>Soil compaction presents a physical barrier to root growth, inhibiting seedling and mature tree growth. Compaction may also reduce tree growth by reducing oxygenation of the roots, preventing adequate water movement, and limiting mycorrhizal growth (Froehlich and McNabb 1984).</p>
			<p>Factors affecting soil compactability include:</p>
			<ul>
				<li>Soil moisture</li>
				<li>Soil texture</li>
				<li>Soil bulk density</li>
			</ul>
			<p>No quantitative measure of soil compaction exists. Instead, approximations are substituted for direct measurement.</p>
			<p>A 15% increase (20% in volcanic ash soils) in natural bulk density or a 50% reduction in the infiltration rate is considered detrimental.</p>
			<h3><a name="guidelines"></a>Guidelines</h3>
			<h3><a name="measurement"></a>Measurement/Approximations</h3>
			<h4>Bulk Density</h4>
			<p>Bulk density is used as an indicator of soil compaction. An increase in bulk density correlates to decreases in soil porosity, air exchange, root penetration, infiltration, and permeability.</p>
			<p>Bulk density measurements are customarily taken 1 inch below the mineral soil surface. Generally, samples are collected, dried and converted to a mass per volume (g per cubic cm).</p>
			<p>If the change (increase) in bulk density after an activity or event is greater than 15%, then the soil has been detrimentally compacted.</p>
			<h4>Soil Penetration</h4>
			<p>Soil penetration</B> is another method used to determine changes in soil condition. Clayton et al. (1987) presents a subjective, but effective, method for estimating compaction from the resistance of pushing a shovel into the soil. A <strong>cone penetrometer</strong>, when pushed into the soil at a calibrated constant rate of speed, displays a digital estimate of bulk density throughout a soil profile, based on resistance to insertion.</p>
			<h4>Infiltration Rates</h4>
			<p>Measuring infiltration rates may be appropriate when monitoring rangelands since the compacted layer is often too thin to measure using core sampling techniques. If the infiltration rate is reduced by 50%, the soil has been detrimentally compacted.</p>
			<p><br />Note that each of these methods requires knowledge (measurement) of the specific parameter <strong>before</strong> the activity occurs.</p>
