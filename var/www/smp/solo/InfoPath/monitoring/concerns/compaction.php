<P><B>Compaction</B></P>

<P>Soil compaction presents a physical barrier to root growth, inhibiting
seedling and mature tree growth. Compaction may also reduce tree growth
by reducing oxygenation of the roots, preventing adequate water movement,
and limiting mycorrhizal growth (Froehlich and McNabb 1984).</P>

<P>Factors affecting soil compactability include <BR>
soil moisture,<BR>
soil texture, and<BR>
soil bulk density.</P>

<P>No quantitative measure of soil compaction exists. Instead, approximations
are substituted for direct measurement.</P>

<P>A 15% increase (20% in volcanic ash soils) in natural bulk density or
a 50% reduction in the infiltration rate is considered detrimental.</P>

