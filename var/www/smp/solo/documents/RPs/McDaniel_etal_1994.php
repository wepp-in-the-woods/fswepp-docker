	<h3>Results and Discussion</h3>
	<p>The distribution of soil and forest climax community classifications along the Trout Creek climatic gradient are summarized in <a href="#fig2">Figure 2</a>. The approximate ranges of soil moisture and temperature regimes are also given. These regimes are based on data collected at the sites (Fosberg, unpublished data) and related studies in northern Idaho by Schauer (1976). Cryic and frigid soil temperature regimes are both characterized by a mean annual soil temperature &lt;8 degrees <span title="Celsius">C</span>, but the cryic regime has cooler summer temperatures (Soil Survey Staff 1992). Udic and xeric soil moisture regimes differ in the length of time that soil is dry during the summer months&#8212;xeric soils are subject to longer periods of dry conditions than are udic soils (Soil Survey Staff 1992). In the Trout Creek drainage, the change from cryic to frigid temperature regimes occurs somewhere within the <em class="ital">Tsuga heterophylla </em>zone. The change from the uric to xeric moisture regime appears to coincide with the change from the <em class="ital">T. heterophylla</em> series to the <em class="ital">Pseudotsuga menziesii</em> series.</p>

