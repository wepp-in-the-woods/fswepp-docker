	<p>The highest elevation sampling location, Site 112, is located at an elevation of 1830 m on a 10% slope and represents the coldest and moistest end of the climatic gradient. climax vegetation is classified as an <em class="ital">Abies lasiocarpa-Menziesia ferruginea </em>(fool's huckleberry) h.t.,<em class="ital"> Luzula hitchcockii </em>(smooth woodrush) phase. The soil has formed in a mantle of Mazama ash overlying course-textured glacial drift (<a href="#fig3">Figure 3</a>). The dominant feature of this soil is the morphological evidence of podzolization. In the podzolization process Fe and Al move as soluble metal-organic and inorganic complexes, giving rise to a light-colored E horizon which overlies a brown to reddish-brown Bs (or Bhs) horizon. Sufficient quantities of Fe and Al have been translocated at Site 112 to form an albic (E)&#8212;spodic (Bs) horizon sequence (<a href= "#fig3">Figure 3</a>; <a href="#table2">Table 2</a>). Accordingly, this soil is now classified as Spodosol, which is defined in Soil Taxonomy as a soil in which substantial quantities of amorphous Al and Fe (oxalate-extractable) have accumulated beneath the albic E horizon (Soil Survey Staff 1992). Previously this soil had been classified as an Andisol (Soil Survey Staff 1990) and an Andept (Soil Survey Staff 1975).</p>

	<p class="figure">
		<em class="bld"><a name="fig3"></a>Figure 3</em>&#8212;Comparison of parent material and horizon sequences in Trout Creek soils sampled along a bioclimatic gradient. <a href="graphics/mcdaniel_etal_1994_fig3_lg.gif">[view larger image - 12K]</a>
