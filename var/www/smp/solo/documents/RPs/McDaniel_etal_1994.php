	<p>Site 107 is located on a 35% slope at an elevation of 1450 m. Vegetation fits the <em class="ital">T. heterophylla-Clintonia uniflora</em> (queencup beadlily) h.t.,<em class="ital"> Clintonia uniflora</em> phase. The presence of <em class="ital">Abies lasiocarpa</em> and <em class="ital">Picea englemannii </em>(Englemann spruce) at this site suggest it occupies the cooler, moister end of the <em class="ital">T. heterophylla</em> series. The soil has formed in similar parent materials to those at Site 112, as high NaF pH values, high P retention, and comparatively high glass content all indicate a strong volcanic ash influence (<a href="#fig3">Figure 3</a>, <a href="#table2">Table 2</a>). The major morphological difference is that podzolization processes have not been sufficient to produce an E-Bs horizon sequence. The Bw horizons, do however, exhibit sufficient development to meet the requirements of a cambic diagnostic horizon. The strong influence of volcanic ash and the lack of a spodic horizon result in classification of this soil as an Andisol (Soil Survey Staff 1992). This soil had previously been classified as an Andept (Soil Survey Staff 1975) and was therefore not distinguished fromthe Site 112 soil at the higher levels of Soil Taxonomy despite major differences in soil-forming processes.</p>
	<p>Site 104 is located on a 55% slope at an elevation of 960 m and represents the lower-elevation end of the<em class="ital"> T. heterophylla </em>series. Vegetation is classified as a <em class="ital">T. hetreophylla-C. uniflora</em> h.t., <em class="ital">Aralia nudicaulis </em>(wild sarsaparilla) phase. Cooper et al. (1991) described this as the warmest and generally moistest phase of this habitat type, but also observed relatively dry stands that are barely able to support the moist-site species. Even though this site has the same h.t. classification as Site 107, the absence of <em class="ital">A. lasicarpa </em>and <em class="ital">Picea  englemannii</em> indicates comparatively warmer and drier conditions. The sandy textures and relatively low water content at 1.5 MPa of soil moisture tension further suggest that this is a droughty site (<a href="#table2">Table 2</a>). We feel the A. nudicaulis phase at Site 104 represents a warmer, drier environment than does the <em class="ital">C. uniflora</em> phase at Site 107. Cooper et al. (1991), however, describe the <em class="ital">C. uniflora</em> phase as the driest and warmest phase of the <em class="ital">T. heterophylla-C. uniflora-C. uniflora </em>h.t.</p>
	<p>Steep slopes and greater susceptibility to fire and subsequent erosion associated with this drier site may have resulted in removal of much of the volcanic ash. There is only moderate ash influence on this course-textured soil as evidenced by intermediate NaF pH values, low P retention, and small quantities of glass in the very fine sand fraction, resulting in classification as a Vitrandic Udorthent. This classification indicates the soil has some of the characteristics associated with volcanic ash-derived soils, but they are too weakly expressed for inclusion in the Andisol order (Soil Survey Staff 1992). Development of Bw horizons results in classification as either an Entisol or Inceptisol, depending on soil texture (Soil Survey Staff 1992) and both of these soil orders are well -represented throughout this vegetation zone.</p>
	<p>Site 101 is located on a 50% slope at an elevation of 585 m and represents the warmer, drier end of the climatic gradient observed in Trout Creek drainage. Parent material is glacial drift and vegetation as a<em class="ital"> P. Menziesii-Physocarpus malvaceus </em>(ninebark) h.t., <em class="ital">Physocarpus malvaceus</em> phase. <em class="ital">Pinus ponderosa </em>(ponderosa pine), absent from the higher-elevation sites, and Douglas-fir are the only tree species. The increased abundance of graminoids has resulted in formation of the darkest A horizon of any of the soils examined in this study. there is also sufficient B horizon development to meet the requirements for a cambic horizon, resulting in placement of the soil into the Inceptisol order. There is little or no volcanic ash influence and the soil is therefore classified as a Typic (rather than Andic or Vitrandic) Xerochrept (Soil Survey Staff 1992). throughout this region, it has been observed that Mazama ash is usually absent in soils associated with Douglas-fir h.t.'s (Barker 1981; Weisel 1982). During the warmer, drier conditions that existed at the time of cataclysmic eruption of Mt. Mazama, these sites most likely did not have sufficient canopy cover to prevent erosion of the ash that presumably was deposited uniformly throughout the area.</p>
	<h2>CONCLUSIONS</h2> 
	<p>In Trout Creek drainage of northern Idaho, Spodosols formed in volcanic ash are associated with the <em class="ital">A. lasiocarpa </em>series at the higher elevations. Classification of these soils is determined by the albic-spodic horizon sequence. these soils are also classified as intergrades to the Andisol order because of strong volcanic ash influence. Habitat types of the <em class="ital">T. heterophylla</em> series occupy the middle elevations. Andisols are the dominant soils at the moister and cooler end of this zone. At the warmer, drier end of the western hemlock zone, there has been less influence of volcanic ash on soil properties. Accordingly, soils are classified as Andic (and Vitrandic) intergrades of Inceptisols and Entisols. the <em class="ital">P. Menziesii</em> series occupies the frigid and xeric climatic regime of the lower elevations. Soils have little or no volcanic ash influence and, based on our data, are placed in Typic rather than Andic or Vitrandi subgroups of Inceptisols and Entisols.</p>
