	<h1>SOIL-PLANT COMMUNITY RELATIONSHIPS IN THE SELKIRK MOUNTAINS OF NORTHERN IDAHO<br /><span class="notes">(Reprinted from Northwest Science, Vol. 68, No. 1, 1994)</span></h1> 
	<p class="authors">
		P. A. McDaniel<span class="nobold">, Soil Science division, University of Idaho, Moscow, Idaho</span><br />
		K. E. Houston<span class="nobold">, U. S. Forest Service, Cody, Wyoming</span><br />
		M. A. Fosberg<span class="nobold">, Soil Science division, University of Idaho, Moscow, Idaho</span><br />
