	<h2>ABSTRACT</h2> 
	<p>Soils and the native plant communities they support often exhibit a strong mutual dependence within geographically similar areas. In mid- to high-elevation forests of northern Idaho, volcanic ash is an important soil parent material and has had impact on soil properties and the distribution of vegetation. As a result, there are several well-expressed soil-plant community type associations within this region. This study compares the properties and classification of soils and plant communities along a south-facing elevation gradient in the Selkirk Mountains of northern Idaho. Cold, moist soils of the higher elevations, corresponding to the <em class="ital">Abies lasiocarpa </em>(subalpine fir) series, are strongly influenced by volcanic ash. These soils have undergone intense podzolization and are classified as Spodosols. The intermediate moisture and temperature conditions associated with the middle elevations support plant communities of the <em class="ital">Tsuga heterophylla</em> (western hemlock) series. Associated soils exhibit progressively less influence of volcanic ash with decreasing elevation. Accordingly, soils of the upper end of the western hemlock zone are classified as Andisols and those of the lower end are classified as intergrades to the Andisols. At relatively low elevations, the warmest and driest soils of the climatic gradient support communities of the <em class="ital">Pseudotsuga menziesii </em>(Douglas-fir) series. These soils contain little to no volcanic ash and are classified as non-acidic Inceptisols and Entisols. Results from this study indicate that increased sensitivity of Soil Taxonomy to ash-influenced soils helps distinguish several of the important vegetation interactions that have been operative in this region. As a result, interrelationships between soils, their classification, and associated plant communities can be clearly demonstrated for these and, presumably, similar forested ecosystems of the Pacific Northwest.</p>
	<h2>INTRODUCTION</h2> 
	<p>Interrelationships between soils and the vegetation they support are often observed by field soil scientists and plant ecologists. In recognizing the close relationship between plant communities and soil-forming processes, Jenny (1941) included organisms (primarily vegetation) as one of the five soil-forming factors in his widely used model of soil genesis. Major (1951) used a similar set of independent variables to define a climax plant community. Thus soils and climax plant communities can both be though of as products of the same set of environmental variables&#8212;climate, organisms, relief, parent material, and time&#8212;and an interdependence therefore exists between a specific soil body and its climax vegetation (Hironaka et al. 1991). Considerable research in forests and rangelands of the western U.S. has been directed at identifying and even attempting to quantify the relationships between soils and the native climax communities they support (Daubenmire 1970; Steele et al. 1981; Hironaka et al. 1983; Tisdale and Bramble-Brodahl 1983; Cooper et al. 1991).</p>
	<p>The habitat type (h.t.) approach to classification of land areas was first introduced by Daubenmire (1952) and is based on identification of potential climax plant communities. This system has been widely used to stratify and classify forest lands in the western U.S. (Pfister et al. 1977: Steele et al. 1981; Cooper et al. 1991). Habitat types represent a &quot;type&quot; rather then &quot;continuum&quot; approach to classification&#8212;they are identified by a specific combination of indicator plant species that reflect a modal type. Soil classification also represents a &quot;type approach. Soil taxa are defined in Soil Taxonomy by a discrete set of diagnostic chemical, physical, and morphological properties, many of which influence or are a product of the native vegetation (Soil Survey Staff 1975).</p>
	<p>Given the similarities between soil and habitat type classification schemes, soil classification should be related to a habitat type classification within a given environmental setting. However, efforts to correlate soil and habitat type classifications have not been entirely successful. Potkin (1991) found that many soil properties which are important to plant community distributions in high-elevation environments are not utilized by Soil Taxonomy for classification purposes. Neiman (1986) examined 89 forested sites throughout northern Idaho and found little quantative relationship between h.t.'s and soil physical properties and taxonomic classes. These studies suggest that within some environmental settings, soil classification does not emphasize or consider characteristics that are important to the distribution of plant communities. furthermore, Hironaka et al. (1991) have cautioned that at any scale beyond a very localized application, a universal correlation does not exist between soil properties and vegetation attributes.</p>
	<p>In northern Idaho, volcanic ash has had a significant impact on many soil properties and is believed to strongly influence the distribution of vegetation (Steele et al. 1981). Recent changes in Soil Taxonomy (Soil Survey Staff 1975) have placed more importance on the unique properties of ash-influenced soils. Once ash has been deposited across a region, it is exposed to various soil weathering environments and surficial processes such as erosion. As a result, the impact of the ash on soil properties may be quite variable within a relatively small geographic region. Accordingly, the Andisol order has been added along with the Andic and Vitrandic intergrades of other orders to better reflect a range of ash-influenced soil properties (Soil Survey Staff 1992).</p>
	<p>Given the importance of volcanic ash as a soil parent material in many forested areas of the Pacific Northwest and the increased taxonomic sensitivity to ash-influenced soils, the objective of this study is to illustrate some of the interrelationships that exist between soils and plant communities in this region. Specifically, this paper compares the properties and classification of soils and vegetation along a south-facing elevational gradient in the Selkirk Mountains of northern Idaho.</p>
	<h2>METHODS AND MATERIALS</h2>
	<h3>Study Area</h3> 
	<p>The study area is located in the Trout Creek drainage of the Selkirk Mountains in northern Idaho (<a href="#fig1">Figure 1</a>). Trout creek flows from Pyramid Lake to the Kootenai River, dropping approximately 1300 m in elevation over a distance of 20 km. the general topography of the drainage becomes steeper at lower elevations as Trout Creek has incised more deeply into the underlying substrate. Two contrasting geological substrates occur in this area of the Selkirks. Steeply dipping Precambrian metasediments flank the range on the east side and granitic rocks of the Kaniksu Batholith make up the core of the range (Conners 1976). Pleistocene glacial deposits overlie both substrates and thus constitute one of the major soil parent materials of the area. During the Holocene, large quantities of volcanic ash from the cataclysmic eruption of Mount Mazama (approximately 6700-6800 yrs BP) were deposited throughout the region (Fryxell 1965; Fosberg et al. 1979; Bacon 1983). Some of this ash has been retained in modern soils either as a relatively undisturbed superficial mantle or as an admixture with coarser-textured granitic materials. The 1800 AD eruption of Mount St. Helens added an additional 1-3 cm of ash (Layer T) to many soils of the area (Smith et al. 1968; Okazaki et al. 1972; Smith et al. 1975; Mullineaux 1986). This thin ash layer has been preserved in many of the soils, indicating relatively little surface disturbance has taken place over the past 200 yrs. Ash from the 1980 Mount St. Helens eruption was not observed in this area.</p>

