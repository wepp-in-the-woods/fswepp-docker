	<p>The climatic regime of the Trout Creek area is an inland expression of the Pacific Coast maritime climate. The closest weather station is located approximately 11 km south of the study area at 1677 m elevation. Data from this station indicate annual precipitation averages 1180 mm and mean annual air temperature is 2.6 degrees <span title="Celsius">C</span> (Cooper et al. 1991). Along the Trout Creek elevational gradient, precipitation is estimated to range from 560 to 2000 mm and falls primarily in the spring, fall, and winter (Houston 1988). Summers are generally warm and dry.</p>
	<p>Study sites with the Trout Creek drainage were selected to represent the variation in soils and habitat types observed along elevational gradients in this part of the Selkirk Mountains. Sites chosen for sampling were restricted to those having mature vegetation stands that were relatively undisturbed by logging or fire. Of the 30 sites sampled and determined by Houston (1988), we will report data from four sites which collectively encompass the range in observed communities (<a href="#fig1">Figure 1</a>). All four sites are located on south-facing, relatively stable ridge slopes.</p>
	<h3>Soils</h3> 
	<p>Within each of the climax plant communities, soils were examined using road cuts and shallow hand-dug pits to determine representative morphological characteristics. A sampling pit was then located near the center of the plant community and dug to a depth of approximately 1 m. Soil profiles were described using standard horizon designations and descriptions (Soil Conservation Service 1984; Soil Survey Staff 1992). Soils were sampled by genetic horizons and classified to the family level according to the Soil Taxonomy (Soil Survey Staff 1992) using appropriate laboratory and soil climate data. Samples were air dried and crushed with a rubber-tipped pestle to pass a 2-mm sieve. Laboratory analyses were performed using the following methods: particle-size analysis by pipette and centrifugation procedures (Jackson 1975; Gee and Bauder 1986); water retention at 1.5 MPa of soil moisture tension using pressure-plate extraction (Klute 1986); organic carbon by rapid dichromate oxidation (Nelson and Sommers 1982) l pH (1:1 soil:water) and NaF pH using a combination glass electrode (Soil Conservation Service 1984); P retention by colorimetry (Blakemore et al. 1981); and Fe and Al contained in short-range order minerals by extraction with acid ammonium oxalate (Soil Conservation Service 1972). Glass contents were determined on very fine sand fractions by counting between 100 and 200 grains using a petrographic microscope.</p>
