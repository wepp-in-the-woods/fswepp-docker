	<p>In the Trout Creek drainage, we have observed the E-Bs horizon sequence (and Spodosols) only in association with the subalpine fir series. Similar associations have been reported elsewhere in the Pacific Northwest (Smith et al. 1968; Hunter 1988; Dahlgren and Ugolini 1991). This soil-plant community association exists because the conditions required for subalpine fir climax are also conducive to podzolization. The vegetation and climate interact in a manner to provide soluble organic chelating compounds and relatively large quantities of soil moisture that cause downward migration of organic and inorganic metal complexes.</p>
	<p>The site 112 soil is the most strongly acid and contains the largest quantities of organic matter of any of the soils in this study (<a href="#table2">Table 2</a>). NaF, pH values &gt;10, high P retention, and comparatively high glass contents in the very fine sand fractions all indicate a strong volcanic ash influence and result in classification of this Haplocryod as an Andic intergrade. Relatively large quantities of oxalate-extractable Fe and Al in the Bs horizons reflect the greater intensity of weathering and podzolization processes in this soil compared to those of the other sites.</p>
 

