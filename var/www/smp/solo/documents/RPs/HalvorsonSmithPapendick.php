<h1>ISSUES OF SCALE FOR EVALUATING SOIL QUALITY</h1> 
	<ul class="authors">
		<li>Jonathan J. Halvorson</li>
		<li>Jeffrey L. Smith</li>
		<li>R.I. Papendick</li>
	</ul>
	<p><em>&#8220;Not every soil can bear all things&#8221; (Virgil)</em></p>

<p>How we define soil quality is influenced by many factors
including our relationship to the land, what we use it for and how much
of it is available. thus, assessing soil quality is context sensitive, a
fact appreciated early be scientists like Hilgard (1914), who remarked,
&quot;The observers field of vision should be broad enough to embrace concurrently
the several points of view...that must be conjointly considered in forming
one's judgment of the land.&quot; Recently, soil quality has been defined
as the &quot;capability of soil to produce sage and nutritious foods and
crops in a sustainable manner over the king term, and to enhance human and
animal health without adversely impairing the natural resource base or adversely
affecting the environment.&quot; (Parr et al. 1992). this broad view of
the important functional characteristics of soil has helped heighten the
current wave of interest in soil quality until it has emerged as a major
scientific and political topic.</p>
	<p>An important issue is how to evaluate soil quality. A soil quality
index must be more than just an academic exercise because it will be used
to classify land capability, assess of the impact of management practices;
quantify the value of land as a base for taxation or land credit, establish
regulatory compliance, and provide data for monitoring or modeling environmental
change (Parr et al. 1992). Therefore, much effort has been directed toward
identifying which soil properties relate to soil quality (Smith et al. 1993).
Because soil ecosystems are complex, scientists generally agree that evaluating
soil quality will, in some manner, require integration of several kinds
of soil data including physical, chemical and biological properties and
human factors (Parr et al. 1994). However, little effort has been directed
towards understanding how spatial and temporal variability of these indicators
influence a soil quality index.</p>
	<p>The objective of this paper is to emphasize some of the issues of the scale
that relate to soil quality. By issues of scale we mean the interrelationships
between soil quality data and subsequent interpretations that are affected
by spatial and temporal variability. Appreciation of variability is required
in order to define the basic spatial and temporal units for sampling soil
quality indicators and for utilizing indices of soil quality. In the past,
soil scientists have usually needed to consider only a few soil variables
at a single scale. However, estimates of soil quality will integrate many
kinds of data and may be used for different purposes over a broad range
of spatial and temporal scales. Soil quality data must thus be collected
from sampling units that are of a size, shape, orientation and at times
that result in an efficient tradeoff between the gain of useful information
and cost of sampling. Further, these data must be collected so that the
resultant soil quality index is useful and valid at the temporal and spatial
scales of real world applications.</p>

	<h2>The Spatial and Temporal Variability of Soil Quality</h2>

	<p>The patterns of soil quality indicators result from the operation and interaction
of soil forming processes over a continuum of spatial and temporal scales
(Trangmar et al. 1985). Soil variables may appear to vary spatially in well
defined trends or patterns, fluctuate erratically or appear constant depending
on the scale of our measurements (Warrick et al. 1986; Parkin 1993). Spatial
patterns in the soil may be related to varying ecosystems receiving spatially
constant inputs (Odum 1983). [Patterns and processes associated with soil
quality are sometimes divided into those that predominate at the microscale;
(within soil aggregates) the mesoscale, (on the order of meters, also termed
plot scale); and the macroscale, (landscape units at the regional or continent
wide spatial scale (Coleman et al. 1992; Parkin 1993).]</p>
	<p>The temporal patterns of soil variables may appear as well defined long-term
trends, exhibit patterned (cyclic) behavior, fluctuate erratically or appear
constant depending on the temporal scale of measurement. Temporal variation
of soil quality indicators can often be related to cycles of energy and
mass transfer in the environment. In general, there is an inverse relationship
between the amount of temporal variability and the fluctuation frequency
of the environmental cycle. For example, a soil variable, affected by temperature,
is likely to vary less during a daily temperature cycle (high frequency
event) than during a yearly cycle (low frequency event). The variations
in yield from year to year are likely to be less than the variations recorded
during a decade. The apparent temporal variability of a soil quality indicator
may be influenced by its spatial variability (Hammer et al. 1987) and relate
to the spatial scale of the investigation.</p>
	<p>Many soil quality indicators vary significantly over the course of a growing
season including soil physical, chemical and biological properties. The
patterns for different variables may not be correlated with each other or
synchronized with cycles of temperature and precipitation. Temporal patterns
may also be affected by sample depth or soils type. Temporal patterns for
soil quality indicators may persist for only a few hours, weeks, or throughout
an entire growing season if correlated with a deterministic factor such
as soil texture. Very high temporal variability of <em>in situ </em>measurements
suggests that samples need to be collected often or that standardized laboratory
measurements of potential responses will be more appropriate for large-scale,
long-term sampling of soil variables.</p>
