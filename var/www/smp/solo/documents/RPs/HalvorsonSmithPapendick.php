	<p>Of great importance to measurements of soil quality is characterization of the time stability of spatial patterns of soil quality indicators. Time stability means that the values of a soil quality indicator remain constant over time or that there is a correlation between the measurements taken at t1 and t2 so that spatial patterns remain the same (Kachanoski and DeJong 1988). A low degree of time stability may be characteristic of soil variables that are strongly influenced by many external factors. The important consequences of soil quality indicators with low time stability is that it makes the task of developing a soil quality index that can be used to monitor long-term changes in soil quality virtually impossible. However, soil variables with low time stability may be the only ones responsive enough to be used to manage crop productivity. Further, low time stability observed at the field scale may appear insignificant if soil quality is measured on large spatial scales.</p>
	<h2>Scale Influences Sampling</h2>
	<p>The variability of soil properties affects the way we sample and the number of samples required to estimate summary statistics such as the mean with a known degree of precision and certainty. Sometimes, the number of samples required can be minimized if it is possible to account for the spatial pattern of the samples (McBatney and Webster 1983). However, without any prior information, about the spatial dependence of each soil variable, random sampling or regular interval sampling will likely be necessary to evaluate soil quality. Soil variables controlled by intrinsic variations in soil characteristics such as texture or mineralogy may exhibit strong spatial dependence when compared to those variables controlled by extrinsic factors such as fertilizer application and tillage. The latter may exhibit spatial dependence at scales smaller than the scale of the study and therefore be undetectable (Cambardella et al. 1994).</p>
	<p>Accurate assessment of soil quality requires a sampling strategy that captures the characteristics of many different soil variables at appropriate spatial and temporal scales. The scale of sampling is important because patterns of soil quality that we are able to detect may change as we move from fine scale to broad scale perspectives. Table 2 contrasts some of the attributes of fine scale sampling of soil quality with those for broader scales. Fine scale is distinguished from broad scale by a relatively large number of samples per unit land area or per unit time.</p>

