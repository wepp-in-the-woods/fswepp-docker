			<h3 class="section"><a name="set1"></a>SOILS</h3>
			<p>There was a strong positive correlation among proportion of clay, organic matter, nitrogen, carbon, and litter (litter + duff) depths, and a strong negative correlation between these factors and the proportion of sand (<a href="#table2">table 2</a>). A principal components analysis of these soil factors generated two axes that explained 71 percent of the variation. The first axis accounted for 53 percent of the variation, and the major components of the axis were proportions of sand, clay, organic matter, nitrogen, carbon, and litter depths (<a href="#table2">table 2</a>). Net mineralization of soil organic matter and decomposition of plant material is more rapid in sandy soils than in clay soils (Verberne and others 1990). Lower mineralization in clay soils is caused by a greater physical protection of soil organic matter, which may explain the positive correlations between clay, organic matter, and total nitrogen. In a study of grassland soil texture in the Netherlands, Hassink (1992) found that sandy soils had organic matter contents of 3 to 8 percent, while clay soils ranged from 9 to 10.5 percent. Total nitrogen followed these same trends with 0.11 to 0.30 percent in sandy soils but 0.51 to 0.66 percent in clay soils.</p>
			<p>Total nitrogen content of these alpine soils is similar to <span class="ital">Picea mariana-Salix</span> spp.<span class="ital">-Equisetum</span> spp. plant associations found on poorly drained sites in Alberta, Canada (total <abbr title="nitrogen">N</abbr> = 0.64 percent) (Kojima 1982). In the Sierra Nevada Range at 5,300 to 6,500 ft, total nitrogen content of the mineral soil was 1.1 percent and carbon was 23.4 percent (Schlesinger and others 1989). Higher total nitrogen at these sites reflects a higher net production and input to soil organic pools under forest vegetation.</p>
			<p>There was also a strong positive correlation between <abbr title="hydrogen ion concentration">pH</abbr> and the amount of coarse fragments (<a href="#table2">table 2</a>). The second principal components axis accounted for 18 percent of the variation and was dominated by these two variables (<a href="#table2">table 2</a>). This correlation probably reflects the fact that moist sites generally have lower <abbr title="hydrogen ion concentration">pH</abbr> and little or no coarse fragments in the surface horizons.</p>
			<p>Soil pH for both dry and wet sites are much higher than those reported for mineral soil in alpine and subalpine zones in Alberta (Kojima 1982), likely due to the greater leaching caused by the much higher precipitation occurring in the Canadian Rocky Mountains. Moist, low-elevation <span class="ital">Abies grandis</span> forests of northern Idaho have a mineral soil <abbr title="hydrogen ion concentration">pH</abbr> between 5.4 and 6.0 (Page-Dumroese and others 1989), similar to high-elevation <span class="ital">Pinus contorta</span> stands in Alberta (Florence and Dancik 1988). Lower <abbr title="hydrogen ion concentration">pH</abbr> values of forests are the result of greater inputs of organic matter and faster decomposition rates.</p>
			<p>Soils supporting turf and cushion plant communities differed in a number of characteristics when derived from calcareous compared to crystalline parent materials (<a href="#table3">table 3</a>). Calcareous soils had a mean <abbr title="hydrogen ion concentration">pH</abbr> of 7.5, while those developed from crystalline materials had a <abbr title="hydrogen ion concentration">pH</abbr> of 6.2. The proportion of sand was higher in soils derived from crystalline rock, while calcareous soils were higher in clay, carbon, and nitrogen.</p>
			<p>Many common alpine plant associations were restricted to soils derived from either calcareous or crystalline parent materials. <span class="ital">Carex scirpoidea</span>/<span class="ital">Geum rossii</span> turf and <span class="ital">Geum rossii</span>/<span class="ital">Arenaria obtusiloba</span> cushion plant communities occurred only in areas dominated by crystalline geology, while all but one example of <span class="ital">Carex rupestris</span>/<span class="ital">Potentilla ovina</span> cushion plant association occurred on limestone. Although <span class="ital">Hesperochloa kingii</span> is a common grass found farther south in the Rocky Mountains, in southwestern Montana high-elevation grasslands dominated by this species, occur only on calcareous soils. The <span class="ital">Juncus drummondii</span>/<span class="ital">Antennaria lanata</span>, <span class="ital">Phyllodoce empetriformis</span>/<span class="ital">Antennaria lanata</span>, and <span class="ital">Cassiope mertensiana</span>/<span class="ital">Carex paysonis</span> snowbed associations were found only on soils derived from crystalline parent material. These same patterns have been observed in east-central Idaho (Henderson, personal communication).</p>
			
