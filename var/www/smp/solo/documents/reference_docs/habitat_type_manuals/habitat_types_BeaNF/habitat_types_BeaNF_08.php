			<h3 class="section"><a name="set2"></a>MANAGEMENT CONSIDERATIONS</h3>
			<p>Alpine environments are among the most severe on earth. Low temperatures, nearly constant high winds, and high insolation are among the factors that shape the alpine environment and limit plant growth (Billings 1988; Bliss 1985; Brown and others 1978). The alpine growing season is short, often only 8 to 12 weeks. Temperatures during the growing season are cool, and frost can occur on any night. As a result, plants are limited in the amount of photosynthate they can store. Furthermore, the frequent freeze-thaw cycles make frost-churning a common phenomenon, especially in moist sites. Frost-churning and needle ice damage vegetation and limit recruitment. High windspeeds can damage plants through desiccation. Wind-driven soil and ice particles can destroy plant tissue, especially when young. Wind redistributes snow cover. Ridgetops and upper windward slopes are dry and exposed to severe winter temperatures, while lee slopes and depressions are cold and wet with a reduced growing season. Wind diminishes the formation of a boundary layer around plant parts, further exacerbating low summer temperatures. Solar radiation at high elevations is intense. Intense radiation coupled with high winds promote summer drought and high levels of evaporation. High levels of ultraviolet radiation can damage plant tissues.</p>
<p>The harsh environmental conditions above treeline make growth and the accumulation of biomass a slow process. Furthermore, soil formation takes much longer at high elevations because of the retarded pace of biological processes. As a result, recovery from disturbance is generally slow (Billings 1973; Willard and Marr 1971).</p>
<p>Alpine tundra ecosystems evolved almost completely without the disruptive effects of humans. Only in the past 150 years have these systems been exposed to such large-scale disturbances as livestock grazing, mining, and road-building. Unfortunately, few controlled studies have been done on the effects of these encroachments in alpine landscapes.</p>
												
			<h4><a name="set2a"></a>Livestock Grazing</h4>
			<p>Grazing has two primary effects on plant communities&#8212; removal of plant biomass and trampling. By selecting certain plants over others, grazers alter the competitive balance among species and eventually alter the composition of communities. Although both sheep and cattle graze above treeline in our study area, in most areas of the Rocky Mountains sheep are the principle domestic animal in the alpine zone (Johnson 1962; Thilenius 1975). Consequently, most observations relating to the effects of livestock on alpine ranges refer to sheep. In general, cushion plants such as <span class="ital">Arenaria obtusiloba</span> and <span class="ital">Silene acaulis</span>, and low sedges such as <span class="ital">Carex rupestris</span> and <span class="ital">C. elynoides</span> tend to increase with grazing pressure; but robust graminoids such as <span class="ital">Deschampsia cespitosa</span> and <span class="ital">Poa glauca</span>, and forbs such as <span class="ital">Agoseris</span> spp. and <span class="ital">Potentilla diversifolia</span> tend to decrease (Johnson 1962; Lewis 1970). At low elevations, grazing tends to have the same effect as drought, decreasing mesic site indicators and increasing xeric site species (Weaver 1954). The same appears to be true in the alpine. Henderson (personal communication) reported that toxic forb species (for example, <span class="ital">Lupinus argenteus</span>, <span class="ital">Oxytropis campestris</span>) appear to have increased in turf communities that are subject to long-term sheep grazing. Unfortunately, there have been no controlled quantitative studies to verify the scarce anecdotal evidence available.</p>
<p>Sheep grazing was common on the gentle alpine terrain of the Gravelly Range. We commonly observed cattle or evidence of cattle above treeline in the Snowcrest, Beaverhead, and Pioneer ranges. We were surprised to find evidence of heavy livestock use near 11,000 ft in the Beaverhead Mountains.</p>
<p>There were no exclosures above treeline in our study area, so we have little knowledge of the effects of livestock grazing on plant species composition. Cushion plants were more common in some turf communities than others, but these differences could be due to soils or moisture regime rather than overgrazing. <span class="ital">Poa pratensis</span>, an introduced grass considered an indicator of present or past disturbance, occurred in some grassland, turf, and wetland stands, mainly in ranges that had been subject to long-term livestock grazing (for example, Gravelly and Snowcrest ranges) and in moist or wet community types. <span class="ital">Juncus balticus</span> was codominant with <span class="ital">Deschampsia cespitosa</span> in one wetland site in the Snowcrest Mountains. <span class="ital">Juncus balticus</span> is native but is thought to increase under grazing pressure in wet meadows (Hansen and others 1995). These observations suggest that the moist and wet sites are most susceptible to alteration of species composition from grazing.</p>
<p>In drier portions of our study area, such as the Beaverhead and Snowcrest Mountains, surface water is uncommon above treeline. As a result, cattle use tends to be concentrated in areas near water. We observed the effects of livestock trampling mainly in wetland communities. Streams where use had been heavy had increased turbidity, and banks had been compacted and eroded.</p>
<p>Trampling can destroy plants and result in the loss of soil. Plant communities occupying wet habitats are more easily damaged than mesic communities (Willard and Marr 1970), and continued disturbance often results in significant erosion (Billings 1973). Plants in wet sites are more succulent and susceptible to being broken, and the soil is more prone to compaction (Willard and Marr 1970). Turf communities are not as easily disturbed; but repeated trampling will result in the loss of soil, and recovery may take hundreds of years (Willard and Marr 1971). Wind erosion and frost action enlarge areas that have been denuded by trampling (Willard and Marr 1971). In general, wet communities are more susceptible to adverse effects of trampling, but drier areas will take longer to recover once damage has occurred.</p>
<p>Thilenius (1975, 1979) has written guidelines for livestock grazing in the alpine zone; the following synopsis is taken from his report. Cattle tend to aggregate in lower portions of cirque basins where water and lush vegetation are concentrated. These sites suffer damage under untended cattle grazing. Wet sites (including snowbed communities), dry sites, and steep slopes (40&deg;+) should not be grazed. Livestock should not be allowed to remain in any area for very long. Thus, intensive range-riding or herding is needed for nondestructive use of alpine ranges by livestock. Grazing and trampling by horses used for recreation can also cause damage when use is concentrated.</p>
			
			<h4><a name="set2b"></a>Vehicle Use</h4>
			<p>There are fewer roads above treeline in Montana than in other Rocky Mountain States. Nonetheless, vehicle use, including motorcycles and all-terrain vehicles, was apparent in the alpine zone of the Beaverhead, Snowcrest, Gravelly, Pioneer, and Tobacco Root ranges. Road construction and vehicle use are among the most damaging activities in alpine environments (Brown and others 1978; Thilenius 1975). Repeated vehicle use destroys plants and causes soil erosion and compaction. Damage is generally proportional to (1) wetness of the site, (2) frequency of use, and (3) weight of the vehicles (Thilenius 1975). Four-wheel drive vehicles are banned from the alpine zone in some states (Thilenius 1975).</p>
<p>At the north end of the Pioneer Range, some areas have soils derived from highly metamorphosed limestone that are relatively barren and easily erodible. These areas are also the sites of mining activity, and roads have been built to the mines, providing access to fragile alpine landscapes for four-wheel drive and all-terrain vehicles. Some of these roads remain open, while others have been closed. However, we observed a three-wheel all-terrain vehicle driving on a steep, barren, eroding trail behind a locked gate. We also observed unauthorized all-terrain vehicles in the Italian Peaks area of the Beaverhead Mountains, an area closed to all motor vehicles. Use of vehicles for recreation in the alpine zone is causing damage that will take tens or perhaps hundreds of years to recover (Willard and Marr 1971).</p>
																		
			<h4><a name="set2c"></a>Mining</h4>
			<p>Mines damage alpine communities, causing destruction of vegetation, soil erosion, and water pollution (Brown and others 1978; Thilenius 1975). Evidence of mining activity is common in the Pioneer and Tobacco Root Mountains. Mine shafts, building sites, tailings heaps, dumps, and roads scar the landscape in many areas. In most cases, activity ceased decades ago; nonetheless, the damage is still apparent and revegetation negligible at the majority of these sites.</p>
																		
