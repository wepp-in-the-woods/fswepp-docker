			<h3 class="section"><a name="set1"></a>STUDY AREA</h3>
			<h4>Geology and Soils</h4>
			<p>Representative exposures from the three major groups of parent materials&#8212;sedimentary, metamorphic, and igneous&#8212;are found in mountain ranges of southwestern Montana. Sedimentary and metamorphosed sedimentary rocks predominate in the south and west portions of the study area, while intrusive and metamorphic basement rocks become more common to the east and north (Ross and others 1955). The crests of the southern Beaverhead, Gravelly, Snowcrest, and Tendoy Mountains are composed of Mesozoic and upper Paleozoic limestones, sandstones and quartzites. The southern end of the Beaverhead Mountains is composed of calcareous Beaverhead Conglomerate. The highest point in the Gravelly Mountains, Black Butte, is a remnant stock of Quaternary basalt. The high country of the Tobacco Root Mountains is composed of granite of the Tobacco Root Batholith. Most of the alpine terrain in the Pioneer Mountains is underlain by granite of the Pioneer Batholith; however, the high peaks at the very north end of the range form a contact between the intrusive igneous and Paleozoic limestones and dolomites. Although the main mass of the Anaconda Mountains is granitic, the east end where we sampled is underlain by Precambrian quartzites and limestones. The southern end of the Madison Mountains is composed primarily of Precambrian gneiss and schist with some areas on the east flank underlain by Mesozoic limestone (Ross and others 1955). <a href="#table1">Table 1</a> summarizes the number of plots established in each mountain range by parent material.</p>
			<p>Soils supporting alpine vegetation have been described for the Northern Rocky Mountains by Bamberg and Major (1968), Johnson and Billings (1962), Nimlos and McConnell(1962), and Thilenius and Smith (1985). Soils from our study sites on sedimentary parent material resembled those described by Bamberg and Major (1968), while sites with crystalline parent material had soils similar to those described by Johnson and Billings (1962) and Nimlos and McConnell(1962). In general, turf and meadow soils developed from sandstones, limestones, and shales were finer textured than those derived from granite, quartzite, or metamorphic basement rocks.</p>
			<p>Indications of cryopedogenic processes were evident in all of the mountain ranges. Solifluction lobes and terraces were common on steep, moist north slopes. Frost boils, rock polygons, and stone stripes were often apparent, especially in the Anaconda-Pintlar, Madison, East Pioneer, Tendoy, and Tobacco Root Mountains. Development of these features has been described by Billings and Mooney (1959), Johnson and Billings (1962), Lewis (1970), and Washburn (1956).</p>
			<h3 class="section"><a name="set2"></a>METHODS AND DISCUSSION</h3>
			<h4>Soils</h4>
			<p>For each plot we collected three 1 liter soil samples from along the lower side of the transect line at the 5, 15, and 25 m marks. Each sample was collected from below the litter and organic layers to a depth of 6 inches. Reported means of soil variables refer only to the surface 6 inches. Percent of coarse fragments was determined in the field by sieving through a 2 mm screen and measuring volumetric displacement of the rock fragments remaining on the screen. Soil pH was determined by preparing 2:l aqueous suspensions of sieved soil from each sample, allowing the suspension to equilibrate for 10 minutes and then measuring pH with a portable digital temperature-compensated meter. Means of the three measurements from each plot were used to develop the classification sections. Duff (the fermentation and humus sections of the organic layer) and litter (the surface layer of freshly fallen leaves and twigs) were measured to the nearest 0.1 inch.</p>
			<p>Soil samples were dried at 60&deg;C for 24 hours and passed through a 2 mm sieve before analysis. Organic matter was determined by weight loss after combustion at 375&deg;C for 16 hours (Davies 1974). It was assumed that loss of structural water from clay minerals and loss of CO<sub>2</sub> was not significant. Particle size distribution was determined using the hydrometer method (Gee and Bauder 1986). Soil totals of nitrogen and carbon were analyzed by a medium-temperature resistance furnace (Nelson and Sommers 1982).</p>
			<h4>Productivity</h4>
			<p>To estimate primary productivity, we clipped the current year&#8217;s aboveground growth in three 20 x 50 cm microplots placed at 5, 15, and 25 m along the upper side of the transect line. Clippings were pooled into three life-form classes (shrub, graminoid, forb) for each plot, air-dried, and then weighed to the nearest gram. Due to the appreciable difference in precipitation between the 2 sampling years, productivity estimates were probably lower than average for 1989 and higher for 1991.</p>

