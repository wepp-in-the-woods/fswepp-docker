	<h1>SOIL PROPERTIES AS A FACTOR IN REGENERATION FAILURES IN THE GRAND FIR MOSAIC&#8212;PRELIMINARY RESULTS</h1> 
	<p class="authors">
		Mark Sommer<br />
		David Adams<br />
		Robert Mahler</p>
	<p>The grand fir mosaic (GFM) consists of a patchwork arrangement of several forest plant communities located in an elevation range of 4,000 to 5,800 feet above sea level. The grand fir mosaic is found primarily in Idaho's Clearwater and Nez Perce National Forests. The plant communities (habitat type from Cooper and others 1987) are:</p>

	<ol>
  	<li>Forested stands where the grand fir/wild ginger (<em class="ital">Abies grandis/Asarum caudatum</em>) habitat type predominates.</li>
  	<li>Bracken fern (<em class="ital">Pteridium aquilinum</em>) glades that appear to be climax communities.</li>
  	<li>Alder glades where the Sitka alder/broadleaved montia (<em class="ital">Alnus sinuata/Montia cordifolia</em>) habitat type predominates.</li>
	</ol>

	<p>As the forested areas are harvested, bracken glade communities can invade and exclude most woody vegetation, including conifers. This occurs despite attempts at natural and artificial regeneration. The pocket gopher (<em class="ital">Thomomys talpoides</em>) also invades the harvested areas and rapidly multiplies. Current research indicates bracken fern allelopathy and damage by pocket gophers as major factors in the poor regeneration success (Ferguson and Boyd 1988). The alder glades expand slightly, causing few problems.</p>
	<p>The bracken fern produces a large amount of biomass, which dies in the fall and is buried by snow in the winter. When spring arrives, a surface of almost bare soil is revealed. Thus rapid decomposition and mineralization rates may be taking place beneath the snow.</p>
	<p>This study compares forest and natural bracken glade soils. It also examines soils from cutover areas where bracken fern has invaded to see if soil properties are changing.</p>
	<h2>METHODS AND MATERIALS</h2> 
	<p>The study area is located about 15 miles northeast of Headquarters, ID, in the North Fork Ranger District of the Clearwater National Forest. Portions of the area's forests were clearcut about 25 years ago. Within the study area there is uncut forest, natural bracken fern glades, and harvested areas invaded by bracken fern.</p>
	<p>A randomized block design was used for sampling. Soil cores were taken from the uncut forest, bracken fern glades, and the bracken-invaded areas. To reduce variability, the cores were taken from areas that had similar relief, parent material, and climate. Fifteen samples from each area were taken to a 1-foot depth. Soil analysis was completed at the University of Idaho's Analytical Services Laboratory and Soil Fertility Laboratory.</p>
