	<h2>DISCUSSION</h2> 
	<p>The soil chemical properties of the uncut forest and the bracken glade appear to be somewhat different. Soil chemical properties of the bracken-invaded areas differ from both the uncut forest and the bracken glade. However, they seem most similar to bracken glade soils.</p>
	<p>The most striking change in the bracken glade-invaded areas is the decrease in pH and increase in extractable aluminum. Within the bracken glades and bracken-invaded areas there are high amounts of composition, mineralization, and nitrification during the winter and early spring. During this time there is little active uptake by plants, and nitrate and cations can be leached from the soil. This process lowers the pH, which could cause aluminum to be released. Bracken fern is also high in phenolic acids, which may aid acidification. Apparently the soil's buffering capacity is not able to neutralize the increased acidity.</p>
	<p>The increased amount of decomposition and mineralization could be due to several factors:</p>

	<ol>
  	<li>The bracken glade community produces a greater annual turnover of biomass than the forest community it replaced.</li>
  	<li>A change in microclimate where lack of shade in early spring and late fall causes soil to be warmed to higher temperatures.</li>
  	<li>Warmer and wetter environments than normal for these elevations.</li>
	</ol>

	<p>Preventive measures can be based on an understanding of the mechanism that causes this process. In addition, a search for methods of prevention  without awareness of the mechanism must continue. Different types of silvicultural practices need to be examined to determine which are least likely to lead to this process.</p>
	<h2>CONCLUSIONS</h2> 
	
	<ol>
  	<li>The different plant communities lead to formation of different soils on what otherwise appear to be areas with the same relief, climate, and parent material.</li>
  	<li>When bracken fern invades harvested areas, soil chemical properties can be altered. Thus, in addition to allelopathy and pocket gophers, a change in soil properties may be contributing to the poor regeneration success.</li>
	</ol>
	
	<h2>A NOTE ON THE MOSAIC</h2> 
	<p>The introduction stated that the grand fir mosaic (GFM) exists in both the Clearwater and Nez Perce National Forests. However, as more information is gathered, it seems that the GFM is not the same ecosystem in each Forest. For instance:</p>

	<ol>
  	<li>The &quot;climax&quot; bracken fern glades are almost nonexistent in the Nez Perce National Forest.</li>
  	<li>After harvest, bracken fern is the primary invader in the Clearwater National Forest, but in the Nez Perce National Forest the primary invader is coneflower (<em class="ital">Rudbeckia occidentalis</em>).</li>
  	<li>Soil chemical properties have not changed after harvest in the Nez Perce National Forest</li>
	</ol>
	
	<p>Because of this, this poster paper was largely focused on one National Forest. The Clearwater was chosen because it is more interesting from a soils standpoint. However, from a forestry standpoint, both Forests are important because regeneration failures in the GFM have been noted in each.</p>
