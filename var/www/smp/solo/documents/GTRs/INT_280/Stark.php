	<h1>MINIMIZING THE ADVERSE IMPACTS OF TIMBER HARVEST IN THE NORTHERN ROCKY MOUNTAINS</h1> 
	<p class="authors">
		Nellie M. Stark<br />
Hans Zuuring</p>
	<h2>ABSTRACT</h2> 
	<p>A new model, NUTROSS, has been developed to evaluate nutrient losses from harvest of aboveground biomass of trees in the Northern Rocky Mountains. The model is useful in low-precipitation forest zones with little or no solution losses to streams. It is based on the assumption that materials not removed in harvest will be available to supply nutrients through mineralization to grow the same ecosystem components in the next rotation. Those components of the stand that are removed in harvest should not contain more than 25 to 30 percent of any one biologically essential nutrient. These values are guidelines based on estimated weathering rates. The model was applied to seven stands that were thinned to 3 by 3, 4 by 4 ,and 6 by 6 m with unthinned controls. The results showed that available copper and zinc were most often inadequate to support intensive harvest of trees over three or four rotations. Most of the soils studied can support harvest of boles only.</p>
	<h2>INTRODUCTION</h2> 
	<p>A chemically fragile soil is one that does not hold enough of one or more biologically essential nutrients in available (soil) or recyclable form (slash, ground vegetation, and litter) to support the next three or four forest rotations. Four rotations are reasonable for judging extremely poor soils, and three rotations are used for evaluating more fertile, warmer soils where weathering is more rapid. Studies on nutrient loss related to harvest (Stark 1982) showed that there should be enough nutrient onsite in available or recyclable form at the time of harvest to grow trees and associated vegetation for the next three to four rotations (70- to 100-year rotations). Growth during the next three to four rotations should not be subsidized from nutrients recently weathered that could otherwise allow maturation of the soil.</p>
	<p>Most of the more productive forests have ample nutrients to support conventional harvest of boles to a 7-cm top on a 70-year rotation. Only those sites that have more than 25 to 30 percent of any one nutrient tied up in the biomass to be harvested are chemically fragile and need special management.Chemically fragile soils can be recognized by poor tree growth where there is adequate rainfall, or through chemical analysis. Chemically fragile soils have one or more of the following characteristics: shallow feeder root zone (&lt;36 cm), or deeper but with high rock content (&gt;60 percent rock), or are geologically young, or occur in either cold or dry areas where weathering is slow. Talus slopes are often chemically fragile as are shallow, cold, young rocky soils at high elevations.</p>
	<p>A simple model called &quot;NUTROSS&quot; has been developed to help identify chemically fragile soils that cannot withstand conventional harvest (boles only) or intensive harvest (boles, branches, needles) in the more arid forest types.</p>
	<p>NUTROSS, as it is perceived in the Northern Rocky Mountains, does not contain a nutrient export component for streamflow. The reason is that most areas with under 76 cm of annual precipitation on soils with an appreciable clay content do not normally lose large amounts of nutrients to streams after harvest unless erosion occurs (Stark 1982). The model assumes that no significant erosion will occur with logging or burning of slash. If erosion does occur, an erosion model is needed to predict nutrient losses. The soils in stands that can be analyzed by NUTROSS tend to have high infiltration but slow percolation rates and abundant vegetative cover. It is not unusual to find Montana forests with soils that are below field capacity at the lower extent of the feeder root zone on June 1. High-elevation forests with heavy snow packs contribute most heavily to streamflow in the Rockies. Nutrient losses to streams after logging at low elevations are usually insignificant (Stark 1982).</p>
	<p>In Montana, most nitrogen fixation occurs in old logs, shrub or herb roots, or lichens that are not normally exported from the site. There may be some change in <span title="nitrogen">N</span> fixation in logs after clearcutting because of increased soil temperature, but the nitrogen fixation system is left intact. If logging does not remove the nitrogen fixers themselves or their substrate, and there are minimal solution losses of <span title="nitrogen">N</span> to streams, then there is no need to spend money estimating nitrogen loss. NUTROSS does not attempt to predict future tree growth. Until we can accurately predict future climates, atmospheric CO2 and pollutants, disease and insect outbreaks, or mutations, it is risky to try to predict future tree growth. What we can do is to identify how much nutrient is available in the soil at the time of harvest and evaluate how many harvests can be supported by the available soil and litter nutrient pools, assuming minor climatic change. Since slash normally remains onsite, most of the nutrients in the slash should be released over 70 to 100 years in amounts adequate to supply those same components during the next rotation.</p>
	<p>If sedimentary cycle nutrients do limit tree growth at the time of harvest, then the site is chemically fragile to that level of harvest and NUTROSS will show significant nutrient losses resulting from harvest. If nitrogen or other nutrients limit growth prior to harvest, foliar analysis will identify the problem before the model is applied. The inputs to the model require foliar analyses for micro- and macro elements. The standards for foliar analyses are in the frequency distribution table prepared by Zinke and Stangenberger (1979) or Stark (1981) or locally acceptable standards used to judge good growth for the species in question. If the foliage has a clear deficiency of a sedimentary cycle nutrient, then there is no need to run NUTROSS because we know that the soil does not have an adequate available nutrient pool to supply these nutrients for the next three to four rotations. The only ecologically sound alternative is not to harvest. If the foliage has sufficient sedimentary cycle nutrients but is deficient in N, harvest must be planned to minimize damage to the <span title="nitrogen">N</span>-cycle by leaving large logs and more biomass onsite, or in extreme cases, aborted altogether.</p>
	<p>NUTROSS is intended for sites where trees are limited in growth more by nutrients than water or cold soils, although water may limit growth late in the growing season. Xylem sap or foliar analyses that show deficient nutrient levels before spring flush when water is normally not limiting to tree growth (Stark and others 1985) are useful in evaluating the presence of nutrient stress.</p>
	<p>With NUTROSS the user can specify any degree of harvest from a light thinning to clearcut by indicating a percentage of the total biomass to be removed and whether boles or whole trees would be cut. If a particular level of harvest predicts nutrient losses in excess of 25 to 30 percent, then the user can instruct the model to specify what level of harvest would result in acceptable or specified levels of nutrient loss. The final printout of NUTROSS displays what percentages of each nutrient (except nitrogen) will be removed with the proposed treatment.</p>
	<p>These concepts are based on weathering studies in the Northern Rockies by Clayton (1984). They would change for other geographic areas and could be adapted to other semiarid forest ecosystems.</p>
	<h2>USING THE MODEL</h2> 
	<p>There are some specific limitations to the use of NUTROSS in its present form. The model is recommended for the following conditions:</p>

	<ol style="list-style-type: lower-alpha;">
  	<li>Moderately permeable soils in low-rainfall areas (&lt;76 cm/yr).</li>
  	<li>Forested land that normally does not contain an appreciable nutrient export component for streamflow (before or after harvest).</li>
  	<li>Soils moderate in clay content (&lt;40 percent), low in gravel or sand (&lt;40 percent).</li>
  	<li>Forests with litter and duff accumulations &lt;6-8 cm.</li>
  	<li>Forests with continuous understory vegetation or with species that sprout or reestablish quickly after disturbance.</li>
  	<li>Soils without sand lenses that &quot;pipe&quot; water.</li>
  	<li>Major nitrogen fixers that reside in rotten wood, shrubs, or lichens.</li>
	</ol>

	<p>To use NUTROSS, the following must be known:</p>

	<ol style="list-style-type: lower-alpha;">
  	<li>D.b.h. and height of timber present before and after harvest by species on &plusmn;10 sample plots per stand. Total stand volume present and projected for removal by species must be converted to weight per square meter using Brown (1978) or Faurot (1977).</li>
  	<li>Available nutrient content of the feeder root zone (subsamples composited within each profile by volume in 10-cm depth increments to the depth of the feeder root zone) for &plusmn;10 locations per stand.</li>
  	<li>Ten samples of the most recent needles from the lower third of the crown must be analyzed for the same 14 nutrients. These data in &mu;g/g are examined for possible nutrient deficiencies. If severe nutrient deficiencies are identified (Zinke and Stangenberger 1979), and tree radial growth is poor (for Montana, &gt;5 rings/cm) for the species and climate (because of nutrients), NUTROSS may not be needed because the site may not be suitable for tree harvest at any level. If there is a question about site potential in the absence of large trees, NUTROSS can be run using published data on the species where maximum and minimum nutrient concentrations (Stark 1981) allow calculation of the &quot;worst and best possible case&quot; scenarios.</li>
  	<li>The depth of the feeder root zone (95 percent of feeder roots) is measured in 10 soil pits per stand.</li>
  	<li>The percent coarse fragments (&gt;2 mm), soil texture, litter depth, percolation through the B horizon, and annual precipitation are valuable to decide if the model is appropriate to a particular area.</li>
	</ol>

	<p>The total recyclable nutrient load aboveground for each nutrient in the material to be harvested is converted to meq/m<sup>2</sup> and added to the available feeder root zone nutrients to represent the nutrient pool that could be lost. Those nutrients left behind in unharvested materials are assumed to remain onsite to grow the same components in the next rotation because of low leaching losses. If boles only are to be removed, then the nutrients in the boles represent presently removable nutrients. Nutrients (individually) in the bole to be removed are divided by the total of each nutrient in the potentially harvestable pool &times; 100, representing the percent nutrient loss from the ecosystem resulting from that type of harvest. Although nutrients in the needles and branches can be lost, the assumption is that they will not be lost because: (a) they will not be removed in harvest, and (b) once mineralized, the nutrients released from them into the soil are not likely to be leached away because low precipitation and soil texture prevent a flush of the soil nutrients to streams.</p>
	<p>If any nutrient to be removed during harvest exceeds 25-30 percent, the site cannot support three rotations in the future without subsidizing the nutrient pool from weathering, and that level is inappropriate for the site in question. Different harvest intensities can then be evaluated through the model to identify the harvest level suitable for the nutritional status of the particular site.</p>
