	<h2>CONCLUSIONS</h2> 
	<p>The NUTROSS model can be used economically to examine the impacts of various levels of harvest on ecosystem available nutrient losses. Under clearcutting, stands 1, 5, 6, and 7 would be subjected to nutrient losses in excess of 25 or 30 percent, the projected cutoff point for sound harvest practices. The model not only identified the chemically fragile soils, but it shows that Cu, Zn, and Na are the nutrients most likely to be adversely impacted by excessive harvest. The model also showed considerable variability in percent nutrient losses among treatments within a stand. These variations resulted from differences in original stocking level, species composition, or soil chemistry.</p>
	<p>By September of 1990, the NUTROSS software will be available for distribution by the authors.</p>
