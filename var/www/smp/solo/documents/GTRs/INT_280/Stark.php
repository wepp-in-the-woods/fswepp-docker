	<p>Then the amounts of each nutrient removed are compared to the amounts available as meq/m<sup>2</sup> in the litter and to the depth of the feeder root zone in the soil.</p>
	<p>Since ecosystem nitrogen should be considered, the model is being modified to calculate how much of each nutrient would be left on the site if 16 metric tons of logs were left behind. Larsen and others (1978, 1979) determined that this amount of woody material is necessary to supply the organic habitat for nitrogen-fixing organisms and mycorrhizae needed for the uptake of a number of nutrients. Thus, 16 dry tons of wood/acre that would otherwise be harvested actually remain onsite, reducing the sedimentary nutrient drain from the harvest of boles and assuring reasonable habitat for mycorrhizae and nitrogen fixation.</p>
	<h2>FIELD TEST OF NUTROSS</h2> 
	<p>Harvest studies at Lubrecht, 58 km east of Missoula, <span title="Montana">MT</span>, resulted in the removal of different amounts of fiber and nutrients from several PSME (Pseudotsuga menziesii, Douglas-fir) habitat types. The soils are of varying geologic origin, and hence, of varying fertility. The stands were variable in stocking density before treatment. A question arose concerning the possible depletion of the available nutrient pool due to whole-tree harvest. Seven stands of varying species composition were measured before harvest. The following four treatments were planned for each stand:</p>
	<ol>
  	<li>Control</li>
  	<li>3- by 3-m spacing</li>
  	<li>4- by 4-m spacing</li>
  	<li>6- by 6-m spacing</li>
	</ol>
	<p>Sampling was conducted as indicated earlier, and NUTROSS was run for eight nutrients.</p>
	<p><a href="#table1">Table 1</a> shows the projected percent of available ecosystem nutrients that would be lost if each stand had been clearcut (with boles only removed) rather than thinned. The treatments were not applied in this case, but the data were taken from plots within each stand before treatment. In theory, if the stand densities, tree sizes, ages, and soils were uniform, each treatment within a stand would show the same projected percent nutrient loss from clearcutting. Stands 1, 2, 5, and 7 show variability, especially for the projected losses of copper and zinc (<a href="#table1">table 1</a>). Stands 5 and 7 are also highly variable and show high (&gt;30 percent) projected nutrient losses from clearcutting with the removal of only the boles. If whole trees were to be removed, nutrient losses from stands 4 and 6 would be severe (&gt;30 percent for <span title="sodium">Na</span>). Sodium losses are significant only to the extent that <span title="sodium">Na</span> aids in establishing osmotic potentials allowing cell expansion. Sodium is not a nutritional component of trees. The stands were marginal for clearcutting because of an apparent high level of removal of ecosystem copper, and in some cases <span title="sodium">Na</span> and <span title="zinc">Zn</span>. Stands 2 and 3 appear to be best able to withstand clearcutting because of low total ecosystem nutrient losses from bole removal. Stand 7 had high variability among subplots. <a href="#table1">Table 1</a> also lists the species composition and habitat type of each stand.</p>

