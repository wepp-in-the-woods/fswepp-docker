	<p>Speakers answered questions following their presentations. Following are the questions and answers on this topic:
	</p>
	<p><strong>Q. N is the only element shown in regional nutrition studies to be limiting to tree growth; P, K, and S also may be limiting. How can you relate micronutrients (Zn, Cu, etc.) to tree growth? There does not seem to be that much demonstrated connection between tree growth and micronutrients.</strong>
	</p>
	<p><strong>A.</strong> Early studies with western larch (Behan) and scotch pine have shown that trees require micronutrients in order to grow normally. The Australian literature has prolific references to deficiencies of Cu and Zn that have been demonstrated to reduce tree growth. (Carlyle and others 1989, Canadian Journal of Forest Research, and many others). Our research on xylem sap chemistry in western Montana has shown that nutrient imbalances, particularly trace metal deficiencies, do exist in geologically young soils or calcareous soils and that these are responsible for limiting tree growth (Stark and others 1989, Canadian Journal of Forest Research). Likewise, an accumulation of trace metals in the understory vegetation has been demonstrated to correlate strongly with poor tree growth on geologically young soils such as are common in the mountainous regions of the Northwest (unpublished data). No forester wants to believe that trace metals are important to tree growth, but the evidence is piling up rapidly. If you try to grow tree seedlings using pure chemicals with only macronutrients, they will not grow.
	</p>
	<p><strong>Q. In many of our stands, only some of the branches are removed to a landing and burned (approximately 50 percent). Will NUTROSS handle this kind of scenario?</strong>
	</p>
	<p><strong>A.</strong> Yes, with a minor manipulation.</p>
