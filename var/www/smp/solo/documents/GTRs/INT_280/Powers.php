	<h1>ARE WE MAINTAINING THE PRODUCTIVITY OF FOREST LANDS?
ESTABLISHING GUIDELINES THROUGH A NETWORK OF LONG-TERM STUDIES</h1>
	<p class="authors">
		Robert F. Powers</p>
	<h2>ABSTRACT</h2>
	<p>Protecting the inherent capacity of forest land to grow vegetation is a goal of modern forest management and a legal requirement of the Forest Service, U.S. Department of Agriculture. Options for judging this capacity include potential tree growth and potential net primary productivity of all vegetation. Evidence exists that this capacity is degraded by substantive losses of site organic matter and soil porosity. If such potentials were known for each site, and if changes in key soil properties were calibrated against these potentials, the relative impacts of forest practices could be assessed objectively and directly. Unfortunately, few data have been collected for establishing such calibrations, and managers must turn to soil-quality monitoring standards that are based more on professional judgment than on rigorous science. This paper describes a cooperative study between Research and Administrative arms of the Forest Service to establish such calibrations through a national network of long-term productivity studies.</p>
	<h2>INTRODUCTION</h2>
	<p>Sustaining the long-term productivity of our Nation's forests is an ethical and economic aim of forest management. Ethics relates to a willingness to sacrifice personal gain for the good of a social pyramid with society at its apex and the land at the base (Leopold 1949). Economics refers to protecting the land's ability to meet consumer demands by sustaining a profitable flow of forest products. For the Forest Service, U.S. Department of Agriculture, there is a legal reason, too. Among the World's nations, the United States is unique in its mandate for good stewardship of public lands. By law, the Forest Service must monitor the</p>
	<p>This paper serves several purposes. First, it reviews the main concepts of forest site productivity. It also acquaints the reader with the legal basis for protecting long-term productivity and its implications for field monitoring. Next, the paper summarizes what is known about productivity decline due to poor management. Finally, it describes a newly established Forest Service cooperative program that tackles the subject head-on.</p>
	<h2>SITE PRODUCTIVITY CONCEPTS</h2>
	<p>The productivity of a forested site is shaped by abiotic factors of climate, soil, relief, and mechanical damage by fire, flood, or wind. Productivity also is shaped by biotic factors involving tree genetics, stand age, stocking and degree of competition, and stand health as conditioned by insects, diseases, other pests, and symbionts. Except for such random disturbances as fire, flood, or wind, abiotic factors change relatively slowly under natural conditions. In contrast, most of the biotic factors (tree age, stocking, health) are dynamic. For example, growth rates vary with age. Usually, they are low during juvenility, accelerate with approaching maturity, and decline thereafter. Some abiotic and many biotic factors can be affected by management either by design, or inadvertently. And the degree to which this is understood depends on one's definition of &quot;productivity.&quot;</p>
	<p><strong>Timber Site Quality&#8212;</strong>Historically, the forestry concept of site productivity has centered on the capacity of the land for growing wood of commercial value. Traditionally, this capacity or &quot;timber site quality&quot; is expressed as volume increment averaged over a period of years. Timber site quality has both an actual and a potential component.</p>
	<p>Actual and potential volume increment rarely coincide. The former is the fraction of a site's capability that is actually realized by management. Generally, this is less than the site's potential. For example, trees spaced widely in understocked stands will have lower collective growth rates than trees growing closely. Furthermore, trees weakened through severe and prolonged competition or from mechanical injury are susceptible to insects and diseases, which decreases absolute growth. All of this means that the actual site productivity of a given unit of forest land varies continually. A corollary is that management can affect apparent productivity simply by manipulating stocking, species, and pests.</p>
	<p>Besides its actual productivity, each land unit has a theoretical maximum productivity, or an upper limit for the rate of wood growth when the stand is fully stocked and other factors are held constant. This is the concept of absolute or potential site quality (Spurr 1952), only part of which generally is realized by management. Estimating this potential is difficult. Variable density yield functions may be manipulated to approximate the potential upper limit of wood growth, but empirical yield tables generally reflect norms for stocking and incorporate incipient degrees of stress from insects and diseases. Thus, yield tables would tend to underestimate potential site quality.</p>
	<p>Although it is the historical measure of site quality, wood production has a relatively low priority in the tree's allocation of photosynthate. Production of roots, foliage, and reproductive structures all take precedence (Grier and others 1989). Thus, wood production is a rather incomplete and limited measure of the quality of the whole site for forest growth. Is there an alternative?</p>
	<p><strong>Total Site Productivity&#8212;</strong>The fundamental ecological measure of site productivity is total dry matter produced by autotrophic plants. This &quot;net primary productivity&quot; (NPP) is expressed by the equation:</p>
	<blockquote><span title="net primary productivity">NPP</span> = <span title="gross primary productivity">GPP</span> &#8722; Rs<sub>A</sub></blockquote>
	<p>where GPP is gross primary productivity from total
photosynthesis and Rs<sub>A</sub> is respiration of all parts of autotrophic plants.</p>
	<p>Units of <span title="net primary productivity">NPP</span> generally are expressed as dry mass of organic matter produced per unit area each year, such as Mg/ha/yr. Not all <span title="net primary productivity">NPP</span> appears as standing biomass. Some that is produced annually is cycled to the forest floor and soil or is consumed by grazing animals. The quantity that does accumulate can be defined as:</p>
	<blockquote><span title="net primary productivity">NPP</span><sub>B</sub> = <span title="net primary productivity">NPP</span> &#8722; NPP<sub>C</sub> &#8722; <span title="net primary productivity">NPP</span><sub>D</sub></blockquote>

	<p>where <span title="net primary productivity">NPP</span><sub>B</sub> is the net primary productivity accumulated annually in standing biomass of all plants above ground, below ground, or both; <span title="net primary productivity">NPP</span><sub>C</sub> is the biomass consumed annually by pathogens, insects, and grazing animals; and <span title="net primary productivity">NPP</span><sub>D</sub> is the actual production of detritus from litterfall and root mortality.</p>
	<p>As indicated by the paucity of estimates of <span title="net primary productivity">NPP</span> available for forests of western North America (<a href="#table1">table 1</a>), <span title="net primary productivity">NPP</span> and its individual components are not estimated easily. Tables and equations for estimating tree stem volumes from diameter and height are common, but these generally have a commercial bias (bole volumes to a minimum top diameter, rather than dry matter) and do not account for branches, foliage, or roots. Usually, destructive sampling, drying, and weighing are used to relate the mass of individual tree components through regression to more easily measured tree dimensions. Measurements taken at different times can then be used to estimate standing <span title="net primary productivity">NPP</span>. Obviously, such work is extremely costly, and the equations often are specific only to the stand in question. The detritus component (<span title="net primary productivity">NPP</span><sub>D</sub>) can be estimated by weighings of periodic litterfall, but this can be complicated by storm-induced detritus. Estimating <span title="net primary productivity">NPP</span><sub>C</sub>  difficult and often is ignored, although it can be a sizable component of <span title="net primary productivity">NPP</span>. Insects and diseases can reduce productivity by as much as 25 to 50 percent in western forests (Stewart 1985; Stoszek 1973).</p>
