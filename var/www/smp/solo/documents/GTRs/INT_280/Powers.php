	<h2>SOIL-QUALITY MONITORING STANDARDS</h2>
	<p>The principle behind the Forest Service approach is illustrated in <a href="#fig1">figure 1A</a>. For any given soil and site, a change in a key soil variable (for example, a loss in porosity) will lead ultimately to a change in potential productivity. If the soil variable is not closely linked with productivity, changes in the condition of that variable will have little or no bearing on productivity (potential productivity remains stable along the &quot;line of no change&quot; in <a href="#fig1">fig. 1A</a>). Obviously, a key feature is that the soil monitoring variable must have a close link with potential productivity.</p>
	<p>The conceptual model in <a href="#fig1">figure 1A</a> is simplistic. It implies that potential productivity--the &quot;dependent variable&quot;is a stable and known value. In reality, there is a belt of uncertainty surrounding a productivity estimate that is due to climatic vagaries and to limits in our knowledge. This uncertainty is shown as a shaded band about the line of no change in <a href="#fig1">figure 1B</a>. Uncertainty about the true value of potential productivity leads to uncertainty about how much change a soil can undergo before productivity is affected. Recognizing this, and based largely on collective judgment, the Forest Service estimates that a true productivity decline would need to be as great as 15 percent to be detectable by modern monitoring methods. Thus, soil-quality threshold standards are being set to detect a decline in potential productivity of at least 15 percent (<a href="#fig1">fig. 1B</a>). This does not mean that the Forest Service tolerates productivity declines of up to 15 percent, but merely that it recognizes problems with detection limits.</p>

