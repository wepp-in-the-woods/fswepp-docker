	<p>This cluster of nine treatment plots will cover the range of site organic matter and soil porosity changes apt to occur under present or future forest management. Other treatments such as topsoil removal, conventional harvest and site preparation techniques, or ameliorative practices may be installed at the prerogative of the Regional Steering Committee, provided that they do not confound or alter the core cluster of nine treatments. Work has begun on about a dozen U.S. sites as of this writing, and another dozen should be on line by 1992 (<a href="#fig3">fig. 3</a>).</p>
	<p>Generally, each 0.4-ha treatment plot will be planted with seedlings of the appropriate timber type and a mixture of the best available genetic stock. Natural regeneration (seeding or sprouting) may be substituted where appropriate. The aim is to favor superior growth without narrowing genetic diversity. Each treatment plot will be split in half, creating two 0.2-ha subplots of about 340 trees each (<a href="#fig4">fig. 4</a>), with a measurement plot established from the fourth row of trees inward in each subplot. After establishment, one subplot will be kept weed free. In the other, regional vegetation will be encouraged to grow with the trees&#8212;the aim being to promote complete vegetative recovery as rapidly as site conditions permit so that the site approaches full carrying capacity for vegetative growth. This split-plot arrangement creates a means for side-by-side comparisons of (1) stand productivity vs. total vegetative productivity, and (2) the effect of competing vegetation on tree growth.</p>

