	<p>National Technical and Regional Steering Committees are responsible for establishing and conducting the <span title="Long-term Soil Productivity">LTSP</span> studies.</p>
	<p><strong>National Technical Committee&#8212;</strong>The National Technical Committee for <span title="Long-term Soil Productivity">LTSP</span> consists of Principal Investigators and Regional Soil Scientists who presently are, or who expect to be, involved in implementing and maintaining studies and interpreting study results. This committee, chaired by a Principal Investigator appointed by the National Oversight Committee, will have five main responsibilities:</p>
	<ol>
  	<li>Reviewing study proposals to ensure that scientific methods are consistent and appropriate to meet program objectives.</li>
  	<li>Establishing a national data base of research results.</li>
  	<li>Informing the Washington Office of progress, needs, opportunities, and substantive findings.</li>
  	<li>Coordinating and preparing results for publication.</li>
  	<li>Reviewing, evaluating, and incorporating modifications to the proposals.</li>
  </ol>
	<p><strong>Regional Steering Committee&#8212;</strong>The Regional <span title="Long-term Soil Productivity">LTSP</span> Steering Committee consisting of the Experiment Station Principal Investigator, Regional Soil Scientist, and Regional Silviculturist initiates and encourages cooperative and collaborative ties with National Forests, Ranger Districts, and other researchers. This committee identifies individual study sites, prepares specific study plans based on this general plan, and implements the studies. The Regional Committee shares responsibility with National Forests and Ranger Districts for ensuring public awareness of the program. Departures from procedures described in the study plan must be approved by both the National Technical Committee and the National Oversight Committee. Study sites should be identified in the Monitoring Plan for each National Forest.</p>
	<p><strong>Affiliations&#8212;</strong>Prospective collaborators from a dozen U.S. universities have formed an ad hoc committee to explore ways to secure funding for joint <span title="Long-term Soil Productivity">LTSP</span> research with Forest Service scientists. International collaboration also is under way through British Columbia's Ministry of Forests and New Zealand's Forest Research Institute for parallel <span title="Long-term Soil Productivity">LTSP</span> studies using our protocol.</p>
	<h3>Experimental Protocol</h3>
	<p><strong>Treatments&#8212;</strong>A broad range of manipulations of soil porosity and site organic matter are applied on benchmark soils within the major commercial forest types of the United States. Work began in 1989 in the Pacific Southwest and Southern Regions on lands expected to be managed intensively in the decades ahead. In 1990 and 1991 the <span title="Long-term Soil Productivity">LTSP</span> program expanded to the Northern and Eastern Regions (<a href="#fig3">fig. 3</a>), and it continues to expand both domestically and abroad.</p>

