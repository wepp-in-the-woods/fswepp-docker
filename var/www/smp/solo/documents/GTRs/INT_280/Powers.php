	<h2>SUMMARY</h2>
	<p>Our ability to maintain a site's productive capacity faces increasing challenge through public review of Forest Land Management Plans and timber sales. In response to <span title="National Forest Management Act">NFMA</span>, Forest Service Regions are developing threshold soil-quality standards for detecting declines in potential soil productivity. Such standards are based on best available information. Often, this amounts to professional judgment, because research has not addressed the problem squarely. Professional judgments will be subject to repeated challenge from many sectors. Results from this cooperative study will provide credible responses to many of these challenges, and will address related research needs across the Nation. An immediate benefit is a clear show of good faith by the Forest Service to adhere to the spirit of <span title="National Forest Management Act">NFMA</span> and to tackle the problem aggressively.</p>
	<p>This national network of study sites will provide the scientific basis for validating soil-quality standards established by <span title="National Forest System">NFS</span>, and creates a research opportunity of unusual scope and significance. Initially, existing standards for monitoring soil quality will be compared with interim findings and can be adjusted to reflect the most recent research results. With time, more substantive results will be available for future planning. All Regions and Stations are encouraged to participate to ensure the success of this joint program.</p>
	<p>The experimental design provides researchers with the framework for comparing stand production with more fundamental measures of productivity. Because a gamut of stress conditions is imposed deliberately on vegetation, pest and disease interactions are likely, and multidisciplinary collaboration will be encouraged. Basic models of soil and growth processes can be integrated with site and climatic data to extrapolate findings to a broad array of sites, and to project the possible impacts of changing climate on future productivity. The work fosters close cooperation between Research and <span title="National Forest System">NFS</span>, and opens doors for important work with university and industry colleagues.</p>
