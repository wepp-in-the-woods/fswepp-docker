	<p>Climatic conditions favoring plant growth favor <span title="net primary productivity">NPP</span>. Thus, it is not surprising to see that the highest average rates of <span title="net primary productivity">NPP</span> occur in warm temperate and tropical forests, and the lowest are found in cold or arid regions (<a href="#table2">table 2</a>). Lieth (1975), in analyzing <span title="net primary productivity">NPP</span> rates reported for all forest biomes, suggested that a productivity ceiling exists at a little over 30 Mg/ha/yr under the most favorable conditions of precipitation and temperature. Interestingly, dense young forests of <em class="ital">Tsuga heterophylla</em> (Raf.) Sarg. can achieve this level of <span title="net primary productivity">NPP</span> (<a href="#table1">table 1</a>). Much, perhaps three-quarters of a site's total <span title="net primary productivity">NPP</span>, is directed below ground (Grier and others 1981), where a very high proportion soon becomes detritus from senescing fine roots and mycorrhizae. On the average, only 10 to 30 percent of total tree biomass is maintained below ground, but as much as three-quarters of ecosystem organic carbon is stored there and in the forest floor (<a href="#table2">table 2</a>).</p>
