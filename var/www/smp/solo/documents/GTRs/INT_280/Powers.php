	<p>Just as with timber site quality, <span title="net primary productivity">NPP</span> has both an actual and potential component that varies with the same factors shaping wood production. Actual <span title="net primary productivity">NPP</span> increases steadily as a stand develops, and reaches a maximum rate with the advent of canopy closure, when the site has achieved its carrying capacity for foliage (Grier and others 1989). At this point, leaf mass stabilizes barring disturbance, and litterfall may represent a fairly constant proportion of stand <span title="net primary productivity">NPP</span>. Potential <span title="net primary productivity">NPP</span> depends then on stand age. However, at any given stage of forest development, potential <span title="net primary productivity">NPP</span> would be the total amount of dry mass produced per year when the site is fully stocked with vegetation. Grier and others (1989) provide an extensive discussion of problems and approaches in estimating above- and belowground biomass and <span title="net primary productivity">NPP</span>. As we shall see, site productivity concepts have a direct bearing on how public forest lands are managed in the United States.</p>
	<h2>PUBLIC LAND LAW AND MONITORING REQUIREMENTS</h2>
	<p>The Multiple Use-Sustained Yield Act of 1960 (Sec. 4.[b]) binds the Forest Service to achieve and sustain outputs of various renewable resources without permanently impairing the productivity of the land (USDA Forest Service 1983). This mandate was reinforced in the National Environmental Policy Act of 1969 and the Forest and Rangeland Renewable Resources Planning Act of 1974. Directions were refined further by Sec. 6.(g)(3)(c) of the National Forest Management Act of 1976 (NFMA) which charges the Secretary of Agriculture with ensuring research and monitoring of the effects of each management system to protect the permanent productivity of the land (USDA Forest Service 1983).</p>
	<p>In response to <span title="National Forest Management Act">NFMA</span>, an independent Committee of Scientists was appointed by the Secretary of Agriculture to help develop regulations for implementing the law. This led to a Code of Federal Regulations for Forest Planning which, among other stipulations, requires the Forest Service to monitor the effects of prescriptions, including &quot;significant changes in land productivity&quot; (Code of Federal Regulations 1985). The Chief of the Forest Service then directed each of the nine Forest Service administrative Regions to develop monitoring procedures for detecting significant changes in land productivity over a planning horizon (a rotation).</p>
	<h2>IMPLEMENTING THE REGULATIONS</h2>
	<p>The Forest Service's first task was to define the scope of its monitoring responsibility. &quot;Land productivity&quot; might encompass wildlife, watershed, fisheries, esthetic, and timber values. All of these are valid components of productivity, but they are not equally measurable. Some are intangible, subjective, or temporally unstable. Discussions with the Office of General Counsel and other parties helped form a more objective and usable definition of &quot;land productivity&quot; and &quot;significant change&quot; (USDA Forest Service 1987).</p>
	<p>&quot;Land productivity&quot; was defined as a soil's capacity to support plant growth as reflected by some index of biomass accumulation. Although it may not be the broadest measure of productivity, plant growth is a useful index of ecosystem health. Losing a soil's plant growth capacity also means losing the site's capacity for sustaining other resource values. Further, a &quot;significant change&quot; in productivity was defined as the minimum level of reduced plant growth that is detectable using current technology.</p>
	<p>Thus, the Forest Service is charged with protecting the inherent capacity of the soil to sustain plant growth, and to monitor the consequences of forest practices to the end that this capacity is not endangered. What, then, is &quot;the inherent potential of the soil?&quot; Assessing significant changes under operational field conditions is not simple, nor has the soil's potential been quantified. If current productivity is too variable, and if potential productivity is not easily assessed, could a substitute measure be used?</p>
	<p>The Forest Service believes it is the soil. Along with climate, relief, and biology, soil sets the limits on productivity within a region through its control of nutrient, moisture, and air supplies to tree roots. &quot;Soil productivity&quot; is the term that describes this. If we knew the potential productivity inherent to each soil type, and the important factors that distinguish one soil from another, then the changes in those factors caused by management could be used to indicate changes in inherent, potential productivity. Thus, soil becomes a logical focus for monitoring. The Forest Service's Watershed and Air Management Staff has assumed responsibility for monitoring soil productivity. Monitoring strategy is based on three principles:</p>
	<ol>
  	<li>Management practices create soil disturbances.</li>
  	<li>Soil disturbances affect soil and site processes.</li>
  	<li>Soil and site processes control site productivity.</li>
  </ol>
	<p>Monitoring soil and site processes directly is not feasible. Instead, monitoring focuses on measurable soil variables that reflect important site processes. For example:</p>
