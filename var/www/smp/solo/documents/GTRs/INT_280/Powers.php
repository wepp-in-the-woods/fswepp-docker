	<p>What is the technical basis for soil-quality monitoring standards? Often, they are based on the collective judgment of professionals because the subject has not been tackled from a direct, scientifically rigorous perspective. But is there sound evidence that soil property changes are associated with substantial declines in productivity?</p>
	<h2>HAS PRODUCTIVITY DECLINED?</h2>
	<p>Effects of massive slope failure and loss of the soil mantle on long-term productivity are so obvious that they need not be documented here. But do less dramatic activities also degrade? Evidence from the United States tends to be confounded, or short term and inconclusive (Powers and others 1990). The best clues come mainly from abroad where forestry has been practiced longer, records are more complete, or multiple short rotations have occurred. World findings have been reviewed recently by Powers and others (1990), and the main points are summarized here.</p>
	<h3>Removal of Biomass</h3>
	<p>Utilization standards affect rates of biomass and nutrient removal, and nutritional aspects have drawn most of the research attention. Generally, one-tenth of total ecosystem nitrogen and a smaller fraction of ecosystem phosphorus is contained in the biomass of young, mature forests (<a href="#table3">table 3</a>). Of this, at least half of the nitrogen and phosphorus is contained in foliage and branches. As a rule, a 1 percent increase in biomass removal means a 3 percent increase in nutrient removal (Switzer and others 1981). Direct evidence of productivity decline from biomass removal is rare. Sterba (1988), in a thinning study in Austria, found that residual trees had 12 percent greater growth if felled trees remained on the site than if they had been removed. However, the reason for this remains speculative.</p>

