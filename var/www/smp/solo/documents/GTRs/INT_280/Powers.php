	<p>Although soil-quality monitoring standards now in use tend to focus on changes in porosity and organic matter, they are based mainly on &quot;best professional judgment&quot; from extrapolation of anecdotal studies or from general observations that are subject to various kinds of bias. Definitive calibrations such as depicted in <a href="#fig1">figure 1</a> simply do not exist. Because of this, many standards may face legal challenge as to whether they are too restrictive, or not restrictive enough. The effectiveness of current standards must be validated. If standards are found wanting, they must be improved.</p>
	<p>Work is needed at both the fundamental and applied level to quantify the effects of soil disturbance on potential productivity not only in a timber management sense, but in a more fundamental sense as well. We know little about the inherent carrying capacity of forest sites for producing vegetation, or about its relationship to key soil variables. Yet, this relationship must be known if we hope to produce accurate calibrations (<a href="#fig1">fig. 1A</a>) and effective soil-quality monitoring standards (<a href="#fig1">fig. 1B</a>). Such calibrations must be developed if the Forest Service is to meet its legal and ethical responsibility. Clearly, Forest Service Research has the mandate for conducting such work. But the scope and scale of the problem are far too vast for Forest Service Research to tackle alone. There must be a partnership.</p>
	<h2>A COOPERATIVE NATIONAL STUDY</h2>
	<p>In December 1986, at the annual meeting of the Soil Science Society of America in New Orleans, an informal session was held for Forest Service attendees. There, P. E. Avers National Soils Group Leader from the Washington Office outlined the problem of soil-quality monitoring facing <span title="National Forest System">NFS</span>, and asked for assistance from Research. Further discussions were held between Avers, D. H. Alban, and R. F. Powers. The following June, Avers and four scientists from the North Central, Pacific Northwest, Pacific Southwest, and Southern Forest Experiment Stations met in a California field setting to review the scope of the problem and discuss possible solutions. This was followed by a March 1988 session in St. Louis to plan a formal strategy for Research and <span title="National Forest System">NFS</span> collaboration. Attendance was expanded to include a scientist from the Southeastern Station and one representative each from Timber Management, Timber (now Forest) Management Research, and Forest Environment Research staffs of the Washington Office.</p>
	<p>These meetings led to the creation of two major documents. The first was an extensive, critical literature review concerning the long-term impacts of forest practices on potential productivity. Part of this was a proposal for a new national research effort. The paper was presented before an international gathering of scientists in July 1988, and was published in the peer-reviewed proceedings of the conference (Powers and others 1990). The second document was a plan prepared in October 1988 by Research and <span title="National Forest System">NFS</span> personnel outlining a cooperative national Forest Service study of the impacts of management practices on long-term soil productivity (LTSP). The plan was presented to the Chief's staff and received extensive technical review both in the United States and abroad. In 1989 it was approved by the Washington Office (Powers and others 1989), and the plan now serves as a template for more detailed regional studies.</p>
