	<h3>Coordination</h3>
	<p>Three levels of coordination are described to ensure the achievement of national and regional objectives in the national <span title="Long-term Soil Productivity">LTSP</span> program.</p>
	<p><strong>National Oversight&#8212;</strong>National coordination and review for <span title="Long-term Soil Productivity">LTSP</span> is provided by Forest Management Research, Forest Environment Research, Timber Management, and Watershed and Air Management Washington Office staffs. Coordinating activities include:</p>
