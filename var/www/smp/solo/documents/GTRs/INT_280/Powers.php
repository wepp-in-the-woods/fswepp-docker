	<h3>Responsibilities and Implementation</h3>
	<p>Costs, estimated at about $74,000 per installation for the first 5 years, will be shared by <span title="National Forest System">NFS</span> and Research. In general, <span title="National Forest System">NFS</span> is responsible for establishing and maintaining the sites. Research is responsible for data collection, analysis, and publication. Study plans prepared jointly by Stations and Regions will detail the responsibilities. Contacts and field visits with research and practicing entomologists, pathologists, and wildlife specialists should be frequent. Once funded, approximately 14 months will be the minimum time needed to locate and install a series of study sites within a timber type (<a href="#fig5">fig. 5</a>). The first strong indication of long-term treatment effects can be expected in 5 to 10 years after planting on average sites.</p>

