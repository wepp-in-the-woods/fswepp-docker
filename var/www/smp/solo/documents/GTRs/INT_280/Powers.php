	<p>Organic matter and nutrient losses in conventional timber harvests and rotations seem unlikely to affect potential productivity on most sites (Wells and Jorgensen 1979). However, shorter rotations deplete nutrients much more rapidly than do long rotations. Switzer and others (1981) estimated that with whole-tree harvesting (the removal of all aboveground biomass), three 20-year rotations in loblolly pine would remove three-quarters more organic matter and twice the nitrogen as one 60-year rotation. Even so, this amounted to less than 10 percent of the nitrogen in the ecosystem. To date, assessments of the impacts of increased utilization or shortened rotations are based mainly on model projections and not empirical evidence. They are predicated on many assumptions lacking rigorous proof.</p>
	<h3>Loss of Organic Matter</h3>
	<p>In South Australia, Keeves (1966) showed that radiata pine planted on sandy soils grew much more poorly in the second rotation than in the first. Squire and others (1985) demonstrated that the decline was triggered by the burning of logging slash, which led to nutrient and moisture stress. The effect was evident by the time of crown closure. They showed that the growth of second rotations could exceed that of first rotations if logging residues were retained. Findings suggest that organic matter retention is particularly important on infertile, droughty sites.</p>
	<p>Organic matter in the forest floor (surface residues of plants and animals that are not yet soil) has singular significance in respect to productivity. Although its mass is only a small fraction of that in the standing forest (<a href="#table2">table 2</a>), its nutrient content often equals or exceeds the combined total for the trees and understory vegetation (<a href="#table3">table 3</a>). Substantial losses of forest floor material have degraded some sites. Litter raking the regular removal of freshly fallen conifer needles was practiced in central European conifer forests for centuries to obtain bedding straw for farm animals. Wiedemann (1935) found that several decades of litter raking in Scots pine plantations on sandy soils in eastern Germany led to higher soil densities and to growth declines of nearly two site classes. Soils also were lower in fertility (Baule and Fricker 1970). Similar results were found for radiata pine in New Zealand, where 26 years of litter raking led to appreciable declines in soil nutrients, an increase in bulk density, and about a 12 percent decline in growth (Dyck and Skinner 1990).</p>
	<h3>Loss of Soil Porosity</h3>
	<p>Sands (1983), working with radiata pine in Australia, found sizable increases in soil density on sites converted from pasture to pine plantations. The effect increased by the second rotation because of multiple passes of machinery, and approached a growth-limiting density at depth. In the United States, Froehlich and others (1986) and Helms and others (1986) reported that trees growing on compacted soils contained only about one-fifth the volume of trees growing on less compacted soils nearby. Growth losses depend on the degree of compaction. The greater the proportional increase in soil density, the greater the productivity loss (Froehlich and McNabb 1984).</p>
	<p>Freezing and thawing promotes natural recovery. But for coarse-textured soils on warmer sites, effects largely are irreversible (Sands 1983). And on finer textured soils, natural recovery may take decades (Hatchell and others 1970). Sands (1983) concluded that desirable soil physical properties depended largely on the maintenance of soil organic matter. In time, soils will compact of their own weight without further disturbance if organic matter is lost appreciably from the profile (Sands 1983).</p>
	<h3>Loss of Topsoil</h3>
	<p>Although soils often contain vast amounts of nutrients (<a href="#table3">table 3</a>), relatively small losses of surface soil can affect productivity. This is because many nutrients, such as nitrogen, are concentrated in organic matter. Organic matter, in turn, is not distributed evenly throughout the soil, but is concentrated near the surface and decreases rapidly with depth (Powers 1989). In New Zealand's pumice region, displacing logging debris and a thin layer of topsoil into windrows during site preparation produced nutrient deficiencies and led to a 30 percent loss in volume growth by mid rotation (Dyck and Beets 1987). Similar results were reported in the United States for loblolly pine in North Carolina (Fox and others 1989), for ponderosa pine in California (Powers and others 1988), and for Douglas-fir in the Pacific Northwest (Minore 1986). Tew and others (1986), examining practices in the Piedmont region of the southeastern United States, estimated that piling logging residues into windrows removed two to three times more nitrogen and phosphorus than whole-tree harvesting.</p>
	<p>Topsoil also is lost through erosion if surface organic matter is removed appreciably or if surface soils are compacted. Removing the forest floor exposes a soil to rainfall, particle displacement, and sealing of surface pores. Depending on slope steepness, this can lead to erosion rates 40 to 1,200 percent greater than those caused simply by logging (Megahan 1987). Compaction or topsoil displacement exposes a soil surface with a lessened capacity to absorb rainfall, and leads to greater runoff and erosion. Although erosion following timber harvest and site preparation can be equivalent to rates reported for agriculture (Neary and others 1984), the period of accelerated loss tends to be brief as the site revegetates. Effects of surface erosion on long-term productivity are not well known and can only be inferred.</p>
	<h3>Summarizing the Evidence</h3>
	<p>Substantive losses of surface organic matter and soil porosity have led to documented declines in productivity. Organic matter and porosity influence productivity through their link with more fundamental processes, as indicated by the conceptual model in <a href="#fig2">figure 2</a>. But beyond conceptual models, we lack a specific understanding of what a given change in porosity or organic matter means in terms of its long-term effect on productivity. As Miller and Hazard (1987) have said, uncertainty and skepticism will persist until we establish and maintain studies that help us document and understand the long-term effects of forest practices on productivity.</p>

