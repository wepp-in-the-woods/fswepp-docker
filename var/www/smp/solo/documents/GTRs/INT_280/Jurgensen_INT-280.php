	<h3>Late Seral-Climax Stands</h3>
	<p>Legumes are more widely distributed than nonleguminous <span title="nitrogen">N</span>-fixing plants in late seral-climax stands throughout the Inland Northwest. Studies in Idaho, Montana, and Wyoming have shown the genus <em class="ital">Lupinus</em> to be the most common <span title="nitrogen">N</span>-fixing plant across all habitat types, while species of <em class="ital">Oxytropis</em> and <em class="ital">Lotus</em> were found only in scattered locations (tables 2 and 3). <em class="ital">Shepherdia</em> was the most frequently occurring nonleguminous N-fixing plant, although <em class="ital">Alnus</em> and <em class="ital">Purshia</em> were common in certain habitat types. <em class="ital">Purshia</em> was restricted to the drier sites, while <em class="ital">Alnus</em> was found in cooler, wetter stands. <em class="ital">Ceanothus</em> showed a scattered distribution in these late seral-climax stands, especially in Idaho. Ponderosa pine (<em class="ital">Pinus ponderosa</em> Laws.) and Douglas-fir (<em class="ital">Pseudotsuga menziesii</em> [Beissn.] Franco) sites had the greatest occurrence of <span title="nitrogen">N</span>-fixing plants, which likely reflects their more open stand structure.</p>

