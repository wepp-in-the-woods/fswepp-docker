	<p>Woody residue was an important source of nonsymbiotic <span title="nitrogen">N</span> fixation in the cedar-hemlock stand prior to cutting. Much of this material was removed from the site or destroyed by the prescribed burn and intensive harvesting treatments (<a href="#table">table 5</a>). This was especially evident on the tractor-piled treatment, where woody residue loadings were reduced by &gt;90 percent. The low <span title="nitrogen">N</span> fixation on both the intensive harvest and burn treatments reflected the low woody residue levels. In contrast, the amount of woody residue remaining after clearcutting was nearly the same as in the uncut stand, but the <span title="nitrogen">N</span> fixed was 30 percent less. A similar pattern was seen in the heavy slash treatment, where woody residue loadings were 60 percent higher than in the uncut stand, but <span title="nitrogen">N</span> fixation was nearly equal.</p>
	<p>The contribution of woody residues to soil <span title="nitrogen">N</span> fixation on this site depended on both the amount and type of woody material left after harvest. In the heavy slash treatment 45 percent of the woody residue was in the crumbly or solid rot stage, and 55 percent was undecayed. Solid rot residue is sound enough to withstand fragmentation during logging operations, while crumbly rot residue is easily destroyed (Benson and Schlieter 1980). Decayed wood of both rot types amounted to 59 percent of the total residue on the clearcut treatment. In contrast, 95 percent of the woody residue on the uncut site was large decaying logs, mostly in the crumbly rot stage. Many of these logs were destroyed during harvest and were not present on the cut treatments. They were replaced by smaller pieces of sound wood from harvested trees, which have much lower rates of <span title="nitrogen">N</span> fixation (Jurgensen and others 1987).</p>
	<h2>REPLACEMENT OF NITROGEN LOSSES</h2>
	<p>While nonsymbiotic <span title="nitrogen">N</span> fixation was generally lower after harvesting a northern Idaho cedar-hemlock stand (<a href="#table8">table 8</a>), the actual reduction was quite small (1-2 kg/ha/yr). The question is whether such small losses in <span title="nitrogen">N</span> fixation are important to maintaining long-term site productivity. Using soil organic matter weights before and after harvest (<a href="#table8">table 8</a>), the <span title="nitrogen">N</span> content for these materials (Page-Dumroese and others, these proceedings), and assuming <span title="nitrogen">N</span> is lost in proportion to weight losses, 440 kg of <span title="nitrogen">N</span> were estimated to have been lost from this cedar-hemlock site by clearcutting and prescribed burning. This does not include any <span title="nitrogen">N</span> losses that may have occurred in the mineral soil. Another 200-250 kg <span title="nitrogen">N</span> were likely removed from the site in bolewood (Prescott and others 1989). Using an <span title="nitrogen">N</span>-fixation gain of 1.08 kg/ha/yr (<a href="#table8">table 8</a>), and 1.5 kg N/ha/yr added in precipitation, these <span title="nitrogen">N</span> losses would be replaced in 250 to 270 years. Such a slow return to original soil <span title="nitrogen">N</span> levels could have a considerable impact on subsequent stand growth.</p>
	<p>This calculation was based on the assumption that inputs from <span title="nitrogen">N</span> fixation will not change as the next stand develops. Nitrogen fixation should increase in the forest floor as this layer increases in thickness during stand development. Nitrogen fixation in the mineral soil was reduced by burning (<a href="#table8">table 8</a>), but would likely increase as the forest floor becomes thicker. However, these <span title="nitrogen">N</span> gains would be at least partially offset by a decrease of <span title="nitrogen">N</span> fixation in woody residue. Residue weights would likely decrease with time, as wood decomposition rates are usually greater than residue inputs from young, fast-growing stands. It is only after 100 to 150 years that woody residue begins to increase as mature trees die (Harmon and others 1986; Spies and others 1988). A similar situation would probably occur for the soil wood.</p>
	<p>Other sources of nonsymbiotic <span title="nitrogen">N</span> fixation not accounted for in these calculations could also add appreciable amounts of <span title="nitrogen">N</span> to the soil, and reduce the time required for site recovery. Tree stumps, cull trees left for snags, and large dead roots can add up to 1 kg/ha/yr after harvesting (Granhall and Lindberg 1980; Harvey and others 1989). Increased development of <span title="nitrogen">N</span>-fixing algae and lichens on the soil surface after harvest could also add <span title="nitrogen">N</span>, but this would likely be small due to generally dry conditions throughout the summer. Nitrogen-fixing bacteria may also be active in the rhizospheres of developing tree seedlings and shrubs (Amaranthus and others 1990).</p>
	<p>Nitrogen-fixing plants are present in many cedar-hemlock habitat types in the Inland Northwest (tables <a href="#table2">2</a> and <a href="#table3">3</a>). If any of these plants became established on the harvested site, soil <span title="nitrogen">N</span> losses could be replaced more rapidly than by nonsymbiotic <span title="nitrogen">N</span> fixation alone. Assuming symbiotic <span title="nitrogen">N</span> fixation on this cedar-hemlock site could vary from a low of 3 kg N/ha/yr (Hendrickson and Burgess 1989) to a high of 72 kg N/ha/yr (Youngberg and Wollum 1976), the recovery time to preharvest soil <span title="nitrogen">N</span> levels would range from 10 to 125 years.</p>
	<h2>MANAGEMENT IMPLICATIONS</h2>
	<p>Nitrogen-fixing plants have the potential to add significant amounts of <span title="nitrogen">N</span> to forest sites in the Inland Northwest. Of the many <span title="nitrogen">N</span>-fixing plants present in this region, <em class="ital">Ceanothus</em> seems most amenable to management. Fire, as part of postharvest site treatments or stand underburning, would favor <em class="ital">Ceanothus</em> development in many habitat types. <em class="ital">Alnus</em>, <em class="ital">Shepherdia</em>, <em class="ital">Lupinus</em>, and <em class="ital">Astragalus</em> also have management possibilities on many sites, but much more information is needed on the response of these genera to stand disturbance in a wide range of habitat types.</p>
	<p>While the <span title="nitrogen">N</span> added by <span title="nitrogen">N</span>-fixing plants could be important in replacing <span title="nitrogen">N</span> losses from harvesting or fire, these plants are serious competition for tree seedlings on many sites (McDonald and Fiddler 1989; Petersen and others 1988; Stewart and others 1984). In the short term, reduced stand growth from such plant competiton often far outweighs the benefits from added soil <span title="nitrogen">N</span>. Youngberg and Wollum (1976) recommended using <em class="ital">Ceanothus</em> for soil <span title="nitrogen">N</span> enrichment during initial seedling development, followed by herbicide treatment to release the trees. Whether such a chemical treatment would be economically or environmentally possible in the Inland Northwest is questionable. A better alternative would be to create a mosaic of microsite conditions using fire or mechanical scarification, which allows the development of both <span title="nitrogen">N</span>-fixing plants and adequate tree regeneration (Geier-Hayes 1987), or to artificially establish <span title="nitrogen">N</span>-fixing plants after harvest (Everett and others, these proceedings).</p>
	<p>Another option would be to develop silvicultural systems to minimize competing vegetation, but leave as much organic matter on the soil surface as possible. This would favor the activity of nonsymbiotic <span title="nitrogen">N</span>-fixing bacteria. While annual <span title="nitrogen">N</span> inputs from these bacteria are small, such <span title="nitrogen">N</span> gains over the life of the stand can be appreciable. Maintaining soil <span title="nitrogen">N</span> levels is critical to continued productivity of Inland Northwest forests. These forests must be managed to minimize <span title="nitrogen">N</span> losses from timber harvesting activities, and to encourage <span title="nitrogen">N</span> inputs from biological sources.</p>
