	<p>Although species of at least one nitrogen-fixing plant occurred in all but six of the 115 habitat types examined in Montana, Idaho, and Wyoming, these plants are not major understory components in most older Inland Northwest forests. Even when several <span title="nitrogen">N</span>-fixing species are found on the same site, as often happens, the combined effect rarely averages more than 10 percent canopy coverage (Jurgensen and others 1979). However, some genera are so predominant on certain sites they have been designated the understory indicator for the habitat type: <em class="ital">Pinus flexilis</em>/ <em class="ital">Cercocarpus ledifolius</em> (PIFL/CELE), <em class="ital">Pinus ponderosa</em>/<em class="ital">Purshia tridentata</em> (PIPO/PUTR), <em class="ital">Pseudotsuga menziesii</em>/<em class="ital">Cercocarpus ledifolius</em> (PSME/CELE), and <em class="ital">Abies lasiocarpa</em>/<em class="ital">Alnus sinuata</em> (ABLA/ALSI).</p>
	<h3>Early Seral Stands</h3>
	<p>Much less information is available on the distribution and frequency of N-fixing plants in early seral stands across the region. Most studies have been very site specific and usually detail early successional development after a disturbance, such as timber harvesting or fire. <em class="ital">Ceanothus</em> has received particular attention since it often becomes abundant after prescribed burns or wildfires. <em class="ital">Ceanothus</em> seed, which can remain viable in the soil for up to 200 years, requires a heat treatment to break dormancy (Noste and Bushey 1987). Reports of <em class="ital">Ceanothus</em> canopy coverage on burned sites in Idaho and Montana have ranged from less than 5 percent to over 80 percent (Arno and others 1985; Brown and DeByle 1989; Cholewa and Johnson 1983; Lyon 1971; Mueggler 1965; Noste 1985; Stickney 1980, 1986; Zamora 1975). Generally the hotter and more complete the burn, the greater the development of <em class="ital">Ceanothus</em> from buried seed (Noste and Bushey 1987; Orme and Leege 1976).</p>
	<p>Other <span title="nitrogen">N</span>-fixing plants would also be expected to increase after site disturbance, as part of the general increase in understory vegetation. However, few reports are available on the distribution of these plants in young stands of the Inland Northwest. Separate studies in northern Idaho cedar (<em class="ital">Thuja plicata</em> Donn ex D. Don)-hemlock (<em class="ital">Tsuga heterophylla</em> [Raf.] Sarg.) habitat types have reported <em class="ital">Alnus</em> to be either more common or more restricted after burning (Mueggler 1965; Stickney 1986; Wittinger and others 1977). In Montana, Stickney (1980) noted that <em class="ital">Alnus</em> frequency was reduced on subalpine fir (<em class="ital">Abies lasiocarpa</em> [Hook.] Nutt.) sites after prescribed burning, but this species is commonly observed on road cuts or other scarified sites (Arno and others 1985). Growth and population density of <em class="ital">Purshia</em> are generally reduced after prescribed burns and wildfires in the Northern Rocky Mountains (Noste and Bushey 1987; Wagstaff 1980). <em class="ital">Shepherdia</em> showed a slight reduction in canopy coverage after a prescribed burn in a mixed aspen (<em class="ital">Populus tremuloides</em> Michx.)-conifer stand in western Wyoming (Brown and DeByle 1989). Both <em class="ital">Purshia</em> and <em class="ital">Shepherdia</em> are common mid-seral forest plants in central Idaho habitat types (Steele and Geier-Hayes 1987, 1989).</p>
	<p>Little is known about legume distribution after forest disturbance. Some <em class="ital">Lupinus</em> species are fire survivors that are able to maintain themselves in the initial stages of plant succession after fire. Lyon and Stickney (1976) observed that species of <em class="ital">Lupinus</em> were abundant after wildfire in southwestern Montana. Canopy coverage of <em class="ital">Lupinus</em> was little changed after wildfire in a northern Idaho cedar-hemlock stand (Stickney 1986), or following prescribed burns in aspen or mixed aspen-conifer stands of eastern Idaho and northwestern Wyoming (Brown and DeByle 1989). <em class="ital">Astragalus miser</em> occurred in one of these Wyoming stands prior to burning, and was not present 4 years later. However, <em class="ital">Astragalus canadensis</em> became abundant following broadcast burning on grand fir (<em class="ital">Abies grandis</em> [Dougl. ex D. Don] Lindl.) sites in northern Idaho and northeastern Oregon and maintained a sizable population for at least 20 years (Zamora 1975). <em class="ital">Trifolium</em> was found on disturbed and burned sites in northern Idaho, but showed a rapid decline as the successional plant community developed (Mueggler 1965; Stickney 1986). Two species of <em class="ital">Hedysarum</em> responded strongly to burning and mechanical site preparation in Douglas-fir and subalpine fir habitat types in western Montana, while <em class="ital">Astragalus</em> and <em class="ital">Lupinus</em> did not (Arno and others 1985). Several nitrogen-fixing legumes (<em class="ital">Astragalus</em>, <em class="ital">Lupinus</em>, and <em class="ital">Thermopsis</em>) are important successional components of several central Idaho habitat types (Steele and Geier-Hayes 1987, 1989). The scattered and incomplete nature of these studies indicates that much more information is needed on the successional roles of <span title="nitrogen">N</span>-fixing plants in this region.</p>
	<h3>Nitrogen Additions</h3>
	<p>Little information is available on the actual contribution of <span title="nitrogen">N</span>-fixing plants to the <span title="nitrogen">N</span> economy of Inland Northwest forests. The scattered distribution and low numbers of <span title="nitrogen">N</span>-fixing plants in most late seral-climax stands suggests that annual <span title="nitrogen">N</span> gains on such sites would be small. Fahey and others (1985) estimated that a <em class="ital">Lupinus argenteus</em> density of 1,000 plants/ha in older, southeastern Wyoming lodgepole pine (<em class="ital">Pinus contorta</em> Dougl. ex Loud.) stands would add only 0.1 kg N/ha/yr. <em class="ital">Lupinus</em> growing in Utah aspen stands were reported to fix 0.6 kg N/ha/yr (Skujins and others 1987).</p>
	<p>In contrast, much greater amounts of <span title="nitrogen">N</span> could be added by <span title="nitrogen">N</span>-fixing plants in successional forests. Studies on moist, highly productive sites in western Oregon and Washington have reported additions up to 100 kg of N/ha/yr from <em class="ital">Ceanothus</em> and <em class="ital">Alnus</em>, which became established on cut or burned sites (Conard and others 1985; Kimmins and others 1985). Much less information is available on the generally drier sites in the Inland Northwest, but indications are that <span title="nitrogen">N</span> gains from <span title="nitrogen">N</span>-fixing plants are lower. Youngberg and Wollum (1976) estimated <span title="nitrogen">N</span> gains from <em class="ital">Ceanothus velutinus</em> (70 percent canopy cover) on a clearcut central Oregon ponderosa pine site to be 72 kg N/ha/yr over a 10-year period. A similar 11-year-old stand of <em class="ital">Ceanothus velutinus</em> (64 percent canopy cover) in northeastern Oregon was reported to fix 32 kg N/ha/yr (McNabb and others 1979). <em class="ital">Lupinus arcticus</em> (21,600 stems/ha) growing on a cutover lodgepole pine site in southern British Columbia was estimated to fix 2 kg N/ha/yr (Hendrickson and Burgess 1989). <em class="ital">Shepherdia</em> was also present on this site and added an additional 0.75 kg N/ha/yr.</p>
	<p>The presence of <span title="nitrogen">N</span>-fixing plants on a site in high numbers does not necessarily mean that significant amounts of <span title="nitrogen">N</span> are being fixed. Dalton and Zobel (1977) estimated that less than 0.1 kg N/ha/yr was added to ponderosa and lodgepole pine stands in central Oregon by understories of <em class="ital">Purshia</em> (20 percent canopy cover). The amount of light reaching the soil surface and soil moisture levels during the growing season are critical factors in determining <span title="nitrogen">N</span>-fixing plant activity (Sprent and Sprent 1990).</p>

<h2>NONSYMBIOTIC NITROGEN FIXATION</h2>
	<p>The occurrence of <span title="nitrogen">N</span>-fixing plants in most habitat types in the Inland Northwest should not obscure the fact that these plants are lacking or of low frequency on many sites (tables <a href="#table2">2</a> and <a href="#table3">3</a>). On these sites, nonsymbiotic <span title="nitrogen">N</span>-fixation becomes an important source of <span title="nitrogen">N</span> to replace <span title="nitrogen">N</span> lost from timber harvesting.</p>
	<p>Nonsymbiotic <span title="nitrogen">N</span>-fixation rates are strongly related to soil organic matter contents, being much higher in woody residue and surface organic layers than in mineral soil (<a href="#table4">table 4</a>). Organic matter is required as an energy source for most <span title="nitrogen">N</span>-fixing microorganisms (Jurgensen and Davey 1970), and has a high water-holding capacity. However, when these <span title="nitrogen">N</span>-fixing rates were multiplied by soil weight/volume ratios to obtain the total amount of <span title="nitrogen">N</span> fixed for each soil component, <span title="nitrogen">N</span> gains in mineral soil also became important. This was especially evident on a cedar-hemlock site in Idaho, which had mineral soil <span title="nitrogen">N</span>-fixation rates 500-600 percent greater than three sites in Montana (<a href="#table4">table 4</a>). Favorable temperatures and moisture in the fertile northern Idaho ash cap soil encouraged the incorporation of surface organic matter into the mineral soil. Such higher organic matter levels would stimulate the activity of <span title="nitrogen">N</span>-fixing bacteria (Granhall and Lindberg 1980).</p>

