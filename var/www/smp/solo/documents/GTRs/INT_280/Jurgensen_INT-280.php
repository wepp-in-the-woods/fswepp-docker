	<h3>Harvesting Impacts</h3>
	<p>Nonsymbiotic <span title="nitrogen">N</span> fixation is especially susceptible to harvesting impacts, since it is dependent on adequate organic matter supplies. The greater the amounts of organic matter removed or destroyed by timber harvesting and site treatments, the greater the possible reduction in <span title="nitrogen">N</span> fixation. This effect was studied on a clearcut cedar-hemlock site in northern Idaho that had four harvest/site preparation treatments (<a href="#table8">table 8</a>). The largest decrease in <span title="nitrogen">N</span> fixation (63 percent) occurred after the prescribed burn, which removed 62 percent of the forest floor, soil wood, and woody residue. Slash removal by a bulldozer lowered <span title="nitrogen">N</span> fixation by 48 percent, while clearcutting without any site preparation reduced <span title="nitrogen">N</span> fixation by only 16 percent. In contrast, <span title="nitrogen">N</span> fixation on the heavy residue treatment was 33 percent greater than in the uncut stand. Harvesting and site preparation destroyed much of the forest floor, except on the heavy slash treatment. However, decayed wood in the soil was much less disturbed by logging operations, and became a more important source of <span title="nitrogen">N</span> fixation than the forest floor after harvesting. Soil wood is a major organic matter component in many Inland Northwest forest soils and generally retains more moisture than the forest floor during dry summer months (<a href="PageDumroese_INT-280.php">Page-Dumroese and others</a>, these proceedings).</p>

