	<h3>Nitrogen Gains</h3>
	<p>The amounts of <span title="nitrogen">N</span> fixed by nonsymbiotic <span title="nitrogen">N</span> fixation in forest soils of the Inland Northwest generally reflect site productivity (<a href="#table4">table 4</a>). Of the four old-growth stands examined, the highest <span title="nitrogen">N</span> gain was found in a very productive, northern Idaho cedar-hemlock stand, and the lowest in a dry Douglas-fir stand in western Montana. These <span title="nitrogen">N</span>-fixation differences were related to amounts of surface organic materials on each site, especially large woody residue (<a href="#table5">table 5</a>). The greater the woody residue loadings on a site, the greater the <span title="nitrogen">N</span> gains. On warm, dry sites, the accumulation of woody residue and other surface organic matter is reduced. Consequently, total <span title="nitrogen">N</span> fixation on dry sites is lower than on wetter, more productive sites, while the proportion of <span title="nitrogen">N</span> fixed in the mineral soil of these drier sites is greater.</p>

