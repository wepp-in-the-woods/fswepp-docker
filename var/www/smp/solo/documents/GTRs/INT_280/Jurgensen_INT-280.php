	<h2>ABSTRACT</h2>
	<p>Significant amounts of soil nitrogen (N) are lost from the soil during timber harvesting and related activities. Symbiotic <span title="nitrogen">N</span>-fixing plants have the potential to replace much of these <span title="nitrogen">N</span> losses on many sites in the Inland Northwest, especially during early stand development. However, many of these plants are site specific and can cause competition problems during stand establishment. Much more information is needed on the successional roles of <span title="nitrogen">N</span>-fixing plants in Inland Northwest forests. Nonsymbiotic <span title="nitrogen">N</span> fixation in forest soils of this region is low, but is an important source of <span title="nitrogen">N</span> on sites where <span title="nitrogen">N</span>-fixing plants are lacking or of low frequency. Appreciable amounts of <span title="nitrogen">N</span> can be added to the soil by nonsymbiotic <span title="nitrogen">N</span> fixation over long stand rotation ages typical for this region. Silvicultural systems need to be developed that minimize soil <span title="nitrogen">N</span> losses and maintain the biological <span title="nitrogen">N</span> fixation potential of the site.</p>
	<h2>INTRODUCTION</h2>
	<p>Timber harvesting and subsequent site preparation methods used in western forests can cause significant reductions in the levels of nitrogen (N) in forest soils (Cromack and others 1979; Page-Dumroese and others, these proceedings). The use of prescribed burns could greatly increase such soil <span title="nitrogen">N</span> losses (Little and Klock 1985; Little and Ohmann 1988; Macadam 1987; Wells and others 1979). Nitrogen is required for tree growth in greater amounts than any other mineral nutrient, and is usually the nutrient most limiting in western forest soils (Edmonds and others 1989). Nitrogen is unique among the soil nutrients because it is present almost entirely in organic forms. No inorganic soil reserve is normally present to alleviate losses of <span title="nitrogen">N</span> due to natural or human-caused factors (Wollum and Davey 1975).</p>
	<p>Replacement of soil <span title="nitrogen">N</span> lost due to forest management practices or wildfire in the Inland Northwest can come from four sources: (1) <span title="nitrogen">N</span> present in precipitation and dry deposition, (2) biological N fixation by microorganisms living in plant roots (symbiotic <span title="nitrogen">N</span> fixation), (3) <span title="nitrogen">N</span> fixation by free-living soil microorganisms (nonsymbiotic <span title="nitrogen">N</span> fixation), and (4) <span title="nitrogen">N</span> fertilizers. The contribution of each will vary depending on forest age and vegetation type, site location, and management practices. Rainfall can add in excess of 20 kg/ha/yr to sites influenced by industrialized areas, but amounts normally average between 0.5 and 2 kg <span title="nitrogen">N</span>/ha/yr in the Inland Northwest (Clayton and Kennedy 1985; Fahey and others 1988; Tiedemann and others 1978). Although some <span title="nitrogen">N</span> fertilization trials have been conducted in this region (for example, Graham and Tonn 1985; Shafii and others 1989), widespread fertilizer applications are not presently considered economically feasible.</p>
	<p>Symbiotic <span title="nitrogen">N</span> fixation has the potential to add significant amounts of <span title="nitrogen">N</span> to forest soils of the Inland Northwest. Nitrogen-fixing plants are amenable to manipulation by forest managers and could be considered when developing silvicultural prescriptions, especially those emphasizing forest biodiversity. However, as will be shown, many <span title="nitrogen">N</span>-fixing plants are quite site specific, and can cause considerable problems for conifer regeneration. Nonsymbiotic <span title="nitrogen">N</span> fixation rates in forest soils are quite low, but this <span title="nitrogen">N</span> source is likely critical on sites where <span title="nitrogen">N</span>-fixing plants are lacking.</p>
	<h2>SYMBIOTIC NITROGEN FIXATION</h2>
	<p>Nitrogen-fixing plants found in forests of the Inland Northwest are grouped into two categories: (1) plants in the family Leguminosaenine genera, and (2) nonleguminous plantsfive genera from four different families (<a href="#table1">table 1</a>). The distribution patterns of these <span title="nitrogen">N</span>-fixing plants generally reflect soil moisture/temperature conditions and stand successional stage. Early seral forests usually have greater shrub and herb development than late seral-climax forests, and would give a different picture of <span title="nitrogen">N</span>-fixing plant distribution and importance.</p>

