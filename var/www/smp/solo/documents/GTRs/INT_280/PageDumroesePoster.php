	<h2>CONCLUSIONS</h2> 
	<p>Increased nutrient and moisture availability associated with organic soil components, although in many cases not sufficient to be statistically significant in this study, likely resulted in increased uptake and provided the basis for increased seedling growth. This is a strong indication that organic residues left on a site after harvesting could determine the performance of succeeding stands by providing critical moisture and nutrients, especially on harsh (dry) sites low in <span title="nitrogen">N</span> and <span title="phosphorus">P</span>.</p>
	<p>In the Inland Northwest most harvested areas are being planted to achieve adequate stocking levels. This provides an opportunity for land managers to minimize soil horizon destruction and enhance seedling growth through organic matter conservation. Along with maintaining organic matter levels, careful consideration of microsite/seedling relationships is also very likely to improve stand conditions throughout the rotation.</p>
