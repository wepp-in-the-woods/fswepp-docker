	<h1>SOIL ORGANIC MATTER EFFECTS ON DOUGLAS-FIR GROWTH IN NORTHERN IDAHO SOILS</h1> 
	<p class="authors">
		Deborah Page-Dumroese<br />
		Russell T. Graham<br />
		Alan E. Harvey</p>
	<p>Regeneration of Douglas-fir (<em class="ital">Pseudotsuga menziesii</em> var. <em class="ital">glauca</em> [Beissn.] Franco) usually depends on site manipulation and planting to achieve fully stocked stands. Seed that produces superior survival and early growth is a key factor in supplying quality Douglas-fir wood to meet an increased demand for future timber-based products. Planting success in the Intermountain West is hampered by low soil moisture and nutrient stress (Duryea and Lavender 1982). On sites where large amounts of top-soil and organic matter have been removed during the harvest/site preparation sequence, initial tree growth and survival is reduced and continues to be depressed throughout the rotation (Glass 1976; Pehl and Bailey 1978). Douglas-fir can be considered an especially sensitive species in this regard (Graham and others 1989).</p>
	<p>Soil, wood, litter, duff, and humus govern the quality of many forest sites in the Inland Northwest because they are an important source of moisture-holding capacity and nutrient storage, which are essential for seedling survival (Harvey and others 1976). Organic matter also provides a favorable microsite for many microbes in the rhizosphere. These microbes can be beneficial or deleterious to tree survival and growth (Harvey 1982).</p>
	<h2>NUTRIENT AVAILABILITY</h2> 
	<p>Timber harvesting, site preparation, and slash disposal systems can reduce the nutrient capital of forested ecosystems (Clayton and Kennedy 1985). Nutrient loss is greatest when intensive site preparation or utilization is involved (Jurgensen and others 1990; Leaf 1979). Nutrient deficiencies induced by organic matter removal have been blamed for poor yields and plantation failures on numerous sites (Burger and Kluender 1982; Pritchett 1981; Woods 1981). In the steep, mountainous regions of the Inland Northwest, this loss is often accelerated by erosion and leaching after slash piling, burning, and organic matter removal (Clayton and Kennedy 1985). Site preparation techniques that mound the topsoil and organic horizons together have helped achieve greater conifer survival and growth rates in several instances (Page-Dumroese and others 1987; Shoulders and Terry 1978).</p>
	<h2>MOISTURE AVAILABILITY</h2> 
	<p>Residual harvesting debris has many physical and chemical properties that make it important to biological processes (Harvey and others 1987). Dead plant materials tie up, then gradually release upon decay, substantial quantities of nutrients and tend to retain more available moisture. Available soil moisture is influenced by soil temperature, moisture content, particle size distribution, evaporative losses, and organic matter content (Cleary 1970). Of these five, organic matter is the most influential, since it represents a major component of soil moisture storage (Brady 1974).</p>
	<h2>MICROBIAL POPULATIONS</h2> 
	<p>When the forest floor is exposed through harvesting, there is a sharp increase in solar radiation and a concomitant reduction of transpiration. The previously stable microclimate below the organic layer becomes subject to large temperature, moisture, and nutrient fluctuations.</p>
	<p>Changes in microsite lead to changes in microbial populations as well. Soil microbes, including mycorrhizae, play a critical role in soil development and plant nutrition and are perhaps an important contributor to seedling success. Harvey and others (1976, 1978, 1987; Harvey 1982) have demonstrated that humus and brown cubical decayed wood are the major substrate for ectomycorrhizal growth on Douglas-fir. These fungi tend to confer drought resistance on seedling roots (Parke and others 1983). Other benefits to the host plant include resistance to pathogens, tolerance to environmental stresses, and an increase in moisture and nutrient uptake (Marx and Krupa 1978). Ectomycorrhizal colonization is highly dependent on inoculum source and environmental conditions surrounding the root system (for example, moisture content, temperature, pH, and organic matter [Slankis, 1974]). Harvest and site preparation alter these factors and may reduce the number of ectomycorrhizae and their diversity (Amaranthus and Perry 1987). It follows, therefore, that soils high in organic matter affect plant rhizospheres through microbial activities and subsequent changes in plant moisture and nutrition.</p>
	<p>All these interrelated processes (microbial activity, nutrient and water release) are crucial for the maintenance of conifer plantations in the Inland Northwest and are of particular concern for Douglas-fir. Soil manipulation, in the form of site preparation and harvesting activities, can be a controlling factor for stand establishment or failure.</p>
	<p>Because field trials of site preparation techniques have difficulty in separating the influence of organic matter on seedling performance and independent conflicting variables (Graham and others 1989; Page-Dumroese and others 1986, 1989), this study was established.</p>
	<h2>SITES, MATERIALS, AND METHODS</h2> 
	<p>Soil organic matter, mineral soil, and seed were collected from two dissimilar habitat types in northern Idaho. A low-elevation site (750 m) in the <em class="ital">Thuja plicata</em>/<em class="ital">Clintonia uniflora</em> (Cooper and others 1987) habitat type and a high-elevation site (3,500 m) in the <em class="ital">Abies lasiocarpa</em>/<em class="ital">Xerophyllum tenax</em> habitat type (Cooper and others 1987) were selected. Soil was sieved to pass a 2-mm mesh sieve. Planting media consisted of 100 percent organic soil, 100 percent mineral soil, and 50 percent mineral/50 percent organic soils. Soil was tamped into 163-cm 3 cells and seed was sown. Bulk densities of the cells were kept constant (<a href="#table1">table 1</a>). Seed was sown in April and seedlings were grown until October.</p>

