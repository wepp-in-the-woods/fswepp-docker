	<h2>RESULTS</h2> 
	<p>Organic matter plays an important role in the growth and performance of Douglas-fir seedlings. Soils containing high organic matter levels had a greater water-holding capacity (Page-Dumroese and others 1986) and, therefore, a greater available water content than soils consisting of only mineral soil. The available water in the organic matter may help bring the seedlings through critical drought periods during the growing season.</p>
	<p>Seedlings grown in organic mixes grew significantly better than those grown in only mineral soil (<a href="#table2">table 2</a>). Height, top weight, root weight, root collar caliper, and terminal bud length were all significantly greater for seedlings grown in the organic mixes. Terminal bud length was used as an indicator of both current season conditions and seedling potential (Kozlowski and others 1973). Seedlings grown in the organic mix soils had a significant growth advantage, not only for the current year, but also for the following season, over their mineral soil counterparts. The improved seedling morphological properties likely can be attributed to the additional moisture and nutrients available in soil organic matter.</p>

