<p><u><font face="Times Roman"><font face="Times Roman"><a name="objectives"></a></font></font></u><b><tt><font face="Times Roman">2554.02</font></tt><font
 face="Times Roman">  <tt>OBJECTIVES:</tt></font></b></p>

<blockquote>

<p><font face="Times Roman"><font face="Times Roman">To meet direction in
the National Forest Management Act of 1976 and other legal mandates. To manage
National Forest System lands under ecosystem management principles without
permanent impairment of land productivity and to maintain or improve soil
quality.</font></font></p>

</blockquote>
<p><b><tt><font face="Times Roman"><a name="POLICY"></a>2554.03</font></tt><font
 face="Times Roman">  <tt>POLICY:</tt></font></b></p>
<blockquote>
  <p><font face="Times Roman">Design and implement management practices
  that maintain or improve soil quality. Protection of the soil resource should
  be emphasized; restoration practices should be implemented where necessary.
  Soil quality is maintained when erosion, compaction, displacement, rutting,
  burning, and loss of organic matter are maintained within defined soil quality
  standards.</font></p>
  <p><font face="Times Roman">Design new activities that do not create
  detrimental soil conditions on more than 15 percent of an activity area. In
  areas where less than 15 percent detrimental soil conditions exist from prior
  activities, the cumulative detrimental effect of the current activity
  following project implementation and restoration must not exceed 15 percent.
  In areas where more than 15 percent detrimental soil conditions exist from
  prior activities, the cumulative detrimental effects from project
  implementation and restoration should not exceed the conditions prior to the
  planned activity and should move toward a net improvement in soil quality.</font></p>
</blockquote>

<p><a name="anchor534531"></a><b><tt><font face="Times Roman">2554.04</font></tt><font
 face="Times Roman"> <tt>RESPONSIBILITY:</tt></font></b></p>

<blockquote>
  <p><font face="Times Roman">1. </font><u><font face="Times Roman">Regional
  Foresters.</font></u></p>
  <blockquote>
    <p><font face="Times Roman">a. Develop Regional Soil Quality Standards.</font></p>
    <p><font face="Times Roman">b. Coordinate with Research in the
    selection of suitable methods for monitoring soil disturbances.</font></p>
    <p><font face="Times Roman">c. Review Forest soil quality monitoring
    plans for technical adequacy and to ensure coordination within the Region.</font></p>
    <p><font face="Times Roman">d. Review soil quality monitoring results
    for application to other areas and for coordination with Research efforts.</font></p>
  </blockquote>
  <p><font face="Times Roman">2. <u>Forest Supervisors</u>.</font></p>
  <blockquote>
    <p><font face="Times Roman">a. Ensure that Forest-wide and
    project-level plans include soil quality standards.</font></p>
    <p><font face="Times Roman">b. Assess the extent to which soil quality
    standards are being met and whether they are effective in maintaining or
    improving soil quality.</font></p>
    <p><font face="Times Roman">c. Provide training in the application of
    soil quality standards.</font></p>
    <p><font face="Times Roman">d. Evaluate the effectiveness of soil
    quality standards and recommend adjustments to the Regional Forester.</font></p>
    <p><font face="Times Roman">e. Report monitoring results to the
    Regional Forester.</font></p>
  </blockquote>
  <p><font face="Times Roman">3. <u>District Rangers</u>.</font></p>
  <blockquote>
    <p><font face="Times Roman">a. Ensure that project planning documents
    identify measures necessary to meet soil quality standards.</font></p>
    <p><font face="Times Roman">b. Conduct post activity implementation
    monitoring to determine if soil quality standards have been met. Consult
    with soil scientists to evaluate the need to adjust management practices or
    apply rehabilitation measures.</font></p>
  </blockquote>
</blockquote>
<p><b><tt><font face="Times Roman"><a name="monitoring"></a>2554.10</font></tt><font
 face="Times Roman">  <tt>MONITORING:</tt></font></b></p>
<blockquote>
  <p><font face="Times Roman">Management activities create various amounts
  of soil disturbance, but ecologically sustainable land stewardship can
  minimize adverse impacts on soils. Soil quality standards provide benchmark
  values that indicate when changes in soil properties and soil conditions would
  result in significant change or impairment of soil quality based on available
  research and Regional experience (Page-Dumroese et al. In Review). Proper
  application of these standards requires professional knowledge and judgement.</font></p>
  <p><font face="Times Roman">Soil quality standards apply to lands where
  vegetation and water resource management are the principal objectives, that
  is, timber sales, grazing pastures or allotments, wildlife habitat, and
  riparian areas. The standards do not apply to intensively developed sites such
  as mines, developed recreation sites, administrative sites, or rock quarries.
  They are not intended to prohibit other resource management practices such as,
  installing waterbars or preparing sites for planting, as long as such
  practices are consistent with long-term sustainability of the soil resource.
  Permanent roads do affect soil-hydrologic function, however, their evaluation
  is more appropriately done on a watershed basis using models and other
  watershed analysis techniques.</font></p>
  <p><font face="Times Roman"><b>1. Detrimental Soil Disturbance.</b>These
  disturbances includes the effects of compaction, displacement, rutting, severe
  burning, surface erosion, loss of surface organic matter, and soil mass
  movement. At least 85 percent of an activity area must have soil that is in
  satisfactory condition. Detrimental conditions include:</font></p>
  <blockquote>
    <p><font face="Times Roman"><u>Compaction</u>. Detrimental compaction
    is a 15 percent increase in natural bulk density.<b> </b>The cumulative
    effects of multiple site entries on compaction should also be considered
    since compacted soils often recover slowly.</font></p>
    <p><font face="Times Roman"><u>Rutting</u>. Wheel ruts at least 2
    inches deep in wet soils are detrimental.</font></p>
    <p><font face="Times Roman"><u>Displacement</u>. Detrimental
    displacement is the removal of 1 or more inches (depth) of any surface soil
    horizon, usually the A horizon, from a continuous area greater than 100
    square feet.</font></p>
    <p><font face="Times Roman"><u>Severely-burned Soil</u>. Physical and
    biological changes to soil resulting from high-intensity burns of long
    duration are detrimental. This standard is used when evaluating prescribed
    fire. Guidelines for assessing burn intensity are contained in the
    Burned-Area Emergency Rehabilitation Handbook (FSH 2509.13).</font></p>
    <p><font face="Times Roman"><u>Surface Erosion</u>.<b> </b>Rills,
    gullies, pedestals, and soil deposition are all indicators of detrimental
    surface erosion.<b> </b>Minimum amounts of ground cover necessary to keep
    soil loss to within tolerable limits (generally less than 1 to 2 tons per
    acres per year) should be established locally depending on site
    characteristics.</font></p>
    <p><font face="Times Roman"><u>Soil Mass Movement</u>. Any soil mass
    movement caused by management activities is detrimental.</font></p>
  </blockquote>
  <p><b><tt><font face="Times Roman">2</font></tt></b><b><tt><font face="Times Roman">.</font></tt><font
 face="Times Roman">  <tt>Organic Matter Guidelines.</tt></font></b><font face="Times Roman">
  The loss of surface organic matter can cause nutrient and carbon cycle
  deficits and negatively affect physical and biological soil conditions.
  Objectives for fine organic matter layer thickness and distribution should be
  determined locally based on similar soils or ecological types. The direct
  benefits of coarse woody material to soils can vary widely, depending on
  ecological type. Research guidelines such as those contained in Graham et al.
  1994, should be used if more specific local guidelines are not available.
  Since the management of coarse woody material is important to wildlife, fire,
  and other resources, integration based on local objectives needs to occur.</font></p>
  <p><b><tt><font face="Times Roman">3</font></tt></b><b><tt><font face="Times Roman">.</font></tt><font
 face="Times Roman">  <tt>Monitoring Methods.</tt></font></b><font face="Times Roman">
  Visual methods are generally used to make initial evaluations of the effects
  of management activities on soils. The major objective of soil quality
  monitoring is to ensure that ecologically sustainable soil management
  practices are being applied. In most cases, qualitative estimates will be
  considered sufficient. The use of photo points provides good documentation and
  is recommended. Measurements and detailed sampling are used to calibrate
  visual methods and to conduct investigations where visual methods are
  inadequate or where benchmark or statistically valid sampling is required.</font></p>
  <blockquote>
    <p><font face="Times Roman">a. <u>Areal Extent Sampling.</u> Estimates
    of the percent of an activity area affected by detrimental soil disturbance
    can be made visually or by transecting. If statistically valid techniques
    are needed for benchmark sites, determine sample size and transect design
    using procedures described in Howes, Hazard, and Geist 1983.</font></p>
    <p><font face="Times Roman">b. <u>Soil Sampling Techniques</u>. Soil
    displacement, rutting, severely burned soil, erosion, mass movement, and
    above-ground organic matter can be observed and measured.</font></p>
  </blockquote>
  <p><font face="Times Roman">Soil compaction can be assessed by observing
  management-induced platy structure or by evaluating changes in bulk density,
  macroporosity, or penetration resistance using appropriate methods. Tile spade
  estimations of soil compaction are very effective and can be calibrated with
  soil strength (Clayton 1987). Root-restricting bulk densities for various soil
  particle-size classes are displayed in the National Soil Survey Handbook,
  618.06. These bulk density values can also be used as indicators of
  detrimental soil compaction.</font></p>
  <p><font face="Times Roman">Randomly located samples should be taken
  prior to soil disturbance to estimate the natural bulk density, penetration
  resistance, infiltration rate, or soil structure. If the site has been
  previously disturbed, an adjacent area with similar soils can be sampled.</font></p>
</blockquote>
<p><u><font face="Times Roman"><a name="definitions"></a></font></u><b><font
 face="Times Roman"><tt>DEFINITIONS:</tt></font></b></p>
<blockquote>
  <p><font face="Times Roman"><u>Activity Area</u>. A land area affected by
  a management activity to which soil quality standards are applied. Activity
  areas must be feasible to monitor and include harvest units within timber sale
  areas, prescribed burn areas, grazing areas or pastures within range
  allotments, riparian areas, recreation areas, and alpine areas. All temporary
  roads, skid trails, and landings are considered to be part of an activity
  area.</font></p>
  <p><font face="Times Roman"><u>Bulk Density</u>. The mass of dry soil per
  unit volume, corrected for weight and volume of coarse fragments greater than
  2mm in diameter.</font></p>
  <p><font face="Times Roman"><u>Compaction</u>. A physical change in soil
  properties from compression, vibration, or shearing that increases soil bulk
  density and decreases porosity, air exchange, root penetration, infiltration,
  and permeability.</font></p>
  <p><font face="Times Roman"><u>Coarse Woody Material</u>. Organic
  materials on the soil surface such as plant stems, branches, and logs with a
  diameter greater than 3 inches.</font></p>
  <p><font face="Times Roman"><u>Detrimental Soil Condition</u>. The
  condition where established soil quality standards are not met and the result
  is a significant change in soil quality.</font></p>
  <p><font face="Times Roman"><u>Displacement</u>. The removal and
  horizontal movement of soil from one place to another, usually by mechanical
  forces such as dozer blades, repeated vehicular traffic, or the yarding of
  logs.</font></p>
  <p><font face="Times Roman"><u>Fine Organic Matter</u>. Organic materials
  such as plant litter, duff, and woody material less than 3 inches in diameter
  in contact with the soil surface.</font></p>
  <p><font face="Times Roman"><u>Ground Cover</u>. Ground cover consists of
  vegetation, fine organic matter, coarse woody material, and rock fragments
  larger than three-fourths inch in diameter in contact with the soil surface.</font></p>
  <p><font face="Times Roman"><u>Hydrologic Function</u>. Soil hydrologic
  function is the ability of the soil to absorb, store, and transmit water, both
  vertically and horizontally. Changes in soil bulk density, soil structure, and
  ground cover can alter the hydrologic function of the soil.</font></p>
  <p><font face="Times Roman"><u>Restoration</u>. Treatments that restore
  vital soil functions to their inherent range of variability. It is recognized
  that treatments may need to occur over a period of years and may need to be
  maintained. Restoration treatments could include, but are not limited to,
  tillage, ripping, seeding, mulching, recontouring if temporary roads, and
  water barring.</font></p>
  <p><font face="Times Roman"><u>Rutting</u>. Deformation of the soil under
  saturated conditions resulting in detrimental changes to soil structure and
  reduced porosity.</font></p>
  <p><font face="Times Roman"><u>Soil Function</u>. Primary soil functions
  are: (1) the sustenance of biological activity, diversity, and productivity,
  (2) soil hydrologic function, (3) filtering, buffering, immobilizing, and
  detoxifying organic and inorganic materials, and (4) storing and cycling
  nutrients and other materials.</font></p>
  <p><font face="Times Roman"><u>Soil Mass Movement</u>. The detachment and
  downslope movement of soil or the surface mantle in the form of debris
  slides/avalanches or deep-seated rotational failures or slumps.</font></p>
  <p><font face="Times Roman"><u>Soil Quality</u>. The capacity of a
  specific soil to function within its surroundings, support plant and animal
  productivity, maintain or enhance water and air quality, and support human
  health and habitation.</font></p>
  <p><font face="Times Roman"><u>Surface Erosion</u>. The detachment and
  transport of individual soil particles by wind, water, or gravity. Surface
  erosion is the loss of soil in a fairly uniform layer across the land surface
  (sheet erosion), in many small rills, or as larger gullies.</font></p>
</blockquote>
