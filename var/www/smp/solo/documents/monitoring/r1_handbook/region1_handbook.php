			<h4><a name="objectives"></a>2554.02 - OBJECTIVES:</h4>
			<p>To meet direction in the National Forest Management Act of 1976 and other legal mandates. To manage National Forest System lands under ecosystem management principles without permanent impairment of land productivity and to maintain or improve soil quality.</p>
			<h4><a name="policy"></a>2554.03 - POLICY:</h4>
			<p>Design and implement management practices that maintain or improve soil quality. Protection of the soil resource should be emphasized; restoration practices should be implemented where necessary. Soil quality is maintained when erosion, compaction, displacement, rutting burning, and loss of organic matter are maintained within defined soil quality standards.</p>
			<p>Design new activities that do not create detrimental soil conditions on more than 15 percent of an activity area. In areas where less than 15 percent detrimental soil conditions exist from prior activities, the cumulative detrimental effect of the current activity following project implementation and restoration must not exceed 15 percent. In areas where more than 15 percent detrimental soil conditions exist from prior activities, the cumulative detrimental effects from project implementation and restoration should not exceed the conditions prior to the planned activity and should move toward a net improvement in soil quality.</p>
