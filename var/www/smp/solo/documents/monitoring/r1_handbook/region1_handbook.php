			<h4><a name="monitoring"></a>2554.10 - MONITORING:</h4>
			<p>Management activities create various amounts
  of soil disturbance, but ecologically sustainable land stewardship can
  minimize adverse impacts on soils. Soil quality standards provide benchmark
  values that indicate when changes in soil properties and soil conditions would
  result in significant change or impairment of soil quality based on available
  research and Regional experience (Page-Dumroese et al. In Review). Proper
  application of these standards requires professional knowledge and judgement.</p>
			<p>Soil quality standards apply to lands where
  vegetation and water resource management are the principal objectives, that
  is, timber sales, grazing pastures or allotments, wildlife habitat, and
  riparian areas. The standards do not apply to intensively developed sites such
  as mines, developed recreation sites, administrative sites, or rock quarries.
  They are not intended to prohibit other resource management practices such as,
  installing waterbars or preparing sites for planting, as long as such
  practices are consistent with long-term sustainability of the soil resource.
  Permanent roads do affect soil-hydrologic function, however, their evaluation
  is more appropriately done on a watershed basis using models and other
  watershed analysis techniques.</p>
			<ol style="list-style-type: decimal;">
  				<li><strong>Detrimental Soil Disturbance.</strong> These
  disturbances includes the effects of compaction, displacement, rutting, severe
  burning, surface erosion, loss of surface organic matter, and soil mass
  movement. At least 85 percent of an activity area must have soil that is in
  satisfactory condition. Detrimental conditions include:
  					<ul>
    					<li><strong>Compaction.</strong> Detrimental compaction
    is a 15 percent increase in natural bulk density. The cumulative
    effects of multiple site entries on compaction should also be considered
    since compacted soils often recover slowly.</li>
    					<li><strong>Rutting.</strong> Wheel ruts at least 2
    inches deep in wet soils are detrimental.</li>
    					<li><strong>Displacement.</strong> Detrimental
    displacement is the removal of 1 or more inches (depth) of any surface soil
    horizon, usually the A horizon, from a continuous area greater than 100
    square feet.</li>
    					<li><strong>Severely-burned Soil.</strong> Physical and
    biological changes to soil resulting from high-intensity burns of long
    duration are detrimental. This standard is used when evaluating prescribed
    fire. Guidelines for assessing burn intensity are contained in the
    Burned-Area Emergency Rehabilitation Handbook (FSH 2509.13).</li>
    					<li><strong>Surface Erosion.</strong> Rills,
    gullies, pedestals, and soil deposition are all indicators of detrimental
    surface erosion. Minimum amounts of ground cover necessary to keep
    soil loss to within tolerable limits (generally less than 1 to 2 tons per
    acres per year) should be established locally depending on site
    characteristics.</li>
    					<li><strong>Soil Mass Movement.</strong> Any soil mass
    movement caused by management activities is detrimental.</li>
  					</ul>
				</li>
				<li><strong>Organic Matter Guidelines.</strong> 
  The loss of surface organic matter can cause nutrient and carbon cycle
  deficits and negatively affect physical and biological soil conditions.
  Objectives for fine organic matter layer thickness and distribution should be
  determined locally based on similar soils or ecological types. The direct
  benefits of coarse woody material to soils can vary widely, depending on
  ecological type. Research guidelines such as those contained in Graham et al.
  1994, should be used if more specific local guidelines are not available.
  Since the management of coarse woody material is important to wildlife, fire,
  and other resources, integration based on local objectives needs to occur.</li>
  					</ol>
				</li>
				<li><strong>Monitoring Methods.</strong>
  Visual methods are generally used to make initial evaluations of the effects
  of management activities on soils. The major objective of soil quality
  monitoring is to ensure that ecologically sustainable soil management
  practices are being applied. In most cases, qualitative estimates will be
  considered sufficient. The use of photo points provides good documentation and
  is recommended. Measurements and detailed sampling are used to calibrate
  visual methods and to conduct investigations where visual methods are
  inadequate or where benchmark or statistically valid sampling is required.
  					<ol style="list-style-type: lower-alpha;">
    					<li><strong>Areal Extent Sampling.</strong> Estimates
    of the percent of an activity area affected by detrimental soil disturbance
    can be made visually or by transecting. If statistically valid techniques
    are needed for benchmark sites, determine sample size and transect design
    using procedures described in Howes, Hazard, and Geist 1983.</li>
    					<li><strong>Soil Sampling Techniques</strong>. Soil
    displacement, rutting, severely burned soil, erosion, mass movement, and
    above-ground organic matter can be observed and measured.</li>
					</ol>
				</li>
			</ol>
			<p>Soil compaction can be assessed by observing
  management-induced platy structure or by evaluating changes in bulk density,
  macroporosity, or penetration resistance using appropriate methods. Tile spade
  estimations of soil compaction are very effective and can be calibrated with
  soil strength (Clayton 1987). Root-restricting bulk densities for various soil
  particle-size classes are displayed in the National Soil Survey Handbook,
  618.06. These bulk density values can also be used as indicators of
  detrimental soil compaction.</p>
  			<p>Randomly located samples should be taken
  prior to soil disturbance to estimate the natural bulk density, penetration
  resistance, infiltration rate, or soil structure. If the site has been
  previously disturbed, an adjacent area with similar soils can be sampled.</p>

			<h4><a name="objectives"></a>DEFINITIONS:</h4>
  			<dl class="glossary">
				<dt>Activity Area.</dt>
				<dd>A land area affected by
  a management activity to which soil quality standards are applied. Activity
  areas must be feasible to monitor and include harvest units within timber sale
  areas, prescribed burn areas, grazing areas or pastures within range
  allotments, riparian areas, recreation areas, and alpine areas. All temporary
  roads, skid trails, and landings are considered to be part of an activity
  area.</dd>
  				<dt>Bulk Density.</dt>
				<dd>The mass of dry soil per
  unit volume, corrected for weight and volume of coarse fragments greater than
  2mm in diameter.</dd>
  				<dt>Compaction.</dt>
				<dd>A physical change in soil
  properties from compression, vibration, or shearing that increases soil bulk
  density and decreases porosity, air exchange, root penetration, infiltration,
  and permeability.</dd>
  				<dt>Coarse Woody Material.</dt>
				<dd>Organic
  materials on the soil surface such as plant stems, branches, and logs with a
  diameter greater than 3 inches.</dd>
  				<dt>Detrimental Soil Condition.</dt>
				<dd>The
  condition where established soil quality standards are not met and the result
  is a significant change in soil quality.</dd>
  				<dt>Displacement.</dt>
				<dd>The removal and
  horizontal movement of soil from one place to another, usually by mechanical
  forces such as dozer blades, repeated vehicular traffic, or the yarding of
  logs.</dd>
  				<dt>Fine Organic Matter.</dt>
				<dd>Organic materials
  such as plant litter, duff, and woody material less than 3 inches in diameter
  in contact with the soil surface.</dd>
  				<dt>Ground Cover.</dt>
				<dd>Ground cover consists of
  vegetation, fine organic matter, coarse woody material, and rock fragments
  larger than three-fourths inch in diameter in contact with the soil surface.</dd>
  				<dt>Hydrologic Function.</dt>
				<dd>Soil hydrologic
  function is the ability of the soil to absorb, store, and transmit water, both
  vertically and horizontally. Changes in soil bulk density, soil structure, and
  ground cover can alter the hydrologic function of the soil.</dd>
  				<dt>Restoration.</dt>
				<dd>Treatments that restore
  vital soil functions to their inherent range of variability. It is recognized
  that treatments may need to occur over a period of years and may need to be
  maintained. Restoration treatments could include, but are not limited to,
  tillage, ripping, seeding, mulching, recontouring if temporary roads, and
  water barring.</dd>
  				<dt>Rutting.</dt>
				<dd>Deformation of the soil under
  saturated conditions resulting in detrimental changes to soil structure and
  reduced porosity.</dd>
  				<dt>Soil Function.</dt>
				<dd>Primary soil functions
  are: (1) the sustenance of biological activity, diversity, and productivity,
  (2) soil hydrologic function, (3) filtering, buffering, immobilizing, and
  detoxifying organic and inorganic materials, and (4) storing and cycling
  nutrients and other materials.</dd>
  				<dt>Soil Mass Movement.</dt>
				<dd>The detachment and
  downslope movement of soil or the surface mantle in the form of debris
  slides/avalanches or deep-seated rotational failures or slumps.</dd>
  				<dt>Soil Quality.</dt> 
				<dd>The capacity of a
  specific soil to function within its surroundings, support plant and animal
  productivity, maintain or enhance water and air quality, and support human
  health and habitation.</dd>
  				<dt>Surface Erosion.</dt>
				<dd>The detachment and
  transport of individual soil particles by wind, water, or gravity. Surface
  erosion is the loss of soil in a fairly uniform layer across the land surface
  (sheet erosion), in many small rills, or as larger gullies.</dd>
  			</dl>
