<p>Subject: COMPACTION MONITORING</p>



<p>Message:</p>



<p>Jeanne &amp; Dallas - I just completed the samples we collected on Friday and I was

very pleased with the results, which are as follows:</p>



<p><strong>Hume Sale</strong></p>



<blockquote>

  <p>Main Skid Road with no slash; bulk density = .97 g/cc. </p>

  <p>Slash Skid Trail 1 with the heavy slash; bulk density = .54 g/cc. </p>

  <p>Slash Skid Trail 2 with thinner slash cushion ; bulk density = .72 g/cc.</p>

</blockquote>



<p><strong>Bovine Pine Sale</strong> </p>



<blockquote>

  <p>Recent Skid Trail without slash; bulk density = .98 g/cc.</p>

  <p>Slash Skid Trail 1 with the thinner slash cushion; bulk density = .76 g/cc.</p>

  <p>Slash Skid Trail 2 with the heavier slash cushion; bulk density = .61 g/cc.</p>

</blockquote>



<p>The average natural bulk density for the surface ash layer is .65 g/cc. Detrimental

Compaction in the surface ash begins at about .78 g/cc.</p>



<p>When you had the operator work on slash, all samples we took were under the detrimental

limit. A good relationship developed with thickness of slash and lower bulk density.

Preliminary results look very promising if we can get operators to work on some slash.

Also your sale areas had very little to no soil displacement. You are to be complimented

on a job very well done on both the Hume and Bovine Pine Sales. Thank You for the great

work. </p>



<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;

Jerry Niehoff</p>



