<p><strong>Big Bird Timber Sale</strong></p>



<p><strong>Findings:</strong> A number of road fills have washed out on this pre-Forest

Plan timber sale which called for burying wind rowed slash in fills. The district had

spent over $12,000 for rehabilitating the areas involved. Funding the rehabilitation

project has been difficult as road maintenance accounts cannot be charged for work

performed outside the road prism. This is similar to the situation encountered on Browns

Creek on the Wallace District described above. </p>



<p><strong>Action: Forest Engineer will remove burning slash in windrows as an option on

all future road construction contracts.</strong></p>



<p><strong>Beetle Juice Timber Sale - Eagle Creek</strong></p>



<p><strong>Findings:</strong> The Beetle Juice timber sale in the Eagle Creek drainage is

a helicopter salvage sale of approximately 1.2 million board feet, all to be logged from

existing roads. Fisheries biologists' analyses have suggested that the cumulative effects

of this sale coupled with past and future activities will not provide adequate protection

for the cutthroat trout fishery which is a beneficial use as described in State of Idaho

water quality regulations. </p>



<p><strong>Action: Forest Fisheries/Wildlife/Watershed Staff needs to clarify policy on

protection of beneficial uses according to State of Idaho water quality regulations.</strong></p>



<p><strong>Bottle Creek Timber Sale</strong></p>



<p><strong>Findings:</strong> The Forest interdisciplinary team reviewed the sale plans

and found that the helicopter proposal was planned in accordance with the guidelines in

the management plan for the recreational river corridor that is part of the St. Joe Wild

and Scenic River. </p>



<p><strong>Action: This sale proposal should be used by the Forest as an example on the

type of timber sale activity that can be utilized in classified and eligible recreational

river corridors.</strong></p>



<p><strong>Soil Monitoring Report for 1991:</strong></p>



<p><strong>Soil Standards:</strong></p>



<p>Soil disturbing management practices will strive to maintain at least 80 percent of the

activity area in a condition of acceptable productivity potential for trees and other

managed vegetation. Unacceptable productivity potential exists when soil has been

detrimentally compacted, displaced, or severely burned as determined in the project

analysis.</p>



<p>Projects should strive to maintain sufficient large woody debris to maintain site

productivity. Large woody debris is essential for maintenance of sufficient microorganism

populations.</p>



<p><strong>Recommended practices to maintain long term soil productivity:</strong></p>



<blockquote>

  <p>1. <strong>Compaction and Displacement:</strong> Winter logging, minimizing excavated

  skid trails and total ground area on which equipment operates, grapple piling and

  broadcast burning are all practices which minimize these impacts and protect long term

  productivity to help meet the 80 percent standard in the forest plan. </p>

  <p>2. <strong>Residue Management:</strong> A new research publication from the

  Intermountain Station has refined earlier recommendations for the amount of woody debris

  that should be left on-site. While all the data from the study has not been analyzed, the

  new recommendations, which are made by habitat type, are listed below:</p>

  <center><table border="0" cellspacing="1" width="75%">

    <tr>

      <td valign="top" align="left" nowrap>Douglas fir/ninebark<br>

      grand fir/beargrass<br>

      western hemlock/queencup beadlily<br>

      subalpine fir/beargrass</td>

      <td valign="top" align="left" nowrap width="209"><blockquote>

        <p> 5-10 tons/acre<br>

        10-15 tons/acre<br>

        20-25 tons/acre<br>

        15-20 tons/acre</p>

      </blockquote>

      </td>

    </tr>

  </table>

  </center><p>3. <strong>Severe Burns:</strong> While light and moderate burns are

  generally beneficial to site productivity, hot burns can be very detrimental. Burning when

  soil moisture is above 25 percent reduces the potential for damage from hot burns.</p>

  <p>4. <strong>Roads, Landings &amp; Skid trails:</strong> Minimizing the area used for

  these purposes can reduce the amount of land taken out of production or with reduced

  productivity potential.</p>

</blockquote>



<p><strong>Findings:</strong> All Districts appear to be doing a good job of implementing

the alternative practices discussed above which minimize detrimental impacts on the soil

resource. They are using a much smaller amount of dozer piling which has the greatest

impact.</p>



<p>Districts are also leaving more slash on the ground. This practice: 1) greatly

conserves soil moisture; 2) provides important microsites for tree establishment; and 3)

provides more nutrients for long term productivity.</p>



<p><strong>Action: Managers will implement the recommended practices identified above (as

appropriate) to insure that long term productivity is maintained or improved.</strong></p>



<p><strong>Monitoring:</strong> In response to district requests the following summary of

the soil monitoring which has been done by IPNF soil scientists has been prepared. It

includes monitoring done this year and in previous years. The results are shown by

management practices. While results can vary with management objectives, site

characteristics, skill of the machine operator, etc., the results show the range of soil

disturbances which have been monitored. It can be readily seen that some operations

produce a great deal of soil disturbance while others cause much less.</p>



<p><strong>Definitions:</strong></p>



<blockquote>

  <p><strong>Detrimental Compaction (D.C.)</strong> - More than 20% increase in bulk density

  for volcanic ash soils*</p>

  <p><strong>Detrimental Displacement (D.D.)</strong> - Removal of the forest floor and one

  inch or more of the surface mineral soil layer</p>

  <p><strong>Severely Burned (S.B.)</strong> - The soil surface is in a condition where most

  woody debris and the entire forest floor is consumed down to bare mineral soil. The soil

  surface may have turned red due to extreme heat. Also, fine roots and organic matter are

  consumed or charred in the upper inch of mineral soil.</p>

  <p><strong>Rutted (RUT.)</strong> - This occurs when equipment operates on soils producing

  visible ruts or compressed trails in the surface litter, humus or soil.</p>

</blockquote>



