<p><strong>WESTERN HEMLOCK STUDY SITES - FERNAN AND WALLACE RANGER DISTRICTS</strong></p>



<p>On the western hemlock/queencup beadlily habitat type, Art Zack sampled vegetation

stands that have been subject to timber harvest within approximately the last 30 years.

The strategy was to sample a range of ages since disturbance (differentiated by structural

classes), a range of treatment differences, and a range of site characteristics. Using

ocular macroplots, Art gathered cover data on all vascular plant species; detailed tree

data on variable and fixed area plots within the macroplot; physical site characteristics;

disturbance history and severity data on the same area.</p>



<p>Soil monitoring was completed on 22 macroplots. Soil monitoring included two 50 foot

transects centered on the plot and at right angles to each other. A compaction core was

taken every 10 feet and the visual soil disturbance properties for displacement, severely

burned and the other soil condition classes were taken along each transect.</p>



<p>Soil monitoring was done by Jerry Niehoff, Dale Olson, Art Zack and Val Goodnow during

the 1991 field season.</p>



