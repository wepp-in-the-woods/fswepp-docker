<p align="center"><br>

<strong>BUMBLEBEE TIMBER SALE</strong><br>

FERNAN RANGER DISTRICT</p>



<p>The Winged Subsoiler is an implement used to break up existing compaction on skid

trails, haul roads and landings. During September the Winged Subsoiler was used on this

sale to break up compaction on roads and landings.</p>



<p align="center">Monitoring Results:</p>



<p>An inspection of roads and landings, showed that the Winged Subsoiler did a good job of

breaking up the compaction and produced a high degree of soil clod fracturing. The depth

of fracturing was down to around 36 inches. The Winged Subsoiler did not have any trouble

penetrating very dense material. The results of putting roads to bed using the Winged

Subsoiler will be evaluated over the next few years. After a minor rain-on-snow event in

November the treated roads were evaluated and the results looked very good. A section of

road which previously exhibited severe rill and gully erosion showed no evidence of

erosion after subsoiling. Monitoring was done by Jerry Niehoff in September and November

of 1991.</p>

