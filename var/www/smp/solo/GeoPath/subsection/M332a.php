<p>GLOSSARY OF SELECTED TERMS</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>The definitions listed are taken from the Glossary of Landforms in the

USDA Natural Resources Conservaton Service National Soil Survey Handbook

and from the American Geological Institute Glossary of Geology.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Alluvium--Unconsolidated clastic material deposited by running water,

including</p>



<p>gravel, sand, silt, clay, and various mixtures of these.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Cirque--Semicircular, concave, bowl-like area with steep face primarily</p>



<p>resulting from erosive activity of a mountain glacier.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Cirqueland--Areas dominated by cirques and related glacial features,

rock, and</p>



<p>rubbleland.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Colluvium--Unconsolidated, unsorted earth material being transported

or</p>



<p>deposited on sideslopes and/or at the base of slopes by mass movement

(e.g. direct gravitational action) and by local, unconcentrated runoff.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Ecological Unit--A mapped landscape unit designed to meet management</p>



<p>objectives, comprised of one or more ecological types.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Floodplain--The nearly level plain that borders a stream and is subject

to</p>



<p>inundation under flood-stage conditions unless protected artificially.

It is usually a constructional landform built of sediment deposited during</p>



<p>overflow and lateral migration of streams.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Fluvial--Of or pertaining to rivers; produced by river action.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Intermontane Basin--A generic term for wide structural depressions between</p>



<p>mountain ranges that are partly filled with alluvium and called &quot;valleys&quot;

in the vernacular. Intermontane basins may be drained internally (bolsons)</p>



<p>or externally (semi-bolson).</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Lacustrine deposit--Clastic sediments and chemical precipitates deposited

in</p>



<p>lakes.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Landscape--A collection or related, natural landforms; usually the land

surface</p>



<p>which the eye can comprehend in a single view.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Loess--Fine-grained, wind-deposited material, dominantly of silt size.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Metasediment--A sediment or sedimentary rock that shows evidence of having

been</p>



<p>subjected to metamorphism.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Moraine--A mound, ridge, or other distinct accumulation of unsorted,</p>



<p>unstratified glacial drift, predominantly till, deposited chiefly by

direct</p>



<p>action of glacier ice, in a variety of topographic landforms that are</p>



<p>independent of control by the surface on which the drift lies.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Outwash--Stratified detritus (chiefly sand and gravel) removed or &quot;washed</p>



<p>out&quot; from a glacier by melt-water streams and deposited in front

of or</p>



<p>beyond the end moraine or the margin of an active glacier.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Potential Vegetation--The biotic community that would be established

if all</p>



<p>successional sequences of its ecosystem were completed without additional</p>



<p>human-caused disturbance under present environmental conditions.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Residuum--Unconsolidated, weathered or partly weathered mineral material

that</p>



<p>accumulates by disintegration of bedrock in place.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Section--An ecological unit in the subregion planning and analysis scale

of the</p>



<p>National Hierarchical Framework corresponding to subdivisions of a Province

having broad areas of similar geomorphic process, stratigraphy, geologic

origin, drainage networks, topography, and regional climate. Such areas

are often inferred by relating geologic maps to potential natural vegetation

groupings as mapped by Kuchler (1964).</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Soil Great Group--A category of soil classification where soils are placed</p>



<p>together based on close similarities in kind, arrangement, and degree

of</p>



<p>expression of horizons; close similarities in soil moisture and temperature</p>



<p>regimes; and similarities in base status.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Soil Moisture Regime--Classes of soil moisture that are based on the

assumption</p>



<p>that the soil supports whatever vegetation it is capable of supporting.</p>



<p>Moisture regimes are defined in terms of the ground-water level and in</p>



<p>terms of the presence or absence of water held at a tension of &lt;15

bars in</p>



<p>the moisture control section by periods of the year.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Soil Temperature Regime--The characteristic temperature regime of a soil

that</p>



<p>is described by the mean annual soil temperature, the average seasonal</p>



<p>fluctuations from that mean, and the mean warm or cold seasonal soil</p>



<p>temperature gradient within the main root zone, which is the zone from

a</p>



<p>depth of 5 to 100 centimeters.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Subsection--An ecological unit in the subregion planning and analysis

scale of</p>



<p>the National Hierarchical Framework corresponding to subdivisions of

a Section into areas with similar surficial geology, lithology, geomorphic

process, soil great groups, subregional climate, and potential vegetation.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Till--Dominantly unsorted and unstratified drift, deposited by a glacier,

and</p>



<p>consisting of a heterogeneous mixture of clay, silt, sand, gravel, stones,</p>



<p>and boulders.</p>



