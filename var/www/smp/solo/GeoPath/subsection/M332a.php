	<h2>M332A - Idaho Batholith</h2>

<h3><a name="anchor437959"></a>M332A - Idaho Batholith</h3>



<p>&nbsp;</p>



<p>M332Aa Lochsa/Salmon Breaklands</p>



<p>Landscape Characteristics: Highly dissected stream breaklands formed

in gneiss, schist, and granitic rocks. Volcanic ash influenced loess covers

some areas. Elevations range from 1400 to 7000 feet. Drainage density is

high.</p>



<p>Climate: Mean annual precipitation ranges from 20 to 60 inches, about

60 percent falling as snow. The soil temperature and moisture regimes are

frigid and typic xeric.</p>



<p>Potential Vegetation: Western ponderosa forest/Grand fir-Douglas-fir

forest</p>



<p>Relationships of Dominant Map Unit Components:</p>



<p>Landform Geology Soil Taxa</p>



<p>Stream breaklands Loess/Schist/Gneiss Udivitrands/Haploxerolls/</p>



<p>granite Haplumbrepts</p>



<p>Valley floor/terraces Alluvium Xerofluvents/Udivitrands/</p>



<p>Haploxerolls</p>



<p>Disturbance and Land Use: The primary natural disturbances are fire,

insects, disease, and mass wasting. Land use includes livestock grazing,

mining, and timber harvest.</p>



<p>&nbsp;</p>



<p>M332Ab Central Idaho Glaciated Mountains</p>



<p>Landscape Characteristics: Glaciated mountain slopes, ridges, and cirquelands

that formed in quartz monzonite, gneiss, and granite. Many surface soils

are influenced by volcanic ash. Elevations range from 3000 to 8500 feet.

Drainage density is moderate. Lakes occur in cirque basins and on glacial

valley floors.</p>



<p>Climate: Mean annual precipitation ranges from 35 to 70 inches, about

60 to 70 percent falling as snow. The soil temperature and moisture regimes

are cryic and udic.</p>



<p>Potential Vegetation: Western spruce-fir forest/Grand fir-Douglas-fir

forest</p>



<p>Relationships of Dominant Map Unit Components:</p>



<p>Landform Geology Soil Taxa</p>



<p>Valleys/terraces/ Alluvium/loess Vitricryands/Cryaquepts/</p>



<p>moraines Cryochrepts</p>



<p>Glaciated mountain Granitic rocks/till Cryochrepts/Cryoboralfs/</p>



<p>slopes and ridges Vitricryands</p>



<p>Cirqueland Granitic rocks Cryochrepts</p>



<p>Disturbance and Land Use: The primary natural disturbances are fire,

insects, disease, and mass wasting. Since much of the subsection is either

roadless or designated wilderness, human disturbances are slight.</p>



<p>&nbsp;</p>



<p>M332Ac Central Idaho Mountains and Basins</p>



<p>Landscape Characteristics: Uplands, broad basins, and breaklands that

formed gneiss, schist, and granitic rocks. Volcanic ash caps occur throughout

the area. Elevations range from 1600 to 8200 feet. Drainage density is moderate

to high. Wetlands occur throughout the subsection associated with glacial

till. Lakes occur in high elevation cirques.</p>



<p>Climate: Mean annual precipitation ranges from 25 to 80 inches, about

60 percent falling as snow. The soil temperature and moisture regimes are

cryic and udic.</p>



<p>Potential Vegetation: Western spruce-fir forest/Grand fir-Douglas-fir

forest</p>



<p>Relationships of Dominant Map Unit Components:</p>



<p>Landform Geology Soil Taxa</p>



<p>Uplands/cirquelands Granitic rocks/till Cryochrepts/Vitricryands/</p>



<p>Dystrochrepts/Cryoboralfs</p>



<p>Basins/terraces/ Till/colluvium/ Aquands/Cryands/Cryochrepts</p>



<p>floodplains alluvium Dystrochrepts</p>



<p>Breaklands Granitic rocks/ Dystrochrepts/Haploxerolls</p>



<p>colluvium</p>



<p>Disturbance and Land Use: The primary natural disturbances are fire,

insects, disease, and mass wasting. Land use is predominantly timber harvest

and mining.</p>



<p>&nbsp;</p>



<h3><a name="anchor444576"></a>M332B - Bitterroot Valley</h3>



<p>&nbsp;</p>



<p>M332Ba Bitterroot Glaciated Canyons</p>



<p>Landscape Characteristics: Glaciated mountains with numerous heavily

glaciated parallel canyons. The dominant parent material is till derived

from quartz monzonite, granodiorite, gneiss, and schist. Elevations range

from 4000 to 10000 feet. Drainage density is moderate. Numerous lakes occur

in high elevation cirques.</p>



<p>Climate: Mean annual precipitation ranges from 14 to 70 inches, about

60 percent falling as snow. The soil temperature and moisture regimes are

cryic and typic ustic (udic at higher elevations).</p>



<p>Potential Vegetation: Douglas-fir forest/Western spruce-fir forest</p>



<p>Relationships of Dominant Map Unit Components:</p>



<p>Landform Geology Soil Taxa</p>



<p>Cirquelands/moraines/ Till/talus/ Cryochrepts/Cryaquepts</p>



<p>trough walls granitic rocks</p>



<p>Moraines/terraces/ Till/alluvium Cryochrepts/Cryaquepts/</p>



<p>floodplains Cryaquents</p>



<p>Dipslopes Granitic rocks Argiborolls/Cryochrepts</p>



<p>Disturbance and Land Use: The primary natural disturbance is fire. Land

use is predominantly wilderness. Some timber harvest and grazing occurs

on the eastern dipslope.</p>



<p>&nbsp;</p>



<p>M332Bb Bitterroot/Frenchtown Valleys</p>



<p>Landscape Characteristics: Intermontane valleys that formed in alluvium,

outwash, and lacustrine sediments. Elevations range from 3000 to 4400 feet.

Drainage density is slight. Wetlands occur along both the Clark Fork and

Bitterroot rivers.</p>



<p>Climate: Mean annual precipitation ranges from 11 to 25 inches, about

40 to 60 percent falling as snow. The soil temperature and moisture regimes

are frigid and typic ustic.</p>



<p>Potential Vegetation: Foothills prairie/Western ponderosa forest</p>



<p>Relationships of Dominant Map Unit Components:</p>



<p>Landform Geology Soil Taxa</p>



<p>Foothills Valley fill Eutroboralfs</p>



<p>Terraces/fans Alluvium/outwash Argiborolls/Haploborolls</p>



<p>Floodplains Alluvium Udifluvents/Endoaquents/</p>



<p>Haplaquolls/Endoaquolls</p>



<p>Disturbance and Land Use: The primary natural disturbances are flooding

and fire. Land use is predominantly extensive urban/suburban development

and agricultural activities.</p>



<p>&nbsp;</p>



