<p>metasedimentary/granite</p>



<p>Mountainsides/benches Mixed sedimentary/ Cryoborolls/Cryoboralfs/</p>



<p>metasedimentary/ Cryochrepts</p>



<p>volcanic</p>



<p>Disturbance and Land Use: The primary natural disturbance is fire. Land

use is predominantly timber harvest, grazing, mining, and suburban development.</p>



<p>&nbsp;</p>



<p>M332Be Flint Creek/Upper Willow Creek Basins</p>



<p>Landscape Characteristics: Structural basins and low relief uplands that

formed in lacustrine deposits, alluvium, metasedimentary, and volcanic rocks.

Elevations range from 4000 to 7000 feet. Drainage density is moderate.</p>



<p>Wetlands are fairly common within the basins.</p>



<p>Climate: Mean annual precipitation ranges from 11 to 20 inches, about

30 percent falling as snow. The soil temperature and moisture regimes are

cryic and typic ustic. Frigid temperatures occur at lower elevations.</p>



<p>Potential Vegetation: Douglas-fir forest/Foothills prairie</p>



<p>Relationships of Dominant Map Unit Components:</p>



<p>Landform Geology Soil Taxa</p>



<p>Basins/terraces/ Alluvium/colluvium Cryoborolls/Cryoboralfs/</p>



<p>fans/floodplains Argiborolls/Haploborolls</p>



<p>Uplands Quartzite/argillite/ Cryoboralfs/Cryochrepts/</p>



<p>limestone Haploborolls/Argiborolls</p>



<p>Disturbance and Land Use: The primary natural disturbances are fire and

flooding. Land use is predominantly mining, timber harvest, grazing, hay

production, and suburban development.</p>



<p>&nbsp;</p>



