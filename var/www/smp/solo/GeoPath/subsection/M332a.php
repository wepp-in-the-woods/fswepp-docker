<h3><a name="anchor459716"></a>M332C - Rocky Mountain Front</h3>



<p>&nbsp;</p>



<p>332Ca North Rocky Mountain Front</p>



<p>Landscape Characteristics: Thrust faulted mountains that formed from

argillite, siltite, and quartzite. Elevations range from 5000 to 10800 feet.

Drainage density is high. High mountain lakes are common and their are existing

glaciers. This subsection drains into Hudson Bay.</p>



<p>Climate: Mean annual precipitation ranges from 27 to 90 inches, about

50 to 70 percent falling as snow. The soil temperature and moisture regimes

are cryic and udic (typic ustic at lower elevations). Chinook winds are

common along the mountain front.</p>



<p>Potential Vegetation: Douglas-fir forest/Western spruce-fir forest</p>



<p>Relationships of Dominant Map Unit Components:</p>



<p>Landform Geology Soil Taxa</p>



<p>Mountainsides/moraines/ Argillite/siltite/ Cryocrepts/Cryoborolls</p>



<p>outwash terraces till</p>



<p>Cirquelands Argillite/siltite/ Cryorthents</p>



<p>till/talus</p>



<p>Disturbance and Land Use: The primary natural disturbance is fire. Land

use is predominantly recreation since this subsection is within Glacier

National Park.</p>



<p>&nbsp;</p>



<p>M332Cb Middle Rocky Mountain Front</p>



<p>Landscape Characteristics: Thrust faulted and folded mountains that formed

from mudstone, sandstone, and lesser amounts of limestone and dolomite.

Elevations range from 5000 to 9000 feet. Drainage density is high.</p>



<p>Climate: Mean annual precipitation ranges from 20 to 60 inches, about

50 to 70 percent falling as snow. The soil temperature and moisture regimes

are cryic and typic ustic. Chinook winds are common along the mountain front.</p>



<p>Potential Vegetation: Douglas-fir forest/Western spruce-fir forest with

Foothills prairie on drier south and west lower slopes.</p>



<p>Relationships of Dominant Map Unit Components:</p>



<p>Landform Geology Soil Taxa</p>



<p>Mountainsides/moraines/ Mudstone/sandstone/ Cryochrepts/Cryoboralfs/</p>



<p>outwash terraces till Cryoborolls</p>



<p>Cirquelands Mudstone/sandstone Cryorthents</p>



<p>Disturbance and Land Use: The primary natural disturbance is fire. Land

use is predominantly recreational activities.</p>



