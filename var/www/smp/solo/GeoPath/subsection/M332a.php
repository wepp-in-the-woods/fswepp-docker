<p>Landscape Characteristics: Mountains that formed in granitic rocks with

some glaciation at higher elevations. Elevations range from 3800 to 8800

feet. Drainage density is moderate. Lakes occur in a few high elevation

cirques.</p>



<p>Climate: Mean annual precipitation ranges from 16 to 55 inches, about

60 percent falling as snow. The soil temperature and moisture regimes are

cryic and typic ustic.</p>



<p>Potential Vegetation: Douglas-fir forest/Western spruce-fir forest</p>



