<p>Floodplains/terraces Alluvium/outwash Cryochrepts/Cryaquents</p>



<p>Disturbance and Land Use: The primary natural disturbance is fire. Land

use is predominantly timber harvest, grazing, and some mining.</p>



<p>&nbsp;</p>



<p>M332Bd Anaconda/Flint Creek Mountains</p>



<p>Landscape Characteristics: Glaciated fault block mountains that formed

from complexly folded and faulted sedimentary and igneous rocks. Elevations

range from 4000 to 10200 feet. Drainage density is moderate to high. Lakes

are common in cirque basins.</p>



<p>Climate: Mean annual precipitation ranges from 11 to 50 inches, about

55 percent falling as snow. The soil temperature and moisture regimes are

cryic and typic ustic.</p>



<p>Potential Vegetation: Douglas-fir forest/Foothills prairie</p>



<p>Relationships of Dominant Map Unit Components:</p>



