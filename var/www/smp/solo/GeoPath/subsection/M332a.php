<p>&nbsp;</p>



<p>Landscape Characteristics: Low relief mountains and stream breaklands

that formed in highly weathered Idaho Batholith granitics. Volcanic ash

deposits are common and influence many soils. Elevations range from 2000

to 6500 feet. Drainage density is high.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>Climate: Mean annual precipitation ranges from 35 to 80 inches, about

40 to 50 percent falling as snow. Rain on snow events are common below 4500

feet. The soil temperature and moisture regimes are Frigid and Udic. Some

cryic temperatures occur at the higher elevations. This subsection is influenced

by</p>



<p>a maritime climate.</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



<p>&nbsp;</p>



