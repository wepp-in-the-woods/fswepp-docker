<p><a NAME="anchor344616"></a><b>John A. Nesser</b> is Regional Soil Scientist with the
Forest Service Northern Region in Missoula, MT. He holds a B.S. degree in geography and
and M.S. degree in resource management (soils) from the University of Wisconsin - Stevens
Point. His primary responsibilities include management of the Regional soils program and
ECOMAP/Ecological Unit Inventory coordination.<br>
<b>Gary L. Ford</b> is Regional Soil Management Specialist with the Forest Service
Northern Region. He is stationed in Coeur d'Alene, ID. He holds B.S. and M.S. degrees in
earth sciences and a Ph.D. degree in soil science from Montana State University. His
primary responsibilities include soil/Ecological Unit Inventory correlation and Regional
coordination of soil management and monitoring activities.<br>
<a NAME="anchor346231"></a><b>C. Lee Maynard</b> is a Soil Scientist/GIS Analyst with the
Natural Resources Conservation Service, Helena, MT. She was formerly with the Helena
National Forest. She holds a B.S. degree in forest pathology from Utah State University
and an M.S. degree in forest ecology from the University of Montana. Her primary
responsibilities include the creation and analysis of spatial data and quality control on
map projects.<br>
<a NAME="anchor346993"></a><b>Deborah S. Page-Dumroese</b> is a Research Soil Scientist
with the Rocky Mountain Research Station (formerly Intermountain Research Station) in
Moscow, ID. She holds a B.S. degree in natural resource management from Grand Valley State
University, an M.S. degree in forest soils from Michigan Technological University and a
Ph.D. degree in forest soils from the University of Idaho. Her main research focus is on
maintaining long-term soil productivity after timber harvesting and site preparation.</p>

