			<h2 class="topheading">ECOLOGICAL SUBSECTIONS</h2>

			<p><strong>Ecological Units</strong>&#8212;The National Hierarchical Framework of Ecological Units was formulated at a meeting of representatives from all Forest Service Regions, two Research Stations, the USDA Soil Conservation Service (now the Natural Resources Conservation Service), and the Nature Conservancy, and was adopted by the Forest Service in November, 1993.</p>
			<p><strong>Subsections</strong> are a refinement of this ecological unit hierarchy, based on physical and biological criteria. They allow integration of Forest- and Ranger District-level activities (e.g., timber harvest, monitoring, watershed analysis) into the broader scale analyses, assessments and management decision-making processes.</p>

