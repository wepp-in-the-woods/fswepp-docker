			<h2><a name="anchor280858" id="anchor280858"></a>INTRODUCTION</h2>
			<p>This subsection report is an effort to develop units that integrate information from various disciplines, single-purpose maps, and existing regionalizations. The development of ecological units and descriptive data is an iterative process that will continue as additional information becomes available. Peer review and comments by users of this information are important to this process. All ecological mapping and descriptions follow the Forest Service's National Hierarchical Framework of Ecological Units (ECOMAP 1993).</p>


			<h2><a name="anchor281494" id="anchor281494"></a>DEVELOPMENT OF THE MAP AND DESCRIPTIONS</h2>
			<p class="firstsection">Subsections are smaller areas of sections with similar surficial geology, lithology, geomorphic process, soil groups, subregional climate, and potential natural communities (ECOMAP 1993). Each subsection has landscape components that differentiate it from adjacent subsections. The differentia used to place lines on the map include geologic materials, geomorphic features, and climate. Other components are used to describe each subsection but are not used to delineate the units; these are called accessory characteristics and include soils and vegetation.</p>
			<p>Geologic materials affect ecosystem processes. The physical and chemical properties of bedrock and surficial materials influence such processes as weathering, soil formation, vegetation distribution, and stream chemistry. Landscape features such as elevation, aspect, and slope influence the distribution of solar radiation and precipitation. This affects vegetation patterns, soil formation, hydrologic processes, and animal populations.</p>
			<p>The U.S. Geological Survey 1:500,000 Albers Conic Equal-Area Maps for Montana, Idaho, and North Dakota were used for the base maps. Geologic materials were determined from 1:500,000 State geology maps with some minor refinements based on additional geologic maps and local knowledge. Geomorphic features were determined using 1:500,000 State topographic and shaded relief maps and local knowledge. Broad climatic zones were inferred from potential natural vegetation mapping using Kuchler (1964), regional vegetation information, and isohyetal maps.</p>
			<p>The Forest Service's National Hierarchical Framework of Ecological Units (EOMAP 1993) numbering/lettering system was applied to all subsections. For example, in this publication, a capitalized letter following the number indicates a section; the lowercase letter that follows indicates a subsection.</p>

			<h2><a name="anchor282150" id="anchor282150"></a>USES OF THE MAP AND DESCRIPTIONS</h2>
			<p class="firstsection">Subsection maps and descriptions are used at the subregion scale for strategic, multi-forest, Statewide, and multiagency analysis and assessment (ECOMAP 1993). Use of this or any map should be consistent with the scale and methods used to compile it. The subsection map was designed for broad scale ecosystem analysis and should not be used for detailed work. The map was produced at 1:500,000 and is intended to be used at that or a smaller scale. Plotting and using the subsection map at scales larger than 1:500,000 (1:24,000 for example) constitutes a misuse of the map and should not be done.</p>
			<p>Due to the great amount of variability within the subsections, interpretations for parameters such as erosion were not made. Interpretations at this level must necessarily be based on many assumptions, generalizations, and averages. Such ratings or interpretations generally lack any real value.</p>

			<h2><a name="anchor282601" id="anchor282601"></a>LANDSCAPE/CLIMATE OVERVIEW OF THE NORTHERN REGION</h2>
			<p>The following overview is intended to give the reader a general understanding of the landscapes and climate of the Northern Region. The overview is arranged by Section or groups of similar Sections.</p>
			<p><a name="anchor33004" id="anchor33004"></a><strong><a href="../subsection/S_331.php#anchor901136">331A Palouse Prairie</a></strong> &#8212; This Section has a maritime-influenced warm temperate climate with hot, dry summers and cool, moist winters. Columbia River Basalts formed much of the landscape of low relief plateaus and rolling hills dotted with steptoes. Major streams are deeply entrenched into the uplifted highlands forming large canyons. This Section covers 3,243,257 acres.</p>

<p><a name="anchor32338" id="anchor32338"></a><strong><a href="../subsection/S_331.php#anchor87937">331D Northwestern Glaciated Plains</a></strong> and <br />
<a name="anchor31294" id="anchor31294"></a><strong><a href="../subsection/S_331.php#anchor91124">331E Northern Glaciated Plains</a></strong> &#8212; These Sections have a cold continental climate with warm, dry summers and cold, dry winters. Large expanses of prairie with scattered highlands characterize these Sections. Glacial drift up to 100 feet thick covers the landscape and glacial knob and kettle topography is typical. Ice damming of major rivers also created areas of lacustrine and delta sediments. These Sections cover 40,078,875 acres.</p>

<p><a name="anchor30872" id="anchor30872"></a><strong><a href="../subsection/S_331.php#anchor101492">331F Northwestern Great Plains,</a></strong> <br />
<a name="anchor29722" id="anchor29722"></a><strong><a href="../subsection/S_331.php#anchor104508">331G
Powder River Basin</a></strong>, and <br />
<a name="anchor28951" id="anchor28951"></a><strong><a href="../subsection/S_342.php#anchor10635">342A Bighorn Basin</a></strong> &#8212; These Sections all have cold continental climates with warm to hot, dry summers and cold, dry winters. These non-glaciated plains are typically gently rolling low relief grasslands. Bedrock is Cretaceous marine shale, Tertiary fluvial sediments, and some large sandstone units. Clinker beds or &quot;scoria&quot; from burnt coal seams form colorful resistant beds in Tertiary outcrops. These Sections cover 41,524,948 acres.</p>

<p><a name="anchor28657" id="anchor28657"></a><strong><a href="../subsection/S_332.php#anchor904036">332A Northeastern Glaciated Plains</a></strong> and <br />
<a name="anchor28331" id="anchor28331"></a><strong><a href="../subsection/S_332.php#anchor216048">332B Western Glaciated Plains</a></strong> &#8212; These Sections both have a cold continental climate with warm to hot, humid summers and cold, moist winters. The topography is a rolling, glaciated drift prairie with numerous potholes and moraines. Thick glacial drift covers most of the landscape though lacustrine sediments also occur as a result of glacial ice damming. These Sections cover 16,592,171 acres.</p>

<p><a name="anchor27896" id="anchor27896"></a><strong><a href="../subsection/M331.php#anchor906323">M331A Yellowstone Highlands</a></strong>, <br />
<a name="anchor27481" id="anchor27481"></a><strong><a href="../subsection/M331.php#anchor248862">M331B
Bighorn Mountains</a></strong>, <br />
<a name="anchor27148" id="anchor27148"></a><strong><a href="../subsection/M332.php#anchor459716">M332C
Rocky Mountain Front</a></strong>, <br />
<a name="anchor26664" id="anchor26664"></a><strong><a href="../subsection/M332.php#anchor464231">M332D
Belt Mountains</a></strong>, and <br />
<a name="anchor26229" id="anchor26229"></a><strong><a href="../subsection/M332.php#anchor541053">M332E Beaverhead Mountains</a></strong> &#8212; These Sections all have cold continental climates with warm, dry summers and cold, dry winters. The topography is dominantly uplifted and faulted mountains and high plateaus with a variety of bedrock. Higher elevations are typically shaped by mountain glaciation and exhibit typical cirqueland features. Steep dipslopes, flatirons, and hogbacks are typical of the Rocky Mountain Front and parts of Bighorn Mountains. Large gravel filled valleys are typical in the Beaverhead Mountains Section of southwest Montana. In central Montana, the mountains occur as widely separated uplifts surrounded and isolated by prairie. These Sections cover 23,835,539 acres.</p>

<p><a name="anchor25464" id="anchor25464"></a><strong><a href="../subsection/M332.php#anchor437959">M332A Idaho Batholith</a></strong> &#8212; This Section has a maritime influenced cool temperate climate with warm, dry summers and cool, moist winters. The batholith is a large, contiguous uplifted area of granitics characterized by large, rounded mountain masses and basin areas. Soils formed in gruss, loess, and volcanic ash. The landscape is deeply cut by the Idaho canyonlands. Mountain glaciation occurred at higher elevations. This Section covers 3,883,095 acres.</p>

<p><a name="anchor24930" id="anchor24930"></a><strong><a href="../subsection/M332.php#anchor444576">M332B
Bitterroot Valley</a></strong> and <br />
<a name="anchor24591" id="anchor24591"></a><strong><a href="../subsection/M333.php#anchor138165">M333D Bitterroot Mountains</a></strong> &#8212; These Sections have maritime influenced cool temperate climates with warm, dry summers and cool, moist winters. Belt rocks (quartzite and argillite) are resistant to weathering and form typical landscapes of steep slopes and incised V-shaped valleys. Talus and rock outcrops are common. The Bitterroot Valley is a large intermontane valley formed in valley fill materials. The higher elevations are typically large U-shaped valleys with abundant rock outcrop, cirques, horns, and other glacial features. Till, outwash, and moraines fill the valley floors. These Sections cover 9,136,505 acres.</p>

<p><a name="anchor23569" id="anchor23569"></a><strong><a href="../subsection/M332.php#anchor547638">M332G Blue Mountains</a></strong> &#8212; This Section has a maritime influenced cool temperate climate with warm, dry summers and cool, moist winters. The landscape is dominated by deeply entrenched major streams. Steep, high relief breaks and narrow valleys are characteristic. Columbia River Basalts and related plateau landscapes also occur in this Section. This Section covers 1,345,904 acres.</p>

<p><a name="anchor23223" id="anchor23223"></a><strong><a href="../subsection/M333.php#anchor125147">M333A Okanogan Highlands</a></strong>, <br />
<a name="anchor22590" id="anchor22590"></a><strong><a href="../subsection/M333.php#anchor130638">M333B Flathead Valley</a></strong>, and <br />
<a name="anchor21793" id="anchor21793"></a><strong><a href="../subsection/M333.php#anchor135093">M333C Northern Rockies</a></strong> &#8212; These Sections have maritime influenced cool temperate climates with warm, dry summers and cool, moist winters. The landscape is dominated by rounded landforms that resulted from continental glaciation. Till deposits, often 50 to 100 feet thick, cover the mountainsides and valley floors. A mantle of loess and volcanic ash occurs over most of the glacial deposits. Some metasedimentary Belt rock landscapes remain. The Northern Rockies Section is dominantly metasedimentary mountains that have been characteristically shaped by alpine glaciation. These Sections cover 11,908,199 acres.</p>

