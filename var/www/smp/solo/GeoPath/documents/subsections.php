			<h2><a name="anchor469813" id="anchor469813"></a>GLOSSARY OF SELECTED TERMS</h2>
			<p>The definitions listed are taken from the Glossary of Landforms in the USDA Natural Resources Conservaton Service National Soil Survey Handbook and from the American Geological Institute Glossary of Geology.</p>
			<dl class="glossary">
				<dt>Alluvium</dt>
				<dd>&#8212;Unconsolidated clastic material deposited by running water, including gravel, sand, silt, clay, and various mixtures of these.</dd>
				<dt>Cirque</dt>
				<dd>&#8212;Semicircular, concave, bowl-like area with steep face primarily resulting from erosive activity of a mountain glacier.</dd>
				<dt>Cirqueland</dt>
				<dd>&#8212;Areas dominated by cirques and related glacial features, rock, and rubbleland.</dd>
				<dt>Colluvium</dt>
				<dd>&#8212;Unconsolidated, unsorted earth material being transported or deposited on sideslopes and/or at the base of slopes by mass movement (e.g. direct gravitational action) and by local, unconcentrated runoff.</dd>
				<dt>Ecological Unit</dt>
				<dd>&#8212;A mapped landscape unit designed to meet management objectives, comprised of one or more ecological types.</dd>
				<dt>Floodplain</dt>
				<dd>&#8212;The nearly level plain that borders a stream and is subject to inundation under flood-stage conditions unless protected artificially. It is usually a constructional landform built of sediment deposited during overflow and lateral migration of streams.</dd>
				<dt>Fluvial</dt>
				<dd>&#8212;Of or pertaining to rivers; produced by river action.</dd>
				<dt>Intermontane Basin</dt>
				<dd>&#8212;A generic term for wide structural depressions between mountain ranges that are partly filled with alluvium and called &quot;valleys&quot; in the vernacular. Intermontane basins may be drained internally (bolsons) or externally (semi-bolson).</dd>
				<dt>Lacustrine deposit</dt>
				<dd>&#8212;Clastic sediments and chemical precipitates deposited in lakes.</dd>
				<dt>Landscape</dt>
				<dd>&#8212;A collection or related, natural landforms; usually the land surface which the eye can comprehend in a single view.</dd>
				<dt>Loess</dt>
				<dd>&#8212;Fine-grained, wind-deposited material, dominantly of silt size.</dd>
				<dt>Metasediment</dt>
				<dd>&#8212;A sediment or sedimentary rock that shows evidence of having been subjected to metamorphism.</dd>
				<dt>Moraine</dt>
				<dd>&#8212;A mound, ridge, or other distinct accumulation of unsorted, unstratified glacial drift, predominantly till, deposited chiefly by direct action of glacier ice, in a variety of topographic landforms that are independent of control by the surface on which the drift lies.</dd>
				<dt>Outwash</dt>
				<dd>&#8212;Stratified detritus (chiefly sand and gravel) removed or &quot;washed out&quot; from a glacier by melt-water streams and deposited in front of or beyond the end moraine or the margin of an active glacier.</dd>
				<dt>Potential Vegetation</dt>
				<dd>&#8212;The biotic community that would be established if all successional sequences of its ecosystem were completed without additional human-caused disturbance under present environmental conditions.</dd>
				<dt>Residuum</dt>
				<dd>&#8212;Unconsolidated, weathered or partly weathered mineral material that accumulates by disintegration of bedrock in place.</dd>
				<dt>Section</dt>
				<dd>&#8212;An ecological unit in the subregion planning and analysis scale of the National Hierarchical Framework corresponding to subdivisions of a Province having broad areas of similar geomorphic process, stratigraphy, geologic origin, drainage networks, topography, and regional climate. Such areas are often inferred by relating geologic maps to potential natural vegetation groupings as mapped by Kuchler (1964).</dd>
				<dt>Soil Great Group</dt>
				<dd>&#8212;A category of soil classification where soils are placed together based on close similarities in kind, arrangement, and degree of expression of horizons; close similarities in soil moisture and temperature regimes; and similarities in base status.</dd>
				<dt>Soil Moisture Regime</dt>
				<dd>&#8212;Classes of soil moisture that are based on the assumption that the soil supports whatever vegetation it is capable of supporting. Moisture regimes are defined in terms of the ground-water level and in terms of the presence or absence of water held at a tension of &lt;15 bars in the moisture control section by periods of the year.</dd>
				<dt>Soil Temperature Regime</dt>
				<dd>&#8212;The characteristic temperature regime of a soil that is described by the mean annual soil temperature, the average seasonal fluctuations from that mean, and the mean warm or cold seasonal soil temperature gradient within the main root zone, which is the zone from a depth of 5 to 100 centimeters.</dd>
				<dt>Subsection</dt>
				<dd>&#8212;An ecological unit in the subregion planning and analysis scale of the National Hierarchical Framework corresponding to subdivisions of a Section into areas with similar surficial geology, lithology, geomorphic process, soil great groups, subregional climate, and potential vegetation.</dd>
				<dt>Till</dt>
				<dd>&#8212;Dominantly unsorted and unstratified drift, deposited by a glacier, and consisting of a heterogeneous mixture of clay, silt, sand, gravel, stones, and boulders.</dd>
			</dl>

