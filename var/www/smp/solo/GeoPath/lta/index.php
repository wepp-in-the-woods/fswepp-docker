			<h2 class="topheading">LANDTYPE ASSOCIATIONS</h2>
			
			<p>Mapping of landtype associations (LTAs) in Region 1 was
undertaken in order to provide continuous coverage of landform
and geologic materials across all Forest Service lands in
northern Idaho, Montana, North Dakota and the northwestern
portion of South Dakota.</p>

			<p>LTAs allow biophysical stratification at the landscape level
for characterization of watershed properties important to
management.</p>

<p><a href="../../R1LTAs/text/chapt1_2.pdf">REFERENCE: Ford, G., C. Maynard, J. Nesser, D. Page-Dumroese. 1996. Landtype Associations of the Northern Region, A First Approximation.</a> (PDF<strong>**</strong>)<br />
<strong>NOTE: You will need Adobe Acrobat Reader</strong> (<a href="http://www.adobe.com/">free download</a>) <strong>to view PDF files. <br />**TIP: If your browser freezes when you try to view this PDF file, you may need to download the latest verstion of Adobe Acrobat Reader. Older versions of Reader can cause some browsers to hang.</strong></p>

<p>The use of landforms and geologic materials as differentia is
based on the assumption that these are the two variables which
can be mapped consistently at the landscape level that most
closely predict significant changes in inherent watershed, stream
and riparian properties.</p>

<h3><a href="../../R1LTAs/maps/mls.pdf">MAP: Geomorphic Overview of
the Northern Region</a> (PDF**)</h3>

<p>The 10 major geologic categories, as mapped, are: </p>

<ol>
    <li>Alluvium/Depositional </li>
    <li>Belt Series </li>
    <li>Granitics </li>
    <li>Sandstones and Shales </li>
    <li>Shales, Siltstones and Sandstones </li>
    <li>Carbonates </li>
    <li>Quartzites and Calc-Silicates </li>
    <li>Gneisses and Schists </li>
    <li>Volcanics </li>
    <li>Mass Failures </li>
</ol>

<p>Geologic categories are subdivided according to landform and
soil-forming process.</p>

