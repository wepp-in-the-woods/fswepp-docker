			<h3>Define Problems</h3>
			<p>Region One soil scientists have determined eight major &quot;monitoring concerns,&quot; as well as seven major activities on the land which need to be monitored to ensure sustainablility of natural soil condition and function.</p>
			<h3>Locations Affected</h3>
			<p>In order to be Regionally connected, documents in this information base have been categorized, where possible, by various geographic parameters: physical location; landtype association; ecological subsection; vegetation.&nbsp; These divisions provide the ability to share information among Forests.&nbsp; Research sites, such as Research Natural Areas and Experimental Forests, are special cases, where studies result in deeper understanding of conditions and processes which may lead to better management practices and more robust standards of soil quality.</p>
			<h3>Results to Date</h3>
			<p>There are two sources of information available to National Forest decision makers:</p>
			<ol>
				<li>Ongoing monitoring of soil conditions on activity areas</li>
				<li>Research, including Research Station experiments, NFS administrative studies and academic theses.</li>
