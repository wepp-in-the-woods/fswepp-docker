<p><br>
<font COLOR="#000000" FACE="Times Roman"><b>Purpose: </b>This monitoring item was
established to help ensure that the basic soil resource is not compromised in the
production of other resources such as timber harvesting, grazing, etc. The Plan requires
this item to be reported every five years. The expected accuracy and reliability of the
information is moderate. </font></p>

<p><font COLOR="#000000" FACE="Times Roman"><b>Background: </b>Soil resource management
has the goal of maintaining or improving long-term soil productivity and soil hydrologic
function. Soils can be physically damaged by the displacement, compaction, puddling, and
infiltration reduction due to the use of heavy equipment, especially during wet weather
and wet soil conditions. They can also be physically and chemically damaged by heat during
any intense burning, such as from wildfires, broadcast burning during site preparation, or
by the burning of mechanically-bunched slash piles. Soils that are damaged from the above
conditions incur adverse affects on their hydrologic function or sustain actual losses in
soil productivity. </font></p>

<p><font COLOR="#000000" FACE="Times Roman">Ideally, the soil quality standards that would
be used for measuring soil damage would be soil structure and fertility. Because these
soil qualities are difficult to measure, other soil qualities are substituted. These
substitutes are soil displacement and the associated soil compaction. </font></p>

<p><font COLOR="#000000" FACE="Times Roman">Region One has a policy that allows up to 15
percent detrimental disturbance (FSH 2509.18, 5/l/94). The Kootenai Forest uses the 15
percent detrimental disturbance as a measure to track the impact on site productivity. If
15 percent of an area is significantly disturbed, then we can say that it has probably
incurred a decrease in long-term site productivity. </font></p>

<p><font COLOR="#000000" FACE="Times Roman">Field monitoring is done within activity areas
using the line transect method. The line transect is perpendicular to the direction of the
ground-disturbing activity. Usually, three transects (an upper, middle, and lower) are
completed within each activity area. Each transect represents the activity that occurred
within that portion of the activity area. All of the monitoring completed so far is
representative of timber harvesting operations. The activities represented are cable
logging, forwarder logging, and tractor logging (rubber tired skidders and tracked
vehicles). Both summer and winter operational periods are included in the ground-based
activities. Fuel reduction activities had occurred in some of the units. </font></p>

<p><font COLOR="#000000" FACE="Times Roman"><b>Results: </b>Surveys have been completed on
88 timber harvest units scattered across the forest between 1992 and 1997. These areas
represent the current logging methods including the types of equipment being used for
skidding, mechanical falling, yarding, and slash piling. The areas ranged in size from two
to 143 acres. The 1992 report showed that 49 percent of the 501 acres surveyed to that
point were above the Forest Plan variability limits of 15 percent detrimental compaction.
Since then, 1,998 acres have been surveyed and only 1 percent (21 acres) were above the
Forest Plan limits. </font></p>

<p><font COLOR="#000000" FACE="Times Roman">Table F-4-1 displays the types of timber sales
monitored from 1992-1997. Table F-4-2 displays the number of units by harvest types
monitored from 1992-1997. Areas where cable logging methods were used show little or no
detrimental disturbance. The use of forwarders and winter logging, also, result in very
low to low detrimental disturbance. Areas where tractors were used resulted in a higher
level of detrimental disturbance, however, were still within the desired levels. In
general, the amount of heavily disturbed area increased directly with the number of
machinery operations. </font></p>

