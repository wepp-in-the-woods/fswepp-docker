<p><font COLOR="#000000" SIZE="-1" FACE="Arial1"><br>
</font><b><font COLOR="#000000" FACE="Times Roman">Evaluation:</font></b></p>

<p><font COLOR="#000000" FACE="Times Roman"><b>1992 Results: </b>The 1992 Monitoring
Report indicated that 49 percent of the surveyed acres, to that point, were beyond the
Forest Plan variability limits. Twenty units on 10 sales were monitored. Eight units
comprised of 245 acres contained more than 15 percent detrimental compaction. They ranged
from 19 to 27 percent. The influence of past activities was observed in one of the units.
Unit One of the Good Creek P.C. Sale only had 10 percent detrimental impact from the
current activities. As the result, though, of activity in the early sixties another nine
percent occurred at that time. Since the previous activity built excavated trails
horizontally across the terrain and the current activities were generally accomplished
vertically on the landscape, the combination of the two activity periods created 19
percent detrimental impact. </font></p>

<p><font COLOR="#000000" FACE="Times Roman">Some of the reasons for the areas beyond the
Forest Plan variability limit of 15 percent detrimental disturbance were: the inclusion of
small areas of steep terrain within areas of more gentle terrain, inadequate designation
of the proper logging equipment, the application of an approved silvicultural
prescription, and level of experience of the sale administrator(s) or logging operator(s).
</font></p>

<p><font COLOR="#000000" FACE="Times Roman"><b>Post 1992 Results: </b>Of the 68 units
surveyed since 1992, only 21 acres (one percent of measured acres) (one and one half
units) were beyond the Forest Plan variability limits. This very major change is mainly a
result of reduction of acres that are &quot;dozer piled&quot;. Other reasons include more
winter logging, more broadcast burning, and more use of forwarder logging equipment. </font></p>

<p><font COLOR="#000000" FACE="Times Roman"><b>1992-1997 Summary: </b>The total of 2,499
acres surveyed from 1992-1997 represents about seven percent of the annual harvest acres.
If the areas measured are representative of the entire Forest, about 11 percent of logging
and site preparation activities may be beyond the variability limit of the Forest Plan.
This number, however, is very misleading since only one percent of the harvest activities
since 1992 are detrimentally impactive and it appears that the number will remain this
low. In support of this statement it is noted that only seven of the remaining 68 units
(Table F-4-3a) were even beyond 10 percent. This represents only eight percent of the
acreage (Table F-4-3b). In fact 45 units were less than five percent detrimental impact. <b><br>
</b></font></p>

