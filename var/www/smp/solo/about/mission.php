				<h1 class="topheading">Why is SOLO being created?</h1>
				<p>In spite of Aldo Leopold's warning that soil is the basis of what is on
the landscape, the concern for development of long-term soil management practices for
forest land is relatively recent. Cumulative effects of over-cutting and overgrazing on
our National Forests have diminished the soils' abilities to sustain vegetative
productivity, leading to soil loss and habitat degradation.</p>
				<p>The hue and cry of environmentalism has heightened our awareness of
forest health problems, and required forest managers, who formerly worked in relative
anonymity, to bear the light and heat of public scrutiny. It has become increasingly
necessary to be able to elucidate to the public, as well as to state and federal
legislators, the scientific justification for Forest Service land management policies and
practices.</p>
				<p>As part of the effort to aid scientists and managers in their work to
sustain the health, productivity, natural beauty and cultural value of our National
Forests, SOLO will serve to consolidate and cross-reference Regional information on soil
monitoring and long term ecosystem sustainability.</p>
