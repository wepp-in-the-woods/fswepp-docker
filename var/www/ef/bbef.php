       <h2>Heartland of the Ponderosa Pine forest type</h2>
       <br>
       <p>The Boise Basin Experimental Forest, with three main units (Bannock, Bear Run, and Headquarters) totaling 8,740 acres that surround Idaho City, ID, was established in 1933 for conducting research on ponderosa pine forests.</p>
       <p>Currently, <b>Bannock Creek</b> has old ponderosa pine with Douglas-fir invading the understory.</p>
       <p><b>Bear Run</b> is a younger (80 to 100 year old) ponderosa pine climax forest.</p>
       <p>The <b>Headquarters</b> unit has the old headquarters facilities which are now administered by the Idaho City Ranger District. This small unit has a mixture of climax ponderosa pine and Douglas-fir. It also has ponderosa plantations that were terraced in the 1950s.</p>
       <p>The Boise Basin Experimental Forest is located about one hours' drive from Boise, ID.</p>

