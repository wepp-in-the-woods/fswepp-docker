					<p>A survey of lower Canyon Creek was conducted by Fred Rabe in 1995 (Rabe 1995). The surveyed portion of the stream was the last 0.3 miles (0.5 km) of Canyon Creek before it exits the Wellner Cliffs RNA. In this section of the stream, Canyon Creek is a 2nd order stream with v-shaped valley form and 4-6% gradient. Stream size is 5&#8211;15ft (1.5&#8211;4.6m), averaging 11ft (3.4m). Sinuosity index is 1.3 (average) with many debris dams caused by windthrown trees falling into the stream. This riffle-pool stream had a pH of 7.3 and 45<sup>o</sup>F (7.2<sup>o</sup>C) temperature on September 25, 1995. Canopy coverage was estimated at 60&#8211;75% of the channel shaded by vegetation, which was mostly shrubs and conifers.</p>
					<p>Rabe compiled a list of 40 <a href="#specieslist">macroinvertebrate taxa found in the RNA</a>. A low biotic index was calculated from these data, indicating the macro invertebrate community is quite intolerant of poor water quality conditions.</p>
					<p>In his summary, Rabe notes that lower Canyon Creek would be a good choice as a reference site for comparison with impacted streams of similar size and flow.</p>
