					<p>The headquarters site at <acronym title="Priest River Experimental Forest">PREF</acronym> consists of an office/lab building, three residential buildings, a bunkhouse/mess hall, and modest shop. A full-time resident and employee occupy a fourth residence. All were constructed by Civilian Conservation Corps between 1934 and 1939 and are listed in the National Register of Historic Places. The Gisborne fire lookout is listed on the National Historic Lookout Register. A conference building, with a capacity of 50, was constructed in 1998. A detailed archaeological survey of PREF was conducted in 1978 and is on file at the Moscow Forestry Sciences Lab.</p>
					<p>During the field season, PREF averages 1000 days use by Forest Service personnel and their cooperators. The residential buildings and bunkhouse have a capacity of 27 beds. During the winter months, a cabin and the bunkhouse/mess hall are shut down, reducing bed space to 18. The residential buildings are ADA compliant and provide visitors with full kitchen facilities and linen.</p>
