					<p>In general terms, the Priest River-northern Rocky Mountain climate is transitional between a northern Pacific coastal type and a continental type.</p>
					<p>The Pacific influence is noted particularly by the late autumn and winter maximum in cloudiness and precipitation; also in the relatively moderate average winter temperatures, compared with areas east of the continental divide. Summer is characteristically sunny and dry, though July and August are the only distinct summer months (Finklin, 1983).</p>
					<p>The average annual (water year) precipitation at the Forest headquarters is 817mm (32.17in). At higher elevations, precipitation slightly increases as noted by the Benton Spring rain gage (elev. 1455m), which has an annual average of 925mm (36.42in). The wettest months are November through January and close to 60% of the annual total occurs from November through March. Snowfall accounts for more than 50% of the total precipitation at elevations above 1400m. Snow cover usually persists in the valleys from early December through the end of March; the seasonal maximum depth averages 49cm (19.29in). High elevation snowpack can reach an average depth of 132cm (51.97in) at the end of March and sometimes remain into June.</p>
					<p>The monthly mean temperatures at the headquarters range from -4<sup>o</sup>C (25<sup>o</sup>F) in January to 18<sup>o</sup>C (64<sup>o</sup>F) in July; these are midpoint values between the average daily max and min temperatures based on a 1700-hour observation time. The annual mean temperature is 7<sup>o</sup>C (45<sup>o</sup>F). Extreme temperatures have ranged from a high of 40<sup>o</sup>C (103<sup>o</sup>F) to a low of -38<sup>o</sup>C (-36<sup>o</sup>F).</p>
					<p>The climate at PREF provides a transition between the strong maritime influence on the coniferous forests of the coastal Pacific Northwest domain and the dryer, colder interior flora found east of the continental divide. The long-term daily weather record, coupled with the snow pack record, allow researchers to document subtle changes in weather patterns. This is particularly relevant to stakeholders within the drainages of the Priest, Pend O'Reille, Snake, Clearwater, and Columbia River systems.</p>
