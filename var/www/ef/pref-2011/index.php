       <p>In 1911 the Priest River Experimental Forest (PREF) was among the first sites in the United States to be set aside by the USDA Forest Service for the purpose of conducting forest research.
  PREF also served as the headquarters for the District 1 (now Region 1) investigative program until 1930 when PREF was incorporated into the Northern Rocky Mountain Forest and Range Experiment Station, headquartered in Missoula, Montana.
       <p>Since the establishment of the Priest River Experimental Forest, numerous educators, Forest Service researchers, and state and private forestry personnel have used the Forest. Research conducted by J.A. Larsen, Harry Gisborne, Ken Davis, Charles Wellner, and Irvine Haig provided information on basic forestry principles still used today for managing Rocky Mountain forests. These and other researchers throughout the Forest's history have made it a key location for conducting studies of forest ecosystems of the northern Rocky Mountains.</p>
       <h2>Introduction</h2>
        <p>PREF contains approximately 2,758 ha (6,368 ac.) of mountainous forestland, with small areas of talus and alpine grassland. Within its boundaries is a diversity of forest successional stages that are representative of forested ecosystems found in the northern Rocky Mountains. There are two Research Natural Areas, Canyon Creek, 395 ha (977ac.), which was established in 1937, and Wellner Cliffs, 125 ha (310ac.), established in 2006. At the headquarters area, there is housing, laboratory space, and a conference building. Long-term data sets of daily weather (1911), snow pack (1937), tree growth (1914) and streamflow (1938) have been collected and archived. PREF became a monitoring site (site # ID02) of the National Atmospheric Deposition Program (NADP) in 2003.</p>
	<p>The site contains a variety of managed and reserve areas, a multitude of ecological site choices from wild to extremely disturbed situations, and all of the significant forest types and habitat associations of the region.</p>
	<h2>General</h2>
	<p>In terms of climate, topography, flora, and fauna, the Northern Rocky Mountains is a diverse region. Within this context, PREF is an excellent representation of the forested ecosystems that are common to an area west of the continental divide and north of the Snake River Plain.</p>
	<p>The Forest lies on the westward slope of a spur of the Selkirk Mountains. Elevation varies from 671m at the Priest River on the west boundary, to 1798m at Experimental Point, 9km to the east. Mountainous land occupies approximately 90% of the area. This high land is cut by a number of streams flowing from east to west, leaving between them ridges which run in a general east and west direction. The ridges are further dissected by tributary drainages. Thus, though the main slopes are those facing north and south, there are also some of considerable extent having east and west exposures. The slopes range from moderately steep to very steep. Priest River cuts across the southwest corner of the area and receives the drainage of Benton Creek, Canyon Creek, and Fox Creek.</p>
