	<h2 class="breaktext">Location</h2>
	<p>
         Road access to PREF is by State Highway 2 from Spokane, Washington to Priest River, Idaho.
         From Priest River, drive north on State Highway 57 for 3 miles, then north on Peninsula Road, a Bonner County road
         (5 miles pavement and then 5 miles of gravel) to reach PREF.
         Within PREF, the entry and forest roads are surfaced with gravel or native soils.
