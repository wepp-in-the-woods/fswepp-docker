					<p>The soils of the mountain slopes are underlain by rocks which grade on the coarse side into granites and imperceptibly into gneiss and schist. The major rock type appears to be gneiss. The schist contains prominent biotite and muscovite mica, and is thinner bedded than the gneiss. Soil surface horizons have formed in aeolian loess and volcanic ash. Surface textures are mostly silt loams, relatively fine-textured soils with low rock contents and high water-holding capacity.</p>
					<p>Soils at elevations up to 1,000m on southerly aspects are associated with shallow Saltese soils with minor amounts of deeper Jughandle soils on concave slopes of the drainages. Above this threshold, the Jughandle soils and associated phases of exposure, except the subalpine phase above 1400m, are mapped in the zone of maximum soil moisture and soil temperature effectiveness. Fertility is high on these sites. Above 1,400m, the colder soils predominate, though moisture is near maximum. South-facing slopes near this elevation and above contain the Horsehead soils which represent a prairie-forest transition to the subalpine environment.</p>
					<p>Moisture is usually limiting to high timber production on soils of the river terraces and lacustrine benches, and the south-facing slopes at lower elevations. Relatively high evaporation from prevailing southwest winds helps make these sites dry. The stream terraces and fans of Benton and Canyon Creeks have favorable run-in moisture from side slopes, and fertility and productivity are near maximum in the area. Soils of the middle elevations and on northerly, easterly, and westerly slopes have highest fertility and productivity associated with  favorable moisture-temperature relationships. Fertilizer experiments have demonstrated that nitrogen and, on metamorphic bedrocks, potassium deficiencies, may co-limit productivity. Soils of the high elevations have less favorable soil temperature and lowest fertility (McConnell 1965).</p>
					<p>The soils of PREF, Jughandle, Cabinet, Springdale, and Chamokane, are extensively mapped in forest lands in northern Idaho. Saltese and Horsehead soils have been mapped in Montana in the St. Regis and Ninemile area. A detailed geologic map of the Sandpoint Quadrangle, which includes the entire Priest River drainage, was completed in 1979.</p>
