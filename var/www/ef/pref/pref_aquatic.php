	<p>Two surveys of Canyon Creek have been conducted to characterize its physical, chemical, and biotic components.
         The first survey examined a section of the creek as it leaves the Canyon Creek Research Natural Area.
         Analysis (Rabe and Crazier 1993) of the upper reach of Canyon Creek provided physical and chemical descriptions and a survey of the macroinvertebrate community at the family level.
         At this study site, Canyon Creek is a first order perennial stream with an average gradient of 20%.
         The V-shaped gorge is bordered by an old growth forest consisting of western red cedar (<i>Thuja plicata</i>) and western hemlock (<i>Tsuga heterophylla</i>).
         The turbulent fast water habitats are predominantly cascades or small waterfalls over rock or woody debris.
         The chemical analysis revealed the nitrate/nitrite levels to be 0.2 mg/l and ortho phosphorus levels to be below detectable limits.
         Test results for ortho phosphorus, potassium, sodium, aluminum, iron, and zinc were below the detectable limits of the analysis.
         He also measured water temperature, pH, conductivity, turbidity, total dissolved solids, and fecal coliform.
        </p>
	<p>The second survey was conducted about 2.5 miles downstream (Rabe and Catts 1995).
         A low biotic index was calculated from these data, indicating the macro invertebrate community is quite intolerant of poor water quality conditions.
        </p>
	<p>In his summary, Rabe notes that lower Canyon Creek would be a good choice as a reference site for comparison with impacted streams of similar size and flow.</p>
