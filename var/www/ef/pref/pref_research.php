					<p>Early research at Priest River provided information on basic forestry principles still used today for managing Rocky Mountain forests. Throughout the Forest's history, it has been a key location for conducting forestry research. Regeneration studies using shelterwood, seedtree, and clearcut methods have provided information for regenerating mixed conifer forests. Site preparation, planting, cleaning, weeding, and thinning studies have provided information on how to regenerate and maintain forest stand composition and growth.</p>
					<p>Performance trials began in 1911 with a racial variation test of ponderosa pine, composed of seed sources from 22 locations throughout the Western United States. Growth characteristics of disease-resistant western white pine have been studied since 1955 and the Priest River has been the site of numerous common garden studies defining seed-transfer rules and zones for western conifers.</p>
					<p>Fire research began with the development of the first fire danger rating system. This was followed by studies on weather factors, fire behavior, fuel inflammability, and fire effects. This effort is the foundation for the current fire danger systems now used throughout the United States and Canada.</p>
					<p>Forest growth and yield has been studied on the forest since 1914. Several permanent plots on the Forest are remeasured at regular intervals. Information from these plots and other locations throughout the Northern Rocky Mountains is used to verify and test growth and yield computer models.</p>
					<p>Currently, studies are being conducted on ecosystem processes and functions. This includes the effects of management activities on long-term soil productivity, coarse woody debris function, atmospheric deposition, western white pine seedling development in canopy gaps, forest structure impacts on water yield and quality, white bark pine progeny trials, and carbon sequestration in mixed conifer forests.</p>
					<h2>Major Research Accomplishments and Effects on Management</h2>
					<p>The following major research findings were developed at Priest River:</p>
					<ul>
						<li>Rust-resistant western white pine</li>
						<li>Mapping of habitat types, development of the Prognosis computer model, forerunner to the present <a href="/gems/fvs.html">Forest Vegetation Simulator</a> (FVS)</li>
						<li>Beginnings of fire behavior, fire danger rating, and fire effects information</li>
						<li>Influence of forest structure on water yield and quantity</li>
						<li>Importance of coarse woody debris in maintaining forest productivity</li>
					</ul>

