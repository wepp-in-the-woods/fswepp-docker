					<p>The climatic, hydrological, and atmospheric data sets collected at PREF are public domain and available to interested parties. Raw data are archived at PREF and the Moscow Forestry Sciences Lab and is also available at several websites:</p>

					<h2>Control Weather Station (CWS):</h2>
					<p>Data (11/27/1911 to 12/31/2005) are available from several websites, under Priest River Experimental Forest, National Weather Service ID #107386.</p>
					<ul>
						<li><b>National Climatic Data Center (NCDC)</b> has the complete daily weather record available in a csv or pdf format.  Go to <a href="http://www.ncdc.noaa.gov/cdo-web">www.ncdc.noaa.gov/cdo-web</a> and search with the PREF station ID number.</li>
						<li><b>Western Regional Climate Center</b> has monthly averages and station metadata. Go to <a href="http://www.wrcc.dri.edu/cgi-bin/cliMAIN.pl?idprie">www.wrcc.dri.edu/cgi-bin/cliMAIN.pl?idprie</a>. </li>
					</ul>

					<h2>Snow Pack data sets:</h2> 
					<p>(WY 1937 to present) are available for the Benton Meadow (elev. 2380 ft, 725 m) and Benton Spring (elev. 4775 ft, 1455 m) snowcourses:</p>
					<ul>
						<li><strong>Natural Resource Conservation Service (NRCS)</strong> has the full record for both snowcourses. See <a href="http://www.wcc.nrcs.usda.gov/cgibin/state-site.pl?state=ID&report=snowcourse">www.wcc.nrcs.usda.gov/cgibin/state-site.pl?state=ID&report=snowcourse</a> and select the snow course of interest (Benton Meadow or Benton Spring).</li>
					</ul>

					<h2>Atmospheric Chemistry:</h2>
					<p>National Atmospheric Deposition Program (NADP) site was established in PREF on December 31, 2002. The site reference is <strong>ID02</strong>. Data can be found at the NADP web page at: <a href="http://nadp.sws.uiuc.edu/sites/siteinfo.asp?net=NTN&id=ID02">nadp.sws.uiuc.edu/sites/siteinfo.asp?net=NTN&id=ID02</a>.</p>

					<h2>Precipitation:</h2>
					<ul>
						<li>Benton Spring (elev. 4725 ft, 1440 m), monthly observations, transcribed into Excel spreadsheets from 1961 to present.</li>
						<li>Benton gauging dam (elev. 2640 ft, 805 m) spreadsheet format of daily and monthly totals from 1941 to present.</li>
					</ul>
					<h2>Stream Flow Data:</h2>
					<p>Digitized streamflow is available in a spreadsheet format in daily and monthly time intervals, 1939 to present, from the Moscow Forestry Sciences Lab.
                                           The flow data and metadata can be found in the <a href="http://www.fs.usda.gov/rds/archive/Product/RDS-2015-0006" target="arc">RMRS Archive</a> at http://www.fs.usda.gov/rds/archive/Product/RDS-2015-0006</p>

					<h2>Tree growth data:</h2>
					<p>available from permanent growth and yield plots, 1914 to present.</p>


