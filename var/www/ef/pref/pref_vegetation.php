					<p>About two thirds of PREF is covered in mixed conifer forest more than 100 years old. Most of this is timber in the 120 to 140-year age class (resulting from a fire ca. 1860). The remainder is in timber more than 200 years old. The other third of PREF is in non-stocked areas or in young timber on harvest units and burns that have occurred since PREF was established (Wellner 1976).</p>
					<p>The most detailed and intensive sampling of habitat types and cover types at PREF was reported by Daubenmire (1973) (also see Wellner 1976). A  <a href="pref_vascularFloraDaubenmire.pdf">species list of vascular flora collected and identified by Daubenmire</a> is found in Wellner's 1976 publication. Reconnaissance surveys and mapping have helped determine cover types and habitat types. Habitat types within PREF are best described as "complex" because of the extremely rapid changes in aspects and from wet to dry habitats. Plant species diversity is high due to the number of different habitat types that are intimately intermixed. Within the Wellner Cliffs RNA are plants indicative of wet habitats &#8212; western redcedar (<em>Thuja plicata</em>), wild ginger (<em>Asarum caudatum</em>), baneberry (<em>Actaea rubra</em>), and devils club (<em>Oplopanax horridum</em>). These occur within a few feet of dry habitats that support species such as ponderosa pine (<em>Pinus ponderosa</em>), lodgepole pine (<em>P. contorta</em>), Idaho fescue (<em>Festuca idahoensis</em>), and bluebunch wheatgrass (<em>Agropyron spicatum</em>) (Ferguson and Zack 2006).</p>
					<p>Mosses and lichens were inventoried along the lower section of Canyon Creek within the RNA in 2001. During this inventory, the moss <em>Ulota megalospora</em> was recorded for the first time in Idaho. A <a href="pref_lichensHays.pdf">list of lichens observed by Mike Hays at Wellner Cliffs RNA</a> was complied in July 1997.</p>
